	<p><strong>Donna Petersen</strong> is a professor of public health at the University of South Florida. She earned her masters and doctoral degrees in maternal and child health from the Johns Hopkins School of Hygiene and Public Health. She has held faculty and senior leadership positions at the University of Alabama at Birmingham School of Public Health, at the University of Minnesota School of Public Health, and served as Director of the Division of Family Health at the Minnesota Department of Health.</p>
    
