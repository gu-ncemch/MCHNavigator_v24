	<p><strong>Dr. Noel Chávez</strong> is an Associate Professor Emerita in the Community Health Sciences Division of the University of Illinois School of Public Health. Her research centers on the health and nutrition status of Latinos and other underrepresented groups, particularly that of children and families. Dr. Chávez’s research projects focus on Latino child health, including studying maternal environmental and dietary influences on subsequent development of asthma in children, dietary acculturation, family health and nutrition attitudes, and behaviors in Latinx families with young children.</p>
    
