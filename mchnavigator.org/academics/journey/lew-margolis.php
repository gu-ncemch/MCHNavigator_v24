	<p><strong>Dr. Lew Margolis</strong> is a veteran MCH educator who most recently was an associate professor in the Department of Maternal and Child Health at the UNC Gillings School of Global Public Health.	</p>
	<p>Dr. Margolis served on the faculty at the Gillings School for 28 years, during which he earned a reputation among MCH students as a passionate and dedicated teacher, mentor and advisor. He was especially committed to challenging students to think critically and consider the many sides of complex public policy issues. Margolis retired from teaching at the Gillings School in 2018, although his impact on the field continues as his many students advance in their MCH careers.</p>
    
