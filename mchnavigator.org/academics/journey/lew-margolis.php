    <p>Dr. Margolis started his career as a pediatrician. After developing an interest in the social determinants of health, he earned a master's in epidemiology at UNC. From there, he combined his passions for public health and the well-being of children to begin his career in MCH academe. He was a co-lead of the UNC MCH Interdisciplinary Leadership Development program which brought together students and faculty from five MCHB-funded training programs. He is the recipient of the Vince Hutchins Leadership Award from the Association of Maternal and Child Health Programs, as well as the Edward G. McGavran Award for Excellence in Teaching and the Department of Maternal and Child Health Teaching Award.</p>			
			
			
