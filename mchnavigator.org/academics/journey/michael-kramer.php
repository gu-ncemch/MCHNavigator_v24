	<p><strong>Dr. Michael Kramer</strong> is a social epidemiologist, health disparities scholar, and the Director of the Emory Center of Excellence in Maternal and Child Health Education, Science, and Practice. He was also a HRSA MCHB trainee himself. His research seeks to advance theory for the social determinants of health, develop and implement novel methods for measuring and quantifying the biological embodiment of social experiences and environments, and advancing impactful science focusing on improving the health of women and children. His research has been supported by NIH, HRSA, CDC, and March of Dimes. Current projects focus on building the evidence base to inform social and community policies to eliminate disparities in severe maternal morbidity and mortality. He currently serves as the co-chair of the Georgia maternal mortality review committee.</p>
