<h2>MCH Public Health Academic Journey: From PhD to Legacy</h2>
	<p><img src="../../images/mch-legacy.jpg" width="300" height="200" alt="Decorative image of story telling" class="right border" />This project documents the unique paths of MCH leaders in academe, focused on five key questions. The hope of this project is to preserve these experience and the wisdom from the field that each expert brings and also to inspire a new generation of MCH academics.</p>
    
	<p><strong>Trish Elliott, DrPH</strong> is a Clinical Associate Professor of Community Health Sciences dedicated to training future practitioners of public health. She teaches courses in maternal child and adolescent health, leadership, and program management. Dr. Elliott uses a combination of simulation, case method, and practice-based learning in the classroom and is committed to using innovative approaches in academic-practice collaboration. Dr. Elliott directs the Practice Fellowship Program of the Maternal and Child Health Center of Excellence, connecting exceptional master's degree students with year-long placements in public health organizations, and leads a multi-state initiative to facilitate co-learning for public sector MCH workforce development.  She serves as the Director of the Doctor of Public Health program, a school-wide doctoral degree in leadership, management, and policy, preparing students for high-level positions in public health practice, and as the Director of Education for the Department of Community Health Sciences.</p>
    
	
	
    <div style="display: flex; flex-direction: column;" class="right">
    <img src="../../images/trish-elliott.jpg" alt="Trish Elliott" class="ppic right" /></div>
    
   <p>Dr. Elliott&rsquo;s academic and practice interests focus on using implementation science, systems science, and process evaluation methods to improve the health of vulnerable populations. She is particularly interested in adolescent and family access to health services, the intersection of mental health and juvenile justice, school health, academic-practice partnerships, and health policies at the local, state, and federal level. Dr. Elliott joined the department of Community Health Sciences as faculty in 2013, after teaching for four years in an adjunct capacity. Prior to her work in academe, she spent ten years in management within the health sector overseeing complex projects.  She received a BA in Psychology from Colby College, a MPH degree with dual concentrations in Social and Behavioral Sciences, and Maternal and Child Health from Boston University where she also completed her DrPH in Maternal and Child Health.</p>
	 
    
