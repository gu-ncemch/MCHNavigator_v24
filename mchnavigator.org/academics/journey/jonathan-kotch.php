	<p>His expertise in health and safety in out-of-home child care has resulted in national training programs for child care health consultants across the country, and he was co-chair of the steering committee that produced the third edition of Caring for Our Children: National Health and Safety Performance Standards; Guidelines for Early Care and Education Programs.</p>
    
    <p>As the Principal Investigator of one of five local sites of the Longitudinal Studies Consortium on Child Abuse and Neglect, Dr. Kotch was responsible for the North Carolina cohort, studied from birth until age 18. He was PI for all 5 sites and the coordinating center of LONGSCAN's last two years. A follow-up funded by NIDA has just concluded (2019).</p>
    
