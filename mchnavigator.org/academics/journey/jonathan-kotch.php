<h2>MCH Public Health Academic Journey: From PhD to Legacy</h2>
	<p><img src="../../images/mch-legacy.jpg" width="300" height="200" alt="Decorative image of story telling" class="right border" />This project documents the unique paths of MCH leaders in academe, focused on five key questions. The hope of this project is to preserve these experience and the wisdom from the field that each expert brings and also to inspire a new generation of MCH academics.</p>
    
	<p><strong>Dr. Jonathan Kotch</strong>, a board-certified medical specialist in pediatrics and preventive medicine, was on the active faculty of the Department of Maternal and Child Health for 38 years. Currently emeritus research professor, he was the Carol Remmer Angle Distinguished Professor of Children's Environmental Health until he entered phased retirement in 2013. He edited the first three editions of <em>Maternal and Child Health: Programs, Problems, and Policy in Public Health</em> (Jones and Bartlett) and is the recipient of the Martha May Eliot Award (APHA), the John C. MacQueen Lectureship (AMCHP), the Academic Leadership Award (ATMCH), and the Young Professional of the Year Award (MCH Section).</p>
    
