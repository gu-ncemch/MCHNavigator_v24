	<p><strong>Cheri Pies</strong> is a clinical professor emerita at the UC Berkeley School of Public Health and served as the Principal Investigator for the Best Babies Zone Initiative from 2012 to 2019, a place based multi-sector approach to reducing infant mortality through community driven transformation. As one of the pioneers in understanding social determinants of health, she has worked to address the ways in which social and economic disparities and inequities influence birth outcomes and generational health across the life course. Dr. Pies received her MSW from Boston University in 1976. She earned her MPH (1985) and DrPH (1993) from the University of California at Berkeley.</p>
    
