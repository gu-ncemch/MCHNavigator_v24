<h2>MCH Public Health Academic Journey: From PhD to Legacy</h2>
	<p><img src="../../images/mch-legacy.jpg" width="300" height="200" alt="Decorative image of story telling" class="right border" />This project documents the unique paths of MCH leaders in academe, focused on five key questions. The hope of this project is to preserve these experience and the wisdom from the field that each expert brings and also to inspire a new generation of MCH academics.</p>
    
	<p><strong>Carol Hogue</strong> began her career as a social worker in Knoxville, Tennessee, and later moved to North Carolina in 1969 where she earned an MPH and PhD in Epidemiology (minor Biostatistics) from the University of North Carolina at Chapel Hill in 1971 and 1973.    Hogue is most widely known for using surveillance systems, developing psychosocial measurement of racial disparities, and conducting groundbreaking epidemiologic studies of perinatal outcomes.</p>
    
	<div style="display: flex; flex-direction: column;" class="right">
    <img src="../../images/Carol-Hogue.jpg" alt="Carol Hogue" class="ppic right" /></div>
   
   <p>At CDC, she developed national linkage of birth and infant death surveillance. Her analyses of this system revealed that high educational attainment was not protective against the racial gap in African American infant deaths and triggered further, ongoing research into discrimination as a risk factor for maternal and infant health. Hogue also led the development of CDC&rsquo;s Pregnancy Risk Assessment Monitoring System (PRAMS) that provides unique data for research, governmental programming, and evaluation. Hogue&rsquo;s study of the effects of induced abortion on subsequent fertility challenged accepted knowledge from case-control studies, and its methodology continues to be the gold standard to assess long-term health effects of abortion. She served as Lead Epidemiologist for the Stillbirth Collaborative Research Network&rsquo;s population-based case-control study of stillbirth. In 1992 Hogue was appointed to the Jules and Uldeen Terry Chair of Maternal and Child Health at Emory University&rsquo;s Rollins School of Public Health and established the Women&rsquo;s and Children&rsquo;s Center.</p>
			
			
