<p>Using ATMCH Innovative Teaching Award (ITA) funds, this team created a graduate-level teaching and learning resource that aimed to (1) identify and analyze MCH data, (2) communicate MCH data through data visualizations and products that meet end-user needs, and (3) create action steps that link the data to practice and/or policy. The resource includes lecture materials, assignments, and evaluation materials.</p>
