		  <h2>MCH CELL: Linking Academia and Community through Practice-based Teaching: A Case Study of a Communication Strategies Course and its Impact</h2>
          
          
<p>In Workshop #4 of the <a href="https://www.mchnavigator.org/academics/atmch/innovative-teaching-awards/mch-cell.php" data-saferedirecturl="https://www.mchnavigator.org/academics/atmch/innovative-teaching-awards/mch-cell.php">MCH  CELL for Community-Engaged Learning Lab (MCH CELL)</a> series, we introduce how a five-step framework (PBT STEPS*) was used to guide the development of an MPH course on  the design, implementation, and funding of an environmental-level intervention and communications campaign for community trauma in Winthrop, Massachusetts. The workshop describes the evolution of a partnership between the Winthrop Department  of Public Health and Clinical Services (WDPH) and Boston University School of Public Health, and walks through each phase of PBT STEPS as applied to the community trauma example including:   <u></u><u></u></p>
<ul type="disc">
  <li><strong>S</strong> - Securing partnerships with WPHD and configuring the semester to result in success   </li>
  <li><strong>T</strong> - Technology and training to ensure active engagement through different technologies  </li>
  <li><strong>E</strong> - Engagement and implementation of the course including tailoring deliverables </li>
  <li><strong>P</strong> - Presenting course deliverables (papers and communication materials) and a grant for funding </li>
  <li><strong>S</strong> - Sizing up results including qualitative data on the utility, benefits, and challenges of the course and quantitative data through the learning management system and surveys from an alumni perspective.  </li>
</ul>
