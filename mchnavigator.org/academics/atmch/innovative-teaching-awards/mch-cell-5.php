  <h2>MCH CELL:  The Art and Science of Teaching and Learning with Community Partners: A Field–Based Course in Consultation and Team Leadership</h2>
  <p>In Workshop #5 of the <a href="https://www.mchnavigator.org/academics/atmch/innovative-teaching-awards/mch-cell.php" data-saferedirecturl="https://www.mchnavigator.org/academics/atmch/innovative-teaching-awards/mch-cell.php">MCH  CELL for Community-Engaged Learning Lab (MCH CELL)</a> series, we  highlight a graduate-level public health course that combines the art and science of team-based leadership and consulting with  community organizations, offered to MPH students at Boston University's School of Public Health, Department of Community Health  Sciences. In the first half, we  present the history and structure of the course, the iterative assignments that lead to the ultimate report and deliverable for the client, and feature frameworks and tools that support the course  and that students can carry with them.  The presentation  emphasizes ways to foster healthy, productive teamwork and authentic, relationship-building with community partners. In the second part of the webinar, a panel of clients and alums  of the course  share their experiences and engage in a conversation about the challenges and successes from multiple perspectives. </p>
