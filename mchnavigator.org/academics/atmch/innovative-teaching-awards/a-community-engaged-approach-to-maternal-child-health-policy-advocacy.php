<h2>A Community-Engaged Approach to Maternal Child Health Policy &amp; Advocacy</h2><p><img src="../../../images/box-5.jpg" alt=" " class="right border" /></p>

<p>Developed by:</p>
<p><strong>Molly McNulty, JD</strong> – Assistant Professor of Public Health, University of Rochester</p>
<p>These ITA funds were used to develop a new course, <em>Maternal Child Health Policy and Advocacy</em>, with 3 aspects: (1) a legal approach to teaching MCH policy, (2) a community-engaged partnership with 2 policy and advocacy agencies, and (3) a focus on undergraduates. This course melds standard MCH coursework about policies and programs with the added overlay of a law and policy-making framework and a community-level perspective on policy and advocacy. The course is structured following a political science model known as the &ldquo;stages framework of policy-making&rdquo;. </p>
