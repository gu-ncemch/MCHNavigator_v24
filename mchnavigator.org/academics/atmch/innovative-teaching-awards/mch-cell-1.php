		  <p>In Workshop #1 of the <a href="https://www.mchnavigator.org/academics/atmch/innovative-teaching-awards/mch-cell.php" data-saferedirecturl="https://www.mchnavigator.org/academics/atmch/innovative-teaching-awards/mch-cell.php">MCH  CELL for Community-Engaged Learning Lab (MCH CELL)</a> series, this discussion and workshop, led by long-time community leader and advocate, Ms. Marylyn Creer, and Boston University&rsquo;s Clinical Associate Professor of Community Health Sciences, Candice Belanoff,  offers up best-practices, strategies, and lessons learned for the development and implementation of a community-engaged course on needs and assets assessment. For the first hour of the session, participants had the opportunity to hear from Marylyn and Candice (see below for highlights), and then we  hosted a workshop space (unrecorded), giving participants the opportunity (in smaller break-out groups) to critically examine their own experiences and practices, discuss potential barriers to authentic community partnerships, and leave with new ideas to try out in the field. </p>
