<h3>Competency Proficiency for Results Orientation, Self-Management, External Awareness</h3>
<ul>
  <li><strong>Results Orientation:</strong> Set and achieve challenging goals that focus on desired results.
    <ul>
      <li>Develop standards against which to measure behavior and performance</li>
  <li>Measure performance results against goals/objectives aligned with organizational strategic plan</li>
  <li>Take risks, face fears, and demonstrate a strong sense of urgency about achieving goals</li>
  <li>Persist in the face of significant difficulties to achieve goals</li>
  <li>Set achievable goals</li>
  <li>Set challenging goals</li>
  <li>Strive to achieve goals</li>
  <li>Strive to meet expectations of others</li>
  <li>Use resources effectively to achieve desired outcomes</li>
  <li>Seek innovative ways to improve individual and organizational performance</li>
</ul>
  </li>
  <li><strong>Self Management:</strong> Work independently and proactively, accept responsibility, fulfill commitments, and complete assignments.
    <ul>
      <li>On-time, ready, and consistently engaged in the work of HRSA</li>
  <li>Effectively manage workload with appropriate level of supervision</li>
  <li>Seek continued learning and improvement, including application of technologies, as appropriate, to increase productivity</li>
  <li>Regularly analyze systems, processes, and performance trends to identify opportunities for improvement</li>
  <li>Identify skills to be developed</li>
  <li>Analyze costs, benefits, risks, and chances for success of potential solutions</li>
</ul>
  </li>
  <li><strong>External Awareness:</strong> Remain aware of trends, issues, events and organizations outside one&rsquo;s program and HRSA, and of effects of those forces on the work of HRSA.
    <ul>
      <li>Keep up-to-date on events at the national, state, or local level that do or may have an impact on one&rsquo;s program, grantees, or stakeholders, and assess potential risks or opportunities</li>
      <li>Maintain understanding of the needs, issues, and objectives of important stakeholders, customers, and partners</li>
      <li>Partner, negotiate, and educate others regarding program objectives and needs</li>
      <li>Anticipate and mitigate the impact of local, state, and national political forces and trends</li>
      <li>Maintain sensitivity to the context of one&rsquo;s actions on grantees, customers, and stakeholders to ensure a positive and accurate representation of HRSA&rsquo;s work</li>
      <li>Identify and pursue opportunities for collaboration</li>
      </ul>
  </li>
  </ul>
<h3>Competency Proficiency for Resilience/Flexibility, Leading People, Decisiveness</h3>
<ul>
  <li><strong>Resilience and Flexibility: </strong> Respond quickly and professionally to change and easily consider new approaches in stressful and difficult situations.
<ul>
      <li>Remain open to new ideas, perspectives, structures, strategies, or assignments</li>
      <li>Support new ideas, systems, and procedures</li>
      <li>Maintain work efficiency, support others, and respond constructively to a changing environment, even when setbacks occur</li>
      <li>Take positive steps to understand reasons for change in environment or tasks</li>
      <li>Promptly change strategies or tactics if the current ones are not working by coming up with creative/innovative ideas and actions</li>
      </ul>
  </li>
  <li>
    <p><strong>Leading People: </strong> Assure achievement of organizational objectives through effective recruitment and selection, development, assessment, motivation and inspiration, assignment, and management of staff members. <em>(Note: may be only applicable to supervisors, managers).</em></p>
    <ul>
      <li>Make accurate assessments of staff members, assigning them to work and to groups in ways that take advantage of capabilities, address interests, provide for development in areas of need</li>
      <li>Conduct performance reviews consistent with HRSA requirements, and provide clear and accurate feedback to each employee regarding their strengths, performance improvement needs, and status regarding  expected level of performance</li>
      <li>Take action consistent with policy and procedures when employee performance or conduct is not consistent with requirements and standards</li>
      <li>Provide opportunities through informal and formal work assignments, team assignments, and developmental or training opportunities, both group and individual, for employees to broaden and deepen workplace competencies</li>
      <li>Develop effective team approaches by modeling open, positive, trustful interactions, providing clear feedback, and setting and enforcing expectations for shared responsibility, work, and credit</li>
      <li>Encourage constructive, creative differences of opinion; Anticipate and take steps to prevent counter-productive confrontations/workplace violence; Manage and resolve conflicts and/or disagreements in a positive, constructive manner</li>
      <li>Build a workforce based on organizational goals, budget considerations, and staffing needs;  Ensure employees are recruited and selected (includes external hiring and internal merit promotion and placement) appropriately</li>
      </ul>
  </li>
  <li><strong>Decisiveness: </strong> Exercise good judgment by making sound and well informed decisions; perceive the impact and implications of decisions; make effective and timely decisions, even when data is limited or solutions produce unpleasant consequences; is proactive and achievement oriented.
<ul>
      <li>Gather available and pertinent information that should be considered in reaching a decision</li>
      <li>Obtain the input of appropriate people/organizations to enhance the quality and timing of a decision</li>
      <li>Foresee and evaluate the short and long-range consequences of different options, particularly those involving personal and/or organizational risks</li>
      <li>Inform appropriate people/organizations of decision(s) to assure all stakeholders are aware and not caught off guard</li>
      <li>Is able to satisfactorily justify/defend and stand behind decision(s) made</li>
      </ul>
  </li>
  </ul>
<h3>Competency Proficiency for Strategic Thinking, Leveraging Diversity, Influencing/Negotiating</h3>
<ul>
  <li><strong>Strategic Thinking:</strong>  Formulate objectives and priorities, and implement plans consistent with the long-term interests and view of the organization in a global environment.  Build and influence a shared vision with others, acting as a catalyst for organizational change.  Capitalize on opportunities and manage risks.
<ul>
      <li>Ensure that the organization&rsquo;s strategic plan and business practices are consistent with its vision and mission</li>
      <li>Translate strategy into business plans and tactics, assisting others with incorporating the vision and mission into day-to-day activities and behaviors</li>
      <li>Modify business plans based on changing business conditions, through an understanding of industry and market trends</li>
      <li>Develop and explain organizational values, principles, and competencies that support the mission and vision to others within and outside the organization</li>
      <li>Inspire others by modeling behaviors that support the organization&rsquo;s vision through an appreciation of the organization&rsquo;s long-term sustainability and competitiveness</li>
      </ul>
  </li>
  <li>
    <p><strong>Leveraging Diversity: </strong> Foster an inclusive workplace where diversity and individual differences are respected, valued, and leveraged to achieve the vision and mission of the organization.</p>
    <ul>
      <li>Lead and/or manage an inclusive workforce in an environment that maximizes the talents of each individual in an equitable manner  to achieve sound business practices and results</li>
      <li>Respect, understand, value, and solicit individual differences in the workplace to achieve the vision and mission of the organization</li>
      <li>Develop and use measures and rewards to hold self and others accountable for achieving results that embody the principles of diversity and inclusiveness</li>
      <li>Develop and/or promote policies, procedures, and values that encourage diversity awareness and acceptance</li>
      <li>Use an understanding and appreciation of cultural differences to communicate, influence, and manage, including conflict resolution, across cultures</li>
      </ul>
  </li>
  <li>
    <p><strong>Influencing/Negotiating: </strong> Use persuasion to gain the support and cooperation of others, e.g., colleagues, supervisors, stakeholders, to achieve a desired course of action consistent with the organization&rsquo;s strategic goals and objectives.</p>
    <ul>
      <li>Present one&rsquo;s point of view in a way that enlists others&rsquo; support</li>
      <li>Anticipate reactions and/or objections and effectively respond using information or approaches that overcome major resistance or objections</li>
      <li>Identify ways to increase visibility and influence by participation in formal and informal activities</li>
      <li>Understand and use the interests, motivations, and agenda of others to build alliances and gain support for program and/or mission accomplishment</li>
      <li>Develop and present persuasive arguments to address the concerns, needs and desires of others, understanding  the roles individuals play in an organization and using that to achieve objectives</li>
      <li>Accurately anticipate changes or shifts in the political climate and initiate appropriate action(s) in relation to strategy or tactics to successfully accomplish objectives</li>
      <li>Understand and utilize the dynamics of power, organization, and decision making to achieve objectives </li>
    </ul>
  </li>
  </ul>


