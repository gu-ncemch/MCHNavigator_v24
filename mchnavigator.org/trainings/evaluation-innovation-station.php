			<h2>Evaluating a Practice</h2>
			<p>Evaluation helps you to understand what practices work, in which settings and why. Formally, evaluation involves the systematic collection of information about the activities and outcomes of practices to improve their effectiveness, inform decision making, and maintain accountability.</p>
			<p>The MCH Navigator has trainings based on the <a href="https://www.cdc.gov/eval/framework/">CDC’s Framework for Evaluation</a> that can help you evaluate your practice. Click the picture below to learn more:  </p>
			
			<div style="display:flex;">
			<a href="https://www.mchnavigator.org/trainings/evaluation.php" style="margin:1rem auto;"><img src="https://www.mchnavigator.org/images/logo.svg" alt="MCH Navigator logo" style="width:300px!important;" /></a>
			</div>
			
			<p>Here you will find examples of practices from Innovation Station that demonstrate each of the 6 Steps in the CDC’s Framework for Evaluation. Click to explore each step:  </p>
		
			
		<button class="accordion"> <strong>Step 1.</strong> Engage Stakeholders</button>
        <div class="panel">
		  
		  <h3>Definition:</h3>
		  <p>Stakeholders are involved in or affected by the program, as well as those interested in the outcomes of the evaluation. Stakeholders can be a part of developing the evaluation, and should be informed of results (<a href="https://www.cdc.gov/mmwr/PDF/rr/rr4811.pdf">CDC, 1999</a>)</p>
		  <h3>Innovation Station Example:</h3>
		  <p><em><a href="http://www.amchp.org/programsandtopics/BestPractices/InnovationStation/ISDocs/Barbershops%20and%20Period%20of%20Purple%20Crying.pdf">Using Barbershops to teach Period of PURPLE Crying/Infant Development</a></em>. This practice engaged stakeholders (specifically barber shop owners/customers) in evaluating program delivery. They tested the Period of PURPLE Crying curriculum to make sure it resonated with barber shop customers, and worked with barber shop owners to see how delivering the curriculum could fit in their existing workflow</p>
		  
        </div>
            <button class="accordion"> <strong>Step 2.</strong> Describe the Program</button>
        <div class="panel">
			
            <h3>Definition:</h3>
            <p>As outlined by <a href="https://www.cdc.gov/mmwr/PDF/rr/rr4811.pdf">CDC</a>, describing the program involves stating the need for the program, the expected effects of the program, the program activities and resources needed to conduct it, the stage the program is in (early development or later phases), the setting or context it is being delivered in, and the program’s logic model.</p>
            <h3>Innovation Station Example:</h3>
            <p>North Carolina’s <a href="http://www.amchp.org/programsandtopics/BestPractices/InnovationStation/ISDocs/NC%20IA%20Implementation%20Handout_Final.pdf">Innovative Approaches: Community Systems Building Grants for Children and Youth with Special Health Care Needs</a> includes clear objectives and a model outlining the program’s theory of change. Objectives include: : (1) to thoroughly examine the community system of care for CYSHCN; (2) to facilitate community identification of sustainable system changes and promising practices; and (3) to coordinate the implementation of these practices with agencies, providers, and families in the community.</p>
			
        </div>
		
            <button class="accordion"> <strong>Step 3.</strong> Focus the Evaluation Design</button>
        <div class="panel">
          
		  <h3>Definition:</h3>
		  <p>This involves thoughtfully planning an evaluation strategy, including thinking about the evaluation’s purpose, users, and uses; the evaluation questions; methods; and partners in the evaluation process along with agreements necessary to work with those partners. (<a href="https://www.cdc.gov/mmwr/PDF/rr/rr4811.pdf">CDC, 1999</a>)</p>
		  <h3>Innovation Station Example:</h3>
		  <p><a href="http://www.amchp.org/programsandtopics/BestPractices/InnovationStation/ISDocs/Implementation%20Handout_Pono%20Choices%202019.pdf">Pono Choices: A Culturally Responsive Teen Pregnancy and STI Prevention Program</a> involves a carefully-planned randomized controlled trial evaluation strategy involving collaboration with internal partners and external contractors.</p>
			
        </div>
		
          <button class="accordion"> <strong>Step 4.</strong> Gather Credible Evidence</button>
        <div class="panel">
            
			<h3>Definition:</h3>
			<p>Gathering credible evidence depends on your stakeholders and the audience for your evaluation results. While certain stakeholders may see quantitative data as valuable, others may value stories of success, or both. (<a href="https://www.cdc.gov/mmwr/PDF/rr/rr4811.pdf">CDC, 1999</a>)</p>
			<h3>Innovation Station Example:</h3>
			<p>The South Carolina <a href="http://www.amchp.org/programsandtopics/BestPractices/InnovationStation/ISDocs/PASOs_2015.pdf">PASOS Program</a> collected evaluation data from pre- and post-tests as well as qualitative data in the form of women’s stories to demonstrate their impact</p>
			
						
		</div>
            <button class="accordion"> <strong>Step 5.</strong> Justify Conclusions</button>
        <div class="panel">
			
			<h3>Definition:</h3>
			<p>Justifying conclusions can include comparing evaluation results to standards, analyzing and synthesizing results, interpreting meaning from synthesized findings, and making judgements about a program’s success or need for improvement. (<a href="https://www.cdc.gov/mmwr/PDF/rr/rr4811.pdf">CDC, 1999</a>)</p>
			<h3>Innovation Station Example:</h3>
			<p>The <a href="http://www.amchp.org/programsandtopics/BestPractices/InnovationStation/ISDocs/Pathways%20Community%20HUB.pdf">Pathways Community HUB</a> program uses evaluation results to make conclusions about program performance and has implemented a process of continuous quality improvement to implement changes. Check out the evaluation and lessons learned section. </p>

		</div>
		
            <button class="accordion"> <strong>Step 6.</strong> Ensure Use and Share Lessons Learned</button>
        <div class="panel">
			
			
			
			<h3>Definition:</h3>
			<p>Evaluation findings should be shared and translated into action. The five elements of ensuring use and sharing lessons are: 1) evaluation <em>design</em> to understand who the target users of the results will be and how stakeholders will use the evaluation 2) <em>preparation</em> to consider how findings might impact the program (positively or negatively) 3) <em>feedback </em>from stakeholders and partners throughout 4) <em>follow-up</em> to ensure that evaluation results and lessons learned are not lost and to ensure that findings are not mis-used 5) <em>dissemination</em> of the results to stakeholders. (<a href="https://www.cdc.gov/mmwr/PDF/rr/rr4811.pdf">CDC, 1999</a>)</p>
			<h3>Innovation Station Example:</h3>
			<p>The <a href="http://www.amchp.org/programsandtopics/BestPractices/InnovationStation/ISDocs/Oral%20Health%20Home%20Visiting.pdf">Improving Oral Health Outcomes for Pregnant Women and Infants by Educating Home Visitors</a> practice in Virginia conducted pilot studies. Positive evaluations from the pilot studies were used to spur policy change and expansion of training programs.</p>

