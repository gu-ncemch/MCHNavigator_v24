<h2>What is Policy? What is the Policy-Making Process?</h2>
	<p><a href="evaluation-intro.php"></a><img src="../images/policy-thumb.jpg" width="300" height="200" alt="A collage that represents policy and advocacy" class="right border" />It is important for MCH leaders to possess policy skills, particularly in changing and competitive economic and political environments. MCH leaders understand the resources necessary to improve health and well-being for children, youth, families, and communities, and the need to be able to articulate and educate for those needs in the context of policy development and implementation.	</p>
	<p>MCH leaders should have a firm understanding of the policy process, because they often must defend and educate on MCH resources in competitive economic and political environments.	However,	data	from the MCH Navigator online self-assessment shows that professionals' understanding of the knowledge and skills that are necessary to incorporate policy into daily work are consistently lacking.</p>
			<p><strong><img src="../images/ardenhandler.jpg" alt="Arden Handler" class="ppic right" /></strong>This learning opportunity is a lecture from a course developed and presented by <strong>Arden Handler, DrPH</strong>, Professor, Community Health Sciences, Director, Center of Excellence in MCH and Co-Director, MCH Epidemiology Training Program at the University of Illinois at Chicago. In the presentation, she outlines the terminology used in public policy and the types of public policy as they are practiced; the process and paradigms of making public policy, including the legislative process; and the role of economics in the policy-making process. She rounds off this lecture with an analysis of the most common public policy instruments.</p>
			<p>Scroll down to access the 5 focused learning sessions of Dr. Handler's presentation followed by additional learning opportunities from the MCH Navigator and supplemental resources from the National Center for Education in Maternal and Child Health (NCEMCH) to put knowledge into practice.</p>
