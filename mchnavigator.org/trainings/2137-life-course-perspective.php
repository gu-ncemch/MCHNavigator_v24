    <div class="panel">
          <p>The Maternal, Child, and Adolescent Health (MCAH) Life Course Perspective, Practice, &amp; Leadership course is designed to provide an understanding of life course perspective, its practical applications, and related leadership opportunities. The life course perspective is a conceptual framework for understanding health trajectories of populations over time. The life course perspective posits that broad social, economic, and environmental factors not only shape health and contribute to health outcomes but are also the underlying causes of inequities in a wide range of maternal and child health outcomes. This course first provides a brief summary of the development and central components of this perspective. Building off these foundational concepts, the course then focuses on practical applications of this perspective in both healthcare settings and public health interventions. Through interviews with leaders in the MCAH field, including clinicians, researchers, and public health practitioners, this course will highlight essential leadership knowledge and skills necessary to apply a life course perspective in practice.</p>
      <p>The course is self-directed, online, and open source, which allows participants to learn at their own speed and convenience free of charge. While the course is designed with clinical professionals and public health practitioners in mind, it is available to all learners including students and professionals in other fields.</p>
