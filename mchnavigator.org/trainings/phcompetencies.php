<p>Use
  this page to find learning opportunities related to specific knowledge and skill sets identified for each of the Core Public Health Competencies. These competencies complement the MCH Leadership Competencies and provide generalized knowledge, skills, abilities, and behaviors required for public health professionals to succeed in their roles, no matter what level they work in. The competencies are a consensus set of knowledge and skills for the broad practice of public health, defined by the <a href="https://www.cdc.gov/public-health-gateway/php/about/index.html">10 Essential Public Health Services</a>.</p>
<p>The Core Competencies have a long history in supporting workforce efforts in public health practice and academic settings. They were initially developed in 1991 by the <a href="https://www.phf.org/programs/council/Pages/default.aspx" data-domain-ext="org" target="_blank">Council on Linkages Between Academia and Public Health Practice<span aria-label=" external icon" role="img"> </span><span aria-hidden="true" alt="external icon"> </span></a>, a collaborative team of <a href="https://www.phf.org/programs/council/Pages/Council_on_Linkages_Members.aspx" data-domain-ext="org" target="_blank">national public health practice-based and academic organizations<span aria-label=" external icon" role="img"> </span><span aria-hidden="true" alt="external icon"> </span></a>. The current version was adopted on October 21, 2021.</p>
