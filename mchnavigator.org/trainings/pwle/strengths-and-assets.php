			  <p>Strength based leadership pays attention to everyone’s strengths and uses those strengths to support the project.
Asset-based thinking means you look at yourself and the world through the eyes of what is working, what
strengths are present, and what the potentials are. It is founded on the belief that communities and neighborhoods
thrive when built upon the knowledge, interests, and capacities of their residents, groups, and institutions.</p>
			  <p>Tips to generate ideas from everyone</p>
			  <ul>
			    <li> Begin each meeting with a not too personal ice-breaker.</li>
			    <li>Go around the group asking each person in the meeting to make a comment.</li>
			    <li>Use 1-2-4-all or pair shares as a technique to allow people time to formulate their ideas.</li>
			    <li>Consider general norms and agreements, like listening with an open mind, being non-judgemental, using&quot;I&quot; vs &quot;You&quot; statements, not interrupting, etc.</li>
			    <li>Invite the quieter people to take a particular role within the meeting, if they are comfortable.</li>
			    <li>Acknowledge the desire to hear from everyone openly in a meeting, and invite the more vocal people to<br>
			      try to speak less often.</li>
			    <li>Use Brainwriting when you think you want to use Brainstorming – it involves more ideas from everyone.</li>
			    <li>To keep communication open, invite everyone to share additional thoughts and ideas after the meeting -			    as this may be an option for quieter folks rather than having them speak during a meeting which may not			    be comfortable for them.</li>
		      </ul>
