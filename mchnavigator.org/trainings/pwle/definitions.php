			          <p>A person with lived experience has knowledge and expertise derived from components of their personal identity and perspective, such as parenting, pregnancy, living in poverty, having a disability, experiencing structural disadvantages such as racism, ableism, classism, etc., which is then employed in service to a population-level goal or objective. </p>
			          <p>In public health practice improvement, persons with lived experience can offer insight into the operations and impact of programs, services, policies and systems on themselves, their peers, and their communities. They can serve in multiple roles, including as peer mentors or coaches, partners in program and policy planning, implementation and evaluation, and as leaders and advocates for systems change. </p>
			          <p>Their knowledge and expertise can help ensure that programs and policies meet the real-world needs of the population served, assist in avoiding unintended negative consequences, and by extension improve the cost- effectiveness of decision-making. </p>
