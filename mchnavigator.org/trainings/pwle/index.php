	<p>People with lived experience (PWLE) are regarded as <em>&quot;experts by experience&quot;</em> in the scope of their first-hand experience.<sup>1 </sup> PWLE have lived (or are currently living) with  issues the community is focusing on and who can offer insight about the system as it is experienced by others. They  have: </p>
    <ul>
          <li>Knowledge that does not come from training or formal education.</li>
          <li>Deep understanding from their own experience in the community.</li>
          <li>Expertise with a system, process or issue.</li>
          <li>Understanding of what works to address the issue based on their personal insight, what does not work, and what resources (formal or informal) are available.<sup>2</sup></li>
    </ul>
    <p>Organizations that incorporate community members with lived experience are better equipped to make their services more focused, efficient, integrated, culturally appropriate, and sustainable.<sup>3</sup> </p>
        <p>This Learning Bundle is based on the tool, <strong><a href="../../documents/Successful-Engagement-with-People-who-have-Lived-Experience-October-2022.pdf">Successful Engagement with People who have Lived Experience</a></strong>, developed by Becky Burns of BBWorks and the National MCH Workforce Development Center (<a href="https://www.mchnavigator.org/documents/Successful%20Engagement%20Spanish.pdf">also available in Spanish</a>).</p>
        <p>The purpose of the tool and this bundle is to provide detailed steps for professionals who seek to authentically engage with PWLE to improve policy, services, supports, systems of care, and health outcomes. The document and videos below provide guidance for setting the stage in order to create trusting relationships and engage PWLE at the beginning of your work. </p>
