			  <p>Once a group is gathered, the group
creates the purpose moving forward.
Everyone can learn from others and when
this is a shared understanding then more
creativity is fostered.</p>
			  <p>The Agenda:</p>
			  <ul>
			    <li> Create opportunities for people who have lived experience to lead the process and create the agenda.</li>
			    <li>Review the agenda prior to a meeting with a small group that includes community representatives.</li>
			    <li>Share the process about how to get time on the agenda or who to talk with.</li>
			    <li>Have five minutes at the beginning or the end of the meeting for additional items or sharing thoughts or ideas.</li>
			    <li> Recognize the importance of the issues for the people with lived experience and have the agenda		      prioritize those issues.</li>
			    <li>Review and confirm mutually agreed upon “next steps” and “to do” items, especially if they have
			      been assigned to particular individuals, before concluding the meeting.</li>
			    <li>Set the next agenda at the end of the current meeting, including the date and time.</li>
			    <li>Request agenda items a few weeks in advance of the meeting.</li>
		      </ul>
			  <p>Behavior:</p>
			  <ul>
			    <li> Once a group has made a decision, honor that decision and see what you and others can learn from making that decision, even and especially if it is not the way you would have chosen.</li>
			    <li>Notice when you or others use ‘either/or’ language and make time to come up with more than two			    alternatives. When you stop after two ideas have been offered, you miss out on the third or fourth		      idea people were thinking about.</li>
			    <li> When a sense of urgency arises, slow down and encourage people to take a pause, a breath, restate
			      the goal, and dive deeper into alternatives.</li>
			    <li>Acknowledge that collaboration takes more time, particularly at the front end and yields a better result with higher buy-in and higher ability to take shared risks.</li>
			    <li>Recognize the power dynamics in the situation and respond thoughtfully.</li>
			    <li>Welcome discomfort and learn to sit with discomfort before responding or acting.</li>
			    <li>		      Leave rank and egos at the door. Only use first names and not titles.</li>
			    <li>Share group agreements or norms, avoid office talk, and avoid acronyms.</li>
			    <li>Include everyone in the process.</li>
		      </ul>
