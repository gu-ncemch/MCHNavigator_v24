			  <p>People with lived experience continue to have
opportunities to share their expertise, make a
difference, and are provided with new experiences
to hone their skills.</p>
			  <p>Current Participants:</p>
			  <ul>
			    <li> Publicly recognize their involvement.</li>
			    <li> Express gratitude, it goes a long way.</li>
			    <li>Acknowledge people’s participation in the final product.</li>
			    <li>Ask and support people with lived experience to share the product (at conferences, posters, etc.).</li>
		      </ul>
			  <p>Future Opportunities:</p>
			  <ul>
			    <li>Broaden the base of people with lived experience by repeatedly identifying new community members to			    be involved in projects or programs. Consider pairing newly involved individuals with more seasoned			    people with lived experience as mentors.</li>
			    <li> Provide technical assistance and institutionalize support mechanisms for people with lived experience to		      be engaged and supported as leaders.</li>
			    <li>Promote policies and practices to support engagement and leadership within organizations.</li>
			    <li>Be honest about the future of the project. Sometimes grants simply end and there are not future opportunities in the same way or with the same group.</li>
		      </ul>
