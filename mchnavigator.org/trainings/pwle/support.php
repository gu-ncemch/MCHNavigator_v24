<p>A key attribute of successful engagement is
                providing support to reinforce the experience
                of engagement and reduce disparities between
                people with lived experience and those
                representing an organization or program.</p>
<p><strong>Logistics</strong></p>
<p>Things a program
                pays or provides</p>
<ul>
                <li>Childcare</li>
                <li>Compensation for their
          expertise and time</li>
                <li>Mileage reimbursement /
          public transportation pass</li>
                <li>Remote access / Internet
          access / Broadband</li>
                <li>Email/Outlook account</li>
                <li>A location that is accessible
          and has good parking available</li>
                <li>Headphones &amp; Cameras</li>
              </ul>
<p><strong>Scheduling</strong></p>
<ul>
                <li>Start by asking people with
          lived experience about
          their availability.</li>
                <li>Consider meeting times in the
          early morning, over the lunch
          hour, evenings, or weekends.</li>
                <li>Identify a clear start
          time and end time and
          honor that decision</li>
              </ul>
<p><strong>Communication</strong></p>
<ul>
                <li>Determine why people with lived experience are at the table – to share their story, participate as an<br>
          equal member, just listen, provide feedback later, etc.?</li>
                <li>Offer mentorship and support for people with lived experience to learn how to best communicate their<br>
          expertise.</li>
                <li>Meet with community members 15-30 minutes prior to the meeting to orient them to the<br>
          discussions likely to take place and answer any questions they may have.</li>
                <li>Ask people with lived experience to contribute to the agenda. If you don't ask, their concerns and<br>
          priorities may not be addressed.</li>
                <li>Offer appropriate (non-jargon-y) background information on the topic.</li>
                <li> Include everyone on all communications related to the project.</li>
                <li>Provide an agenda and documents a week in advance to any meeting.</li>
                <li>Designate consistent team member connectors to support people with lived experience. Make sure the people with lived experience know how to connect with them before, during, and after the meetings.</li>
              </ul>
<p><strong>Comparable Compensation</strong></p>
                <p>For some people with lived
                experience, it is best to bring
                them on staff as a paid
                consultant. For others, they
                may either not want that
                level of commitment or
                financial reimbursement may
                impact other benefits. There
                are many ways to
                acknowledge and be
                reciprocal with people who
                have lived experience – ask
                them what works best.</p>
<p>Small acts of kindness:</p>
<ul>
                <li> Remember a participant's name and use it when		      you greet them.</li>
                <li>Say ‘thank you’ often.</li>
                <li>Be interested in others' personal lives and share		      your story too.</li>
                <li>Celebrate everyone's birthdays.</li>
                <li>Send heartfelt, personalized thank-you notes.</li>
                <li>Give holiday gifts, other than cash or gift card			    equivalents, with a low fair market value, such as		      an ornament.</li>
                <li>At meetings, offer occasional snacks (coffee, fruit,		      candy).</li>
              </ul>
<p>For an individual:</p>
<ul>
          <li> Offer to write letters of recommendation for school or future employers.</li>
                <li> Encourage volunteers to include their work			    with your organization on their resume.</li>
                <li> As appropriate, nominate volunteers for community awards and recognition.</li>
                <li> Offer plaques or similar mementos		      honoring their service.</li>
              </ul>

<p>For everyone:</p>
<ul>
  <li>Use special days e.g., ‘Volunteers Week’ or ‘Make a Difference Day’ to publicize their work and the time
    put into your projects.</li>
  <li>Annually organize a social event, outing, or party to say thank you.</li>
  <li>Post appreciation photos and videos highlighting volunteers on your organization’s social media site (get permission first).</li>
  <li>Write newspaper articles about volunteers’ group achievements.</li>
  <li>Celebrate major achievements, perhaps with an award ceremony to which you invite prominent local  people.</li>
  <li>Create and distribute a volunteer scrapbook.</li>
  <li> Mention the effort put in by your volunteers in any newspaper or radio coverage.</li>
</ul>
