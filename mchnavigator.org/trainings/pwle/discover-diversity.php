<p><strong>What do we know about different communities? </strong></p>
<p>&ldquo;The Essence of Culture&rdquo; https://www.yumpu.com/en/document/read/11717026/the- essence-of-culture-a-peek-at-hispanics-college-of-agriculture- </p>
<p>Key Take-aways: </p>
<ul>
        <li>The persevering processes African Americans use to face adversity include many character strengths, such as positive outlook (hope), spirituality, religiousness, meaning-making, forgiveness, expression of empathy (social intelligence), compassion (kindness), gratitude, and humility (Mattis et al., 2016). </li>
        <li>Latino/x/e and Hispanics, more than any other culture, have a high regard for family. When interacting with others, Latino/x/e and Hispanic prefer being closer to each other in space than non-Hispanic whites do. </li>
        <li>Asian-American traditions emphasize family solidarity, discipline, hard work and schooling. They value educational achievement, responsibility for relatives, and respect for authority. Along with respect for authority, there is also a strong sense of respect for the elderly. </li>
        <li>Native Americans emphasize a nonverbal communication style. Moderation is speech and avoidance of direct eye contact are nonverbal communicators of respect by the listener, especially for respected elders or authority figures. Traditional Native American Indian people are not rewarded for asking questions or verbally analyzing situations. Rather they are expected to learn through patience and observation. Native Americans usually speak softly and take ample time to reflect before responding. Direct confrontation is avoided because it disrupts the harmony and balance that are essential to being. </li>
        <li>The amount of space between you and the person you are talking to can say a great deal in the Middle East. It is common to sit very close to the person you are talking to. Not doing so may show that you are uninterested in the conversation and may offend the other person. </li>
        <li>The emphasis on personal relationships in Islam and the Middle-East mean that the person is valued greatly for whom he is and for what he does for the benefit of the whole, be it family or society. </li>
        <li>LGBT communities are defined by identities based on gender and sexual orientation. Their sense of culture emerges in their experiences in life due to core differences from the cultural norms surrounding gender and sexuality.</li>
        <li>The way people relate to one another in rural communities is more personal, emotional, direct and socially supportive. Everybody knows everybody. There is a feeling of belonging and fellowship. Even relationships with authority figures are softened or tempered by social constraints and niceties.</li>
      </ul>
