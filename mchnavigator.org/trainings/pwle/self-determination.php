			  <p>Self-determination is the process by which a
person controls their own life. They set their
own goals and can pick and choose why,
when, and where to share their lived
experiences to help redesign systems of care.</p>
			  <p>People with lived experience have the right to say
“No” without fear of retribution or a change in
their services or supports. Programs must respect
the boundaries of what a community member can
offer. Provide a graceful way for people with lived
experience to decline the opportunity.</p>
			  <p>People with lived experience must be able
to share experiences, especially negative
ones, without having to worry about
retribution or changes in their services.</p>
			  <p>Life happens to everyone and
circumstances change. It's important to
understand that not everyone, even staff,
may not be able to participate for the
duration of a project. Thank them for their
contributions to date and wish them well.</p>
