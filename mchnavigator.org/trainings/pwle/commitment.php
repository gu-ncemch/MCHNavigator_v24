			  <p>Building authentic relationships and creating an
environment of trust takes time. When the
process ends early, mistrust is created.</p>
			  <p>Support the Process:</p>
			  <ul>
			    <li>Everyone will deepen the relationship and			    continue to work together for the long haul,		      fostering an atmosphere of trust.</li>
			    <li>Provide infrastructure, technical assistance, and support. Invest in developing leadership skills and building capacity for community members to effectively address their current and future challenges. For example, partner with a family-led or community based organization to provide training.</li>
			    <li>Develop a culture of appreciation; take time to make sure that everyone's work and efforts are appreciated. Be specific about what someone did that you are grateful.</li>
			    <li>Ensure communication is ongoing and everyone is getting the same message. Decide as a group how information is going to be shared (email, text, what's ap, etc.)</li>
		      </ul>
			  <p>Evaluate the Process:</p>
			  <ul>
			    <li>Have a safe person who will check in with participants following the meeting to see how the meeting went.</li>
			    <li>Use Plus/Delta (+/&Delta; ) at the end of meetingsto evaluate the meeting.</li>
			    <li>Follow through and “close the loop.”</li>
			    <li>Dedicate funding for evaluation and dissemination of findings to monitor whether initiatives are working as intended and promote community buy-in and<br>
			      sustained investment in interventions.			  </li>
			  </ul>
