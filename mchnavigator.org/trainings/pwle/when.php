			  <p>The best time to engage is the moment you
realize you need others to impact a decision
and never after the decision has been made.</p>
			  <p>Inviting people with lived experience from the beginning of an activity or project is best practice. If you don't
invite the people served at the beginning, it deprives them of the relationship building that may have gone on
among the other program people who are working together. If invited later, people with lived experience will
need the back-story about the project when they join the discussion. If you invite people too early, they may
wonder why they are there and feel as though their time is being wasted or, worse yet, as though the invitation
was merely so the program could check a box. If you wait too long, then people may feel like their ideas do not
really matter. Programs must be intentional and thoughtful when engaging with the community.</p>
