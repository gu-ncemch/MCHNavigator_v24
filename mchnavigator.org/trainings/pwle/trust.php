			  <p>The more a program can be transparent, the
greater the trust will be with the community.
The greatest ideas for change are voiced in
an atmosphere of trust.</p>
			  <p><strong>“13 Behaviors of High Trust Leaders” by Stephen M. R. Covey</strong></p>
			  <p>Character</p>
			  <ul>
			    <li> Talk Straight – Be honest. Tell the			    Truth. Let people know where you			    stand in a polite and honest manner.</li>
			    <li> Demonstrate Trust - Care for others			    and show it. Treat everyone with			    respect, especially those who can’t
			      do anything for you.</li>
			    <li>Create Transparency - Tell the truth;			    be real, genuine, open and			    authentic. This may involve making			    meeting notes and decisions		      publicly available.</li>
			    <li>Right Wrongs - Admit when you’re			    wrong, apologize quickly, show		      humility, don’t let pride get in the		      way of doing the right thing.</li>
			    <li>Show Loyalty - Give credit to others,
			      speak about people as if they’re		      present and represent others who		      aren’t there.</li>
		      </ul>
			  <p> Competence </p>
			  <ul>
			    <li>Deliver Results - Establish a track record of			    getting the right things done. Make things			    happen, on-time and within budget. As Yoda
			      says, “Do or do not, there is no try.”</li>
			    <li>Get Better - Continuously improve. Increase			    your capabilities. Be a constant learner. Don’t
			      consider yourself above feedback.</li>
			    <li>Confront Reality - Tackle all issues head-on,
			      even the “undiscussables.” Address the tough		      stuff directly. Confront the reality, not the		      person.</li>
			    <li>Clarify Expectations - Disclose, reveal and			    validate expectations. Don’t assume they’re clear or shared. Renegotiate if needed/possible.</li>
			    <li>Practice Accountability - Hold yourself			    accountable first, others second. Take			    responsibility. Be clear on how you’ll			    communicate how you’re doing and how
			      others are doing. Don’t blame.			  </li>
			  </ul>
			  <p>Character + Competence</p>
			  <ul>
			    <li>Listen First - Listen			    before speaking.			    Understand, diagnose,		      listen with ears, eyes		      and heart.</li>
			    <li> Keep Commitments -			    Say what you’ll do, then			    do it. Make			    commitments carefully		      and keep them. Don’t		      break confidences.</li>
			    <li>			    Extend Trust - Extend			    trust abundantly to			    those who’ve earned it,			    conditionally to those
			      who are still earning it.</li>
		      </ul>
			  <p>Think about			    who is in the			    room. Your			    truth should not		      harm others.</p>
			  <p>Results +
			    Capabilities = Competence<br>
			    Intent +
			    Integrity =
		      Character</p>
			  <p>Other Tips</p>
			  <ul>
			    <li> Build in time to get to know each other as human beings who may have common interests or experiences.</li>
			    <li>Create a culture where people feel they can bring problems to the group; use meetings as a place to solve problems, not just a place to report activities. This can be encouraged by reporting on activities via email and using meeting time to discuss ideas and solutions.</li>
			    <li>Make sure everyone feels comfortable asking for help, sharing suggestions informally, or challenging the			    status quo without fear of negative social consequences. At times, people in traditional leadership roles are blind/deaf to power dynamics that others experience.</li>
			    <li>Learn to recognize times when you need to go off the planned agenda in order to address people’s underlying concerns.</li>
			    <li>Realize that being your own worst critic does not actually improve the work; if you are constantly
			      criticizing yourself in your relationships with others, you focus the attention on you, on support for you,		      rather than on the issue at hand.</li>
			    <li>Engage in the simple act of using &quot;I&quot; statements, which leads us to claim our own experience rather than generalizing from our experience in ways that can exclude those who have a different experience or perspective.<br>
		        </li>
		      </ul>
