			  <p><strong>Community Connectors</strong></p>
              <p>When building a new relationshipwith a community, finding a community connector is a good place
to start. It is often valuable to have a trusted community leader introduce you to a community. Think
about people you may already know through board or committee members.</p>
			  <ul>
			    <li>Spiritual or Faith Communities</li>
			    <li>Community Leaders</li>
			    <li>Cultural Events & Celebrations</li>
			    <li>Community Meetings</li>
			    <li>University Cultural Unions or Services</li>
			    <li>Community Centers (neighborhood, LGBTQ+, public health)</li>
		      </ul>
			  <p><strong>Other Avenues</strong></p>
			  <ul>
			    <li>Community Newsletter</li>
			    <li> Community Radio Shows</li>
			    <li>Community Health Center</li>
			    <li>Social Media / Listservs</li>
			    <li>Volunteer</li>
		      </ul>
			  <p>Example: An agency wanted to connect with African American/Black fathers of children with special health care needs.
The staff made a connection with a African American/Black faith community through attending a black history
course. It turns out that this community’s church had a men’s group. Through the spiritual leader, a message
was sent to the men’s group asking if any of them were fathers of children with special health care needs who
might be interested in helping out a service program. Through that process, four people with lived experience
were identified and introductions were made.</p>
			  <p>&quot;I don't know anyone, but I
know a program that has that relationship!&quot;</p>
			  <p>If you/your agency does not have the relationship or the connections to build a relationship, then start looking
at who already has the relationships. Find programs that are trusted in the community already and forge a
partnership with them to better meet the needs of the community. Think about health care, public health or
social service programs. <strong>Can you barter with a program to carry your message to the community?</strong> That is, offer
something that supports their program if they help carry your message to the community. Most often, both
programs can benefit from the partnership.</p>
			  <p>&nbsp;</p>
