		<p>Relationships with community members are more positive when their culture is known and honored. This includes current culture as well as historical aspects.</p>
		<p>Definitions according to the National Institutes of Health (NIH):</p>
		<ul>
			<li><strong>Cultural Intelligence
				</strong><br>
				The ability to make sense of unfamiliar contexts and then blend in.</li>
			<li><strong>Cultural Humility
				</strong><br>
				The lifelong self- reflection and self-critique whereby an individual
				learns about another's culture, by starting with an examination of their own beliefs and cultural identities.</li>
			<li><strong>Cultural Competence
				</strong><br>
				The ability to collaborate effectively with individuals from different cultures.</li>
		</ul>
