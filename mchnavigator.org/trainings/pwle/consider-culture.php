			<p><strong>Additional Cultural Considerations: </strong></p>
			<ul>
				<li>Muslims pray five times a day, with their prayers being known as Fajr (dawn), Dhuhr (after midday), Asr (afternoon), Maghrib (after sunset), Isha (nighttime), facing towards Mecca.</li>
				<li>Consider the use of Land Acknowledgement and Honor. Jewish holidays start at sunset the evening before the date on the calendar. Some holidays are observed for more than one day.</li>
				<li>Ramadan lasts a full month. It wouldn't be a good idea to schedule a luncheon or event during that time if possible. Think about how you describe the community – do you use terms like &quot;our&quot; or &quot;we&quot; or &quot;those people&quot; or &quot;these people&quot; vs. &quot;this community.&quot; </li>
			</ul>
			<p><strong>Cultural Considerations for Meetings: </strong></p>
			<p>&ldquo;<em><a href="https://hbr.org/2015/12/how-to-run-a-meeting-of-people-from-different-cultures">How to Run a Meeting of People from Different Cultures</a></em>&rdquo;</p>
			<p>Key Take-aways: </p>
			<ul>
				<li>It can be helpful to develop norms and agreements to govern the meeting and share in advance so everyone knows the expectations. </li>
				<li>Communication styles vary from culture to culture as do notions of authority and hierarchy, which only heightens the potential for misunderstanding and hard feelings.&rdquo; <br>

					There are differences in terms of
					<ul>
						<li>How and where people sit (to convey authority/status); </li>
						<li>The speed in which they get down to business (expectations of social engagement; value placed on punctuality); </li>
						<li>Willingness to provide feedback or argue (disagreement can be perceived as disrespect; perceived power dynamics may inhibit honest feedback). Silence does not necessarily mean agreement. </li>
					</ul>
				</li>
				<li>Research and share material on cultural differences. </li>
				<li>Think about how behavior is viewed by others and make adjustments. </li>
				<li>Solicit feedback from everyone (e.g. go around the room).<br>
					Another method to hear from everyone is to ask team members to email their ideas and thoughts to you and then you can share once everyone is together.</li>
			</ul>
