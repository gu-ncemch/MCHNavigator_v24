			<p>It would be ideal to live in a world where everyone understands the history and culture of all populations, but we are not there right now. Start by learning about the populations with whom you will be building a relationship. Include current and historic values and beliefs. Discover what the relationship has been between your agency and different communities. Think about connecting with local historians or librarians to learn more. Or, if a relationship already exists, chat with the community’s leaders to learn more.</p>
			<p>Reflect on your agency’s culture. Talk with people who have been at the agency a long time to learn about past dynamics. Keep in mind when working with government agencies that they have a culture all their own. That culture is influenced by politics, the current administration, and has a historical approach to solving problems can be transactional rather than focusing on transformation. You carry the history of that government everywhere you go.</p>
			<p>Creating trusting relationships may be driven by taking the time to learn about and understand others' cultures. If you want to shift from mere engagement to equitable engagement, this may be where there is really an opportunity for you to grow. Find ways to create dialog with people from cultures other than your own. You can do this at social events, community circles, and community cultural events. Likewise, you can invite people from another culture to an event you are hosting and spend time talking, and more importantly, listening to them.</p>
