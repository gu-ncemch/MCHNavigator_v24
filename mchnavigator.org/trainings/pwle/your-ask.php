			<p><strong>Implementation. </strong>Remember, the key to effective partnerships with PWLE:</p>
			<ul>
				<li> Shared decision making, involving self-advocates and/or the family, in planning and implementing activities. </li>
				<li>Addressing the priorities of people with lived experience using a strengths-based approach. </li>
				<li>Recognizing the agency of self-advocates in decision-making as they approach transition age, and across the lifespan. </li>
				<li>Connecting people with lived experience to needed services.</li>
				<li>Acknowledging that the effects of the SDOH, and broader systems of care, greatly impact individuals with special health care needs and developmental disabilities.</li>
