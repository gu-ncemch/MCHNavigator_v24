			<p>People can only offer relevant experience if they have a clear understanding of the issue(s) and the specific ask. It is easier for a team to make progress when everyone starts on the same page.</p>
			<p><strong>What, when, why:</strong></p>
			<p>What?</p>
			<ul>
				<li>What is the project?</li>
				<li> What is the goal?</li>
				<li>What is the scope of the project? </li>
				<li>Over describe and be abundantly clear. Provide a clear description, using plain language and spell out any acronyms. </li>
			</ul>
			<p>When?</p>
			<ul>
				<li>Time commitment</li>
				<li>Dates </li>
				<li>Duration of involvement </li>
			</ul>
			<p>Where?</p>
			<ul>
				<li>In-Person or Online</li>
				<li>Location </li>
			</ul>
			<p><strong>Engagement</strong></p>
			<p>Type of Engagement:</p>
			<ul>
				<li>Inform/Educate</li>
				<li>Gather</li>
				<li>Discuss</li>
				<li>Involve</li>
				<li>Partner<br>
				</li>
			</ul>
			<p>Categories and Definition/Methods of Engagement: </p>
			<ul>
				<li>Partner: An ongoing joint venture, involving people in every aspect of decision-making - from the early stages of defining the problems to developing and selecting the most promising solutions. People with lived experience are full partners with program staff. They are fully engaged from the start of a discussion around policy, programming, implementation, etc.
					<ul>
						<li> Examples include members of a team, governance boards, and improvement initiative work groups. <br>
							<br>
						</li>
					</ul>
				</li>
				<li>Involve: People play an important role in innovation/quality improvement efforts, but are still viewed as "advisory," with no role in decision-making. People with lived experience are advisors to program.
					<ul>
						<li> They are provided program challenges to problem solve.
							Examples include Advisory Councils, world café and town halls.<br>
							<br>
						</li>
					</ul>
				</li>
				<li>Discuss: Providing information so that people can respond with their viewpoint. The team commits to consider the input, not to incorporate people's perspectives into ultimate decisions and strategies. People with lived experience and staff discuss issues. The program provides opportunities for input and programs. The input is used in the program or activities. Typically input is offered through discussions which include give and take with opportunity for clarification.
					<ul>
						<li> Examples include focus group, interviews and discussions.<br>
							<br>
						</li>
					</ul>
				</li>
				<li>Gather: Collecting information from people and listening to their insights. People with lived experience are informers. Their input is solicited through opportunities to provide feedback.
					Input may not always be incorporated.
					<ul>
						<li>Examples include surveys, interviews, and suggestion boxes.<br>
							<br>
						</li>
					</ul>
				</li>
				<li>Inform/Educate: Communicating to the people you serve, and educating them about innovation efforts.
					People with lived experience are recipients of information and education. They are informed about the program and activities.
					<ul>
						<li> Examples include brochures, posters, and newsletters.</li>
					</ul>
				</li>
			</ul>
