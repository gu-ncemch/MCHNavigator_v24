     <p>When she moved to Florida in 1985, she joined the COPH as an assistant professor. In 1986 she also served as the health officer for Prevention and Early Intervention at the Florida Department of Health in Children&rsquo;s Medical Services where she was tasked with implementing the newly passed Public Law 99-457 which provides early intervention services to infants and young children.</p>
	 <p>In 1994 she was named Director of the USF COPH&rsquo;s Harrell Center for the Study of Family Violence, a center focused on integrating knowledge with best practices to strengthen community responses to family violence. The center conducts and translates research into usable information for practitioners to provide education and training, as well as serves as an advocacy center.	</p>
    
