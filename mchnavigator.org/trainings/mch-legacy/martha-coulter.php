	<p><strong>Dr. Martha Coulter</strong> is professor in the Department of Community and Family Health at the USF College of Public Health, maternal and child health has been at the forefront of her public health passion for decades.</p>
	<p>Upon earning her master&rsquo;s degree in public health from the University of California at Berkeley in 1977, she&rsquo;s devoted her public health career to bettering the lives of families and children. Coulter also holds a DrPH from the University of North Carolina at Chapel Hill in maternal and child health. She served as the state program director of the Developmental Evaluation Centers Program at the North Carolina Division of Health Services and as a lecturer at the UNC School of Public Health.</p>
	 
	
