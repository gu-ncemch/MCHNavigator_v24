<h2>MCH Academic Legacy Project</h2>
	<p><a href="../evaluation-intro.php"></a><img src="../../images/mch-legacy.jpg" width="300" height="200" alt="Decorative image of story telling" class="right border" />This project documents the unique paths of MCH leaders in academe. The hope of this project is to preserve these experience and the wisdom from the field that each expert brings and also to inspire a new generation of MCH academics.</p>
    
	<p><strong>Colleen Huebner, PhD, MPH, </strong>[Developmental Psychology (PhD); Epidemiology (MPH)]  was a faculty member of the University of Washington’s Center of Excellence in MCH Education, Science and Practice for 25 years as a teacher, assistant director, director (Principal Investigator) and professor emeritus. Within the University of Washington’s School of Public Health, she taught courses in needs assessment, program planning and evaluation, health policy, and the historical and current influence of Title V on women, children and families in the U.S. She mentored the research of over 70 MPH students, PhD students and post-docs and helped guide many of their projects to peer-reviewed publication.</p>
	<div style="display: flex; flex-direction: column;" class="right"><span class="right" style="display: flex; flex-direction: column;"><img src="../../images/Colleen-Huebner.png" alt="Colleen Huebner" class="ppic right" /></span></div>
    
     <p>Dr. Huebner's own research focused on the design, implementation and evaluation of community- and clinic-based preventive intervention programs for young children and families in poverty including programs to prevent infant maltreatment, promote early language development, emergent literacy and protect children’s oral health. She was a member of several interdisciplinary research teams at the University and worked with numerous collaborators including families and practitioners in rural communities in Washington, Oregon, Colorado, and in Alaska Native villages; people working in public libraries, community health clinics, Head Start programs, early intervention and family centers in Seattle, Chicago, and in the Republic of the Marshall Islands. A common goal of Dr. Huebner’s research was to establish the evidence for effective, feasible policies and programs that support young children’s health and development and, in turn, disseminate those practices broadly to benefit all children and families in need. In acknowledgement of this work, she served as a Presidential Appointee to the U.S. National Commission on Libraries and Information Science (2003-2007).</p>
     <p>Along with colleagues in MCH academe, field-based public health and clinical practice, Dr. Huebner established the groundwork for several MCH resources of national scope including the MCH Leadership Competencies and the MCH Navigator. She was on the editorial board of the Maternal and Child Health Journal for many years and, in 2005, received the Loretta P. Lacey Academic Leadership Award of the Association of Teachers of Maternal and Child Heath.</p>
    
