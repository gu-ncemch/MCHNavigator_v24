	<p><strong>Dr. Russell Kirby</strong> is a perinatal and pediatric epidemiologist trained in the social sciences, with a PhD in human geography (Wisconsin, 1981) and MS in preventive medicine-epidemiology (Wisconsin, 1991). Dr. Kirby worked at the state health agencies in Wisconsin and Arkansas, on the faculties of the Department of Pediatrics, University of Arkansas for Medical Sciences, and Department of Obstetrics and Gynecology, Milwaukee Clinical Campus, University of Wisconsin Medical School, and the School of Public Health, University of Alabama-Birmingham prior to joining the faculty of the University of South Florida in 2008.  He was elected Fellow in the American College of Epidemiology in 1996, and served as president in 2018-19. Dr. Kirby previously served as President of the National Birth Defects Prevention Network, the Wisconsin Association for Perinatal Care and the Arkansas Perinatal Association, Society for Pediatric and Perinatal Epidemiologic Research, and the Association of Teachers of Maternal and Child Health.    </p>
