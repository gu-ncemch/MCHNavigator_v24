	<p><strong>Donna Strobino</strong> has devoted her research career to MCH research with a special focus on the health of disadvantaged women during pregnancy and their young children and families. Her major perinatal health research contributions center on understanding the reasons why disadvantaged women have unfavorable pregnancy outcomes, and interventions to improve their outcomes, including among teenagers, low-income women, African American women and women who use drugs during pregnancy.</p>
    
	<div style="display: flex; flex-direction: column;" class="right">
    <img src="../../images/Donna-Strobino.jpg" alt="Donna Strobino" class="ppic right" /></div>
	
    <p>More recent research addresses maternal mental health as well as the interface of women’s chronic conditions and mental and psychosocial health in pregnancy with an eye to reducing neonatal morbidity as well as the burden of disease as women age. She has also studied the impact of maternal depressive symptoms on young children’s behavior and growth as well as parenting behaviors and use of preventive health services. Dr. Strobino currently works collaboratively with students and other faculty on studies of maternal morbidity and its impact on postpartum health, as well as the impact of chronic conditions on morbidity. She has conducted numerous evaluations of perinatal health programs in a variety of settings and pediatric programs for young children. She was Principal Investigator of the national evaluation of Fetal and Infant Mortality Review Programs and was a key investigator for the national Healthy Steps evaluation. She has been the Director of the Center of Excellence in MCH in Education, Science and Practice since 2004 and of the MCH Epidemiology Doctoral Training Program since 2008.</p>
    
