<h2>MCH Academic Legacy Project</h2>
	<p><a href="../evaluation-intro.php"></a><img src="../../images/mch-legacy.jpg" width="300" height="200" alt="Decorative image of story telling" class="right border" />This project documents the unique paths of MCH leaders in academe, focused on five key questions. The hope of this project is to preserve these experience and the wisdom from the field that each expert brings and also to inspire a new generation of MCH academics.</p>
    
	<p><strong>Ellen Schafer, PhD, MPH, MCHES</strong> earned her Ph.D. in Public Health with an emphasis on  Community and Behavioral Health from the University of Iowa in 2015, and completed a two-year postdoctoral fellowship in Maternal and Child Health at the University of South Florida before coming to Boise State University in 2018. Schafer is a Master Certified Health Education Specialist and received her Masters of Public Health (MPH) in Community Health Education in 2006 from the University of Wisconsin La Crosse.    </p>
	<div style="display: flex; flex-direction: column;" class="right">
    <img src="../../images/ellen-schafer.jpg" alt="Ellen Schafer" class="ppic right" /></div>
    
     <p>Dr. Schafer teaches health promotion and education, and public health classes. Her professional health education experience includes working as a health and wellness specialist with the Greater Southern Tier Board of Cooperative Educational Services, funded by the New York State Tobacco Control Program. She worked with K-12 school districts in New York State to develop, implement, enforce, and evaluate tobacco-free campus policies and educational initiatives. In addition, Schafer worked at the University of Mississippi as the Assistant Director of Student Health for Health Promotion, where she directed and implemented all of the health education and promotion activities on campus for students and staff and participated on the advisory board for the University’s Collegiate Recovery Community.</p>
	 <p>Dr. Schafer’s main research objective is to understand the mechanisms by which social context and social network influences are associated with health behaviors, and she typically applies these factors to studies of infant feeding and care.</p>
    
