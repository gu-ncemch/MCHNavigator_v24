	<p><strong>Milton Kotelchuck</strong>, PhD, MPH, is currently Professor of Pediatrics at Harvard Medical School and Senior Scientist in Maternal and Child Health at the Center for Child &amp; Adolescent Health Research and Policy at Massachusetts General Hospital. Formerly, he was Professor and Chair of the Maternal and Child Health Departments at Boston University School of Public Health and at the University of North Carolina at Chapel Hill.</p>
    
	<div style="display: flex; flex-direction: column;" class="right">
    <img src="../../images/Milton-Kotelchuck.jpg" alt="Milton Kotelchuck" class="ppic right" /></div>
    
	<p>Dr. Kotelchuck has been active in advancing MCH research, practice, policy and training in the MCH field for nearly 50 years; to that end, he has served on numerous local, State, national and professional MCH advisory committees. He has advocated for the advancement of MCH epidemiology and data systems and for the reintegration of MCH life course/social determinant of health perspectives into the MCH field.. His own research interests include examination of the adequacy and content of prenatal care, racial disparities in birth outcomes, maternal morbidity, immigrant health, child nutrition, fatherhood/men’s health, child health services, and health data policy. Dr. Kotelchuck has extensively evaluated public health programs to improve birth outcomes and child health status. He developed the widely used Adequacy of Prenatal Care Utilization (APNCU) Index, the so-called Kotelchuck Index. He served as the founding and initial Editor of the <em>Maternal and Child Health Journal</em>.</p>
    
    <p>In 2000, Dr. Kotelchuck was awarded the first National MCH Epidemiology Award for &ldquo;Advancing Knowledge&rdquo; from the Coalition for Excellence in MCH Epidemiology. In 2010, he was awarded the Martha May Eliot Award, the top honor given for Maternal and Child Health by the American Public Health Association.</p>
