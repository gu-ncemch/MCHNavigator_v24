<h2>From Generation to Generation: Building MCH Academic Leadership</h2>
<p><img src="../images/ATMCH-MCHN.jpg" alt="ATMCH logo" class="right border" />In this Lunch-and-Learn session presented by the Association of Teachers of Maternal and Child Health's Workforce Development Committee, Arden Handler, DrPH (Professor, University of Illinois at Chicago CoE)  moderates a panel discussion consisting of Marti Coulter (Emeritus University of South Florida CoE), Lois McCloskey (Associate Professor, Boston University CoE), and Christine Bozlak (Assistant Professor, University at Albany MCH Catalyst Program).</p>
<p>This webinar is aimed at multiple audiences: those already in MCH Academe who are wondering if they are going to be successful/wondering how they are going to climb the academic ladder, for those not yet in academe, who might be considering a career in public health academe and MCH in particular, and for those who are part of  academe and are curious about whether MCH academe is a good fit for them.</p>
<ul>
  <li><strong>The Journeys: </strong>We  hear about the journeys of three successful academics who are currently faculty in or who have recently retired from maternal and Child Health programs in Schools of Public Health. These three individuals are at various stages of their careers and are asked to respond to a series of questions about their career paths and how they managed or are managing to balance the research and practice demands of MCH academics.</li>
  <li><strong>Lessons Learned: </strong>The moderator  summarizes the key points made by the three academics sharing their journeys.</li>
  <li><strong>Answering Your Questions:</strong> All participants on the webinar are given the opportunity to ask questions of the moderator and the three academics who  share their journeys with all of you. </li>
</ul>
<p>This webinar covers the following objectives:<u></u><u></u></p>
<ul>
  <li><strong>Objective 1: </strong>To explore how one can have a successful career in MCH academe in Schools of Public Health.</li>
  <li><strong>Objective 2:</strong> To understand how one can balance the research demands of academe and of academic institutions while maintaining a commitment to MCH public health practice.</li>
  <li><strong>Objective 3:</strong> To encourage aspiring and current doctoral students, post-doctoral fellows, and junior faculty interested in population maternal and child health to consider a career in MCH academe. </li>
</ul>
