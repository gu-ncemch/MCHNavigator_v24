            <p>The “Messenger Chronicles” consists of five separate learning modules that simulate realistic conversations. The framework used for these scenario-based modules shifts focus away from managing “difficult” people towards an understanding of the process of difficult conversations and accepting responsibility for one’s own performance. Given realistic situations and real-world conversations, learners can experience communication strategies and practical techniques in context.  Many of the conversations are split into three sections: Read, Think and Analyze. Learners read a conversation and then are asked to think about certain aspects of the conversation by answering questions or engaging in activities. Further information can be gained from an interactive analysis of the conversation.</p>
