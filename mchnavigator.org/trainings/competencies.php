<h3>11. Systems Thinking</h3>
<p><strong>Overview. </strong>Improving the health and well-being of mothers, children, and families is a complex process because these populations are influenced by many intersecting factors. Systems thinking recognizes this complexity and examines the linkages between components—norms, laws, resources, infrastructure, and individual behaviors—that affect outcomes. Systems thinking addresses how these components interact at multiple levels and the leadership required to make advances within and across those levels.</p>

