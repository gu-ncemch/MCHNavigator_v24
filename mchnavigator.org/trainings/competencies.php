<h3>1. MCH Knowledge Base/Context</h3>
<p><strong>Overview. </strong>Maternal and child health (MCH) is a specialty area within the larger field of public health, distinguished by:</p>
<ul>
  <li>Promotion of the health and well-being of mothers, children, and families. Particular attention is directed to MCH population domains: women, infants, children, adolescents, young adults, fathers and other caregivers, and children and youth with special health care needs (CYSHCN).</li>
  <li>A focus on individuals as well as the families, communities, populations, and systems of care in communities that support and are accountable to these individuals.</li>
  <li>A life course perspective as an organizing framework that acknowledges distinct periods in human development and presents both risks and opportunities for interventions to make lasting improvements.</li>
</ul>
