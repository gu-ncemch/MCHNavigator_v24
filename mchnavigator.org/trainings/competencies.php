<h3>4. Critical Thinking</h3>
<p><strong>Overview. </strong>Complex challenges faced by MCH populations and the systems that serve them require critical thinking. Critical thinking is the ability to identify an issue or problem, frame it as a specific question, consider it from multiple perspectives, evaluate relevant information, and develop a resolution.</p>
<ul>
  <li><em>Evidence-informed decision-making</em> is the conscientious, explicit, and judicious use of current best evidence to guide practice, policy, and research. It is an advanced manifestation of critical thinking skills.</li>
  <li><em>Implementation science</em> is a vital component of critical thinking to promote the timely adoption and integration of evidence-informed practices, interventions, and policies.</li>
</ul>
