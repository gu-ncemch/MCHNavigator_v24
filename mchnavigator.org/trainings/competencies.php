<h3>10. Interdisciplinary/Interprofessional Team Building</h3>
<p><strong>Overview. </strong>MCH systems are interdisciplinary/interprofessional (ID/IP) in nature. ID/IP practice provides a supportive environment in which team members from different disciplines and sectors are acknowledged and seen as essential and synergistic. Input from each team member is elicited and valued in making collaborative, outcome-driven decisions to address individual, community-level, or systems-level problems. The &quot;team,&quot; which is the core of ID/IP practice, is characterized by shared leadership, equal or complementary investment in the process, and accountability for outcomes.</p>
