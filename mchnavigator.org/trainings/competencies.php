<h3>5. Communication</h3>
<p><strong>Overview. </strong><em>Communication</em> is the verbal, nonverbal, and written sharing of information. The communication process consists of a sender who develops and presents the message and the receiver who works to understand the message. Communication involves both the message and how the message is presented. Health communication is vital for influencing behavior that can lead to improved health. </p>
<p><em>Skillful communication</em> is the ability to convey information to and receive information from others effectively. It includes essential components of attentive listening and clarity in writing for or presenting to (through speaking, signing, use of augmentative and alternative communication devices, etc.) a variety of audiences. An understanding of the impact of factors such as literacy level and disability on communication between MCH professionals and the individuals, families, and populations they serve is also important.</p>
