<h3>6. Negotiation and Conflict Resolution</h3>
<p><strong>Overview. </strong><em>Negotiation</em> is a cooperative process in which participants try to find a solution that meets the legitimate interests of involved parties. <em>Conflict resolution</em> is the process of resolving or managing a dispute by sharing each party's perspective and adequately addressing their interests so that they are satisfied with the outcome. </p>
<p>Leadership in MCH requires knowledge and skills in negotiation and conflict resolution to address differences among groups. </p>
<p>MCH professionals approach conflict and negotiations with an understanding of differences in interpersonal styles, levels of authority, and awareness of their own perspectives. They acknowledge that relationship building and development of trust are critical long-term outcomes. They recognize when compromise is appropriate to overcome an impasse and when persistence toward a different solution is warranted.</p>
