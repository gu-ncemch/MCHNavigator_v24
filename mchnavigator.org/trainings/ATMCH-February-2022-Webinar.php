<h2>Introduction to Literature Reviews</h2>
<p><img src="../images/ATMCH-MCHN.jpg" alt="ATMCH logo" class="right border" />As we focus our efforts on evidence-based/informed practice, we need to be fluent with the science distilled from the literature and the field. Literature reviews are key tools in this process by which a body of literature is classified using systematic methods that are intended to reduce bias and improve understandability. </p>
<p>This session digs into the basics of literature reviews with colleagues who engage in different types of  reviews as part of their work and research.</p>
<p>Presenters  discussed different types of reviews, including scoping, rapid, narrative, meta-analysis, and mixed studies. They summarized the Ten Step Method used by the Community Preventive Services Task Force for their Community Guide. They  shared their own experiences conducting literature reviews and explore tricks and tools for automating the process.  </p>
