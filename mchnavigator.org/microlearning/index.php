<h3><i class="icon mch-cog"></i> Focused and Fast MICRO-LEARNING: Gain knowledge while on the go.<a name="microlearning" id="microlearning"></a></h3>
<button class="accordion"><strong>About Micro-Learning.</strong> Learn on-the-go when your time is limited</button>
    <div class="panel">
      <p><strong>Need fast and focused learning? </strong>Micro-learning is an effective and emerging learning style to address the pressures of time constraints, information overload, and busy schedules by providing highly relevant and curated materials in small bursts at regular intervals.</p>
<p>Benefits of micro-learning include:</p>
      <ul>
        <li><strong>Short learning sessions. </strong>Learning occurs in small, bite-sized segments that are easy to achieve and incorporate.</li>
        <li><strong>Focused content. </strong>Access to highly relevant information happens without a lot of clutter.</li>
        <li><strong>Incremental learning.</strong> Learning bursts that are frequent and short build upon each other.</li>
        <li><strong>Just-in-time. </strong>Acquiring information happens at your own pace, when you need it.</li>
        <li><strong>Communal learning.</strong> Technology allows learners to see what others are thinking, share  ideas, and ask questions.</li>
        <li><strong>Multitasking and learning. </strong>Information can be gathered on-the-go, especially through mobile devices.</li>
      </ul>
      <p><strong>The MCH Navigator has pioneered a series of micro-learning activities for the busy public health professional and student.</strong>    </p>
