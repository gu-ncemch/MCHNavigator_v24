<button class="accordion"><strong>About Macro-Learning.</strong> Find out how to learn when you want to go deep</button>
    <div class="panel">
      <p><strong>Need intensive and immersive learning? </strong>Macro-learning through a structured, comprehensive curriculum addresses the need to learn about a topic deeply and at one's own pace to fully understand topic areas, how to apply what is learned in real-life settings, and track learning over time.      </p>
<p>Benefits of macro-learning include:</p>
      <ul>
        <li><strong>Learning is designed to be thorough, structured, and logical. </strong>Learners have expressed the need to review competencies in a comprehensive manner that  motivates a sense of learning and provides training over the course of time. </li>
        <li><strong>Learning is designed to increase knowledge and skills. </strong>Learners have expressed the need to focus their efforts on areas that they need to strengthen. Participants need to receive evidence-informed strategies to implement learning in their daily work. </li>
        <li><strong>Learning  occurs as part of a group. </strong>Learners have expressed the need to feel part of a collaborative and interact with other professionals. This gives learners the opportunity to share insights and pose questions to fellow professionals and students. </li>
        <li><strong>Learners  become experts. </strong>Learners have expressed the need to be encouraged to measure their before and after understanding of competencies through programs. </li>
        <li><strong>Learners are recognized for participation. </strong>Learners have expressed the need to use MCH Navigator programs as part of their professional-development activities, either individually or as a team-building activity with fellow professionals or students. </li>
      </ul>
