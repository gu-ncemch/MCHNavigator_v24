<p>MCH is a specialized area within public health promoting the health of mothers, children, and families. It addresses specific population domains including pregnant individuals, infants, children, adolescents, young adults, fathers/caregivers, and children and youth with special health care needs (CYSHCN). MCH emphasizes both individual and community/systems-level approaches and utilizes a life course perspective.  </p>
<p><strong>As MCH leaders, thoroughly understanding MCH populations, programs, and principles allows us to:
  </strong></p>
<ul>
  <li>Understand the  needs of MCH populations  </li>
  <li>Navigate key MCH programs and policies</li>
  <li> Apply core MCH values like prevention, and family-centered care  </li>
  <li>Recognize how different factors impact MCH outcomes  </li>
</ul>
<p><strong>Core knowledge areas include:  </strong></p>
<ul>
  <li>MCH populations and programs (including Title V)  </li>
  <li>MCH values (prevention, improved health outcomes, family-centered care)  </li>
  <li>Social determinants of health in MCH contexts  </li>
  <li>Evidence-informed practice in MCH
