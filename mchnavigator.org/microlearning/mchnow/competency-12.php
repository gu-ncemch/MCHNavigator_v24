<p><strong >Read more.</strong> The <strong>Policy</strong> competency helps MCH leaders understand and engage with policy processes at all levels to support positive outcomes for maternal and child health populations. This essential skill enables leaders to effectively navigate the systems that determine resource allocation and program priorities.</p>
<p dir="ltr"><strong>For MCH leaders, policy skills are essential for:</strong></p>
<ul>
  <li>Using key data to describe the needs of MCH populations</li>
  <li>Drawing on available evidence to assess policy effectiveness</li>
  <li>Understanding the roles of different partners in policy development processes</li>
  <li>Applying evaluation frameworks to analyze policy alternatives</li>
  <li>Balancing various partner interests while maintaining MCH priorities</li>
  <li>Communicating evidence effectively to inform decision-makers</li>
</ul>
<p dir="ltr"><strong>As MCH leaders, developing these skills allows us to:</strong></p>
<ul>
  <li>Translate complex data into clear&nbsp; policy narratives</li>
  <li>Navigate government policy-making processes at the local, state, and national levels</li>
  <li>Understand current policies and initiatives affecting MCH populations</li>
  <li>Develop effective strategies for communicating with policymakers</li>
  <li>Support policy solutions that address MCH needs  </li>
</ul>
<p><strong>Core knowledge areas include:</strong></p>
<ul>
  <li>Government policy-making processes at the local, state, and national levels</li>
  <li>Current public policies and programs relevant to MCH populations</li>
  <li>Methods for informing and educating policymakers about MCH population needs</li>
  <li>Strategies for organizational and public-facing communications regarding MCH priorities</li>
  <li>Policy impact analysis and evaluation methods  </li>
</ul>
