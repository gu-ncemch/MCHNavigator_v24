<div class="panel">
  <p>The MCH Leadership Competencies describe the necessary knowledge and skills (foundational and advanced) designed to support and promote MCH leadership. These areas will be further explored in the next section of the module.</p>
  <h3>KNOWLEDGE AREAS</h3>
  <p>MCH leaders will demonstrate a working knowledge of:</p>
  <ul>
    <li>How individual perspectives shape decision-making and influence health organizations.</li>
    <li>How policies and organizational approaches intersect and influence health outcomes.</li>
    <li>How various factors contribute to differences in health, and how these factors interact to shape outcomes.</li>
    <li>How evidence-based strategies can improve participation and engagement in research, programs, and systems.</li>
  </ul>
  <h3>SKILLS</h3>
  <p><strong>Foundational. </strong>At a foundational level, MCH leaders will be able to:</p>
  <ul>
    <li>Conduct personal and organizational self-assessments to improve overall effectiveness.</li>
    <li>Recognize and support the strengths of individuals by understanding their experiences and perspectives.</li>
    <li>Develop professional behaviors that acknowledge varied perspectives while remaining aware of potential influences on decision-making.</li>
  </ul>
  <p><strong>Advanced</strong>. Building on the foundational skills, MCH leaders will be able to:    </p>
  <ul>
    <li>Adapt clinical and public health systems to better address specific individual and group needs.</li>
    <li>Implement strategies that improve effectiveness in public health and healthcare service delivery.</li>
    <li>Incorporate responsive approaches into programs, research, education, communications, and policies.</li>
    <li>Use data-driven tools and analysis to identify trends, improve health efforts, and communicate findings clearly.</li>
  </ul>
