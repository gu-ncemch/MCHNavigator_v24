<h2><i class="mch-book-open"></i> 9.1.1 PROFICIENCY</h2>
<div class="evidenceBorder"><h3><i class="mch-search"></i> <img src="../../images/smart-1-1-magnifying-glass.jpg" alt="Image of Magnifying Glass" class="right ppic" />REVIEW. Summary of the Competency</h3>
<p>Teaching, coaching, and mentoring are three primary strategies used to develop others. The relationships between teachers and students, coaches and coaching participants, and mentors and mentees are mutually beneficial relationships that contribute to building and strengthening the capacity of the public health workforce.</p>
<p><em>Teaching</em> involves designing a learning environment, which can include developing objectives and curricula; providing resources and training opportunities; modeling the process of effective learning; and evaluating whether learning occurred.</p>
<p><em>Coaching</em> refers to methods of training, counselling, or instructing an individual or group how to maximize their potential by developing skills, examining their assumptions, setting goals, taking appropriate actions, and reflecting on the outcomes.</p>
<p><em>Mentoring</em> is a reciprocal learning relationship in which a mentor and mentee work collaboratively toward the achievement of mutually defined goals that will develop participants' skills, abilities, knowledge, and/or thinking.</p>
    
