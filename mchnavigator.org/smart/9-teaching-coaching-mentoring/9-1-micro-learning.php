<div class="panel">
  <p>The MCH Leadership Competencies describe the necessary knowledge and skills (foundational and advanced) designed to support and promote MCH leadership. These areas will be further explored in the next section of the module.</p>
  <h3>KNOWLEDGE AREAS</h3>
  <p>MCH leaders will demonstrate a working knowledge of:</p>
  <ul>
    <li>A variety of teaching strategies and tools that are inclusive of different learning styles,  responsive to various backgrounds, and appropriate to the goals and needs of a range of learners.</li>
    <li>Coaching as a professional relationship that offers tools for dealing with and leading change, working with others, and navigating conflict.</li>
    <li>Mentoring as a personal, career-oriented relationship to promote the mentees' professional growth, enhance their skill sets, and increase their knowledge of relevant resources.</li>
    <li>Teaching, coaching, and mentoring as a tool for leadership development and succession planning.</li>
  </ul>
  <h3>SKILLS</h3>
  <p><strong>Foundational. </strong>At a foundational level, MCH leaders will be able to:</p>
  <ul>
    <li>Practice humility and cultivate rapport so that teaching, mentoring, and coaching relationships can be productive.</li>
    <li>Clearly set and continuously reinforce boundaries and define expectations in a mentoring or coaching relationship.</li>
    <li>Use instructional technology tools that encourage comprehensive participation and engagement across varied learning approaches</li>
    <li>Give and receive constructive feedback about behaviors and performance.</li>
    <li>Cultivate active listening skills.</li>
  </ul>
<p><strong>Advanced</strong>. Building on the foundational skills, MCH leaders will be able to:    </p>
<ul>
  <li>Incorporate evidence-informed education and be responsive to individuals' needs for accommodations</li>
  <li>Consistently engage individuals using active learning methods.</li>
  <li>Effectively facilitate learning in groups with individuals of varying baseline knowledge, skills, and experiences.</li>
  <li>Expand beyond task-or project-focused coaching to career-and professional advancement.focused coaching and mentoring.</li>
  <li>Support professional development by creating meaningful opportunities</li>
</ul>
