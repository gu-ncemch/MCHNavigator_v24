<div class="evidenceBorder"><h3><i class="mch-compass-2"></i> <img src="../../images/smart-1-1-compass-3.jpg" alt="Image of Compass" class="right ppic" />RELATE. Linking to Related Concepts and Competencies</h3>
<p>This  competency relates to <strong>Change Management/Adaptive Leadership</strong></p>
<p>For the MCH, public health, and health care fields, change has been constant in recent years. To achieve the shared Title V MCH goals of improved access, improved quality, and integrated systems, it&rsquo;s more essential than ever for public health practitioners to possess the skills to address change.</p>

<button class="accordion"><i class="mch-graduation-cap"></i> <strong>Learn More.</strong> Expanding on the related concept/competency.</button>
<div class="panel">
  <p>Change Management involves taking a systematic approach to adjusting to workplace change. Within public health, it means that staffers should be willing to:</p>
  <ul>
    <li>Understand transformation and change</li>
    <li>Collaborate</li>
    <li>Think critically and innovate across systems</li>
    <li>Sustain effort</li>
    <li>And contribute to learning as an organization.</li>
</ul>
  <p>Additionally, there is a need for public health professionals to be willing to develop as leaders who can fill the new roles that emerge in a transformed system. Changing social, scientific, and demographic contexts and emerging challenges and opportunities within public health generate what are called &ldquo;adaptive challenges.&rdquo; These are challenges that are difficult to define and don&rsquo;t have obvious or straightforward answers. They stand in contrast to technical challenges, for which there <em>are</em> solutions available. Leaders within public health and MCH need to possess the skills that allow them to address these adaptive challenges effectively, managing change across boundaries while still keeping the big picture in mind. </p>
  <p>Specific skills for adaptive leadership include: </p>
  <ul>
    <li>Being able to speak persuasively and listen intently</li>
    <li>Being able to successfully develop a shared vision with partners</li>
    <li>The ability to develop shared accountability for outcomes</li>
    <li>Problem-solving and innovating</li>
    <li>Measuring and managing outcomes</li>
    <li>And continuing to adapt and improve partnerships and policies over time.</li>
  </ul>
