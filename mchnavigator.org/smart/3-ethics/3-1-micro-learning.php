<div class="panel">
  <p>The MCH Leadership Competencies describe the necessary knowledge and skills (foundational and advanced) designed to support and promote MCH leadership. These areas will be further explored in the next section of the module.</p>
  <h3>KNOWLEDGE AREAS</h3>
  <p>MCH leaders will demonstrate a working knowledge of:</p>
  <ul>
    <li>Understanding the core principles, values, and behaviors that guide professional conduct within community, healthcare, and public health environments.</li>
    <li>Adherence to professional standards.</li>
    <li>Understanding the role of the IRB in ensuring values-based study design, informed consent, and the translation of research into practice, particularly in human subject research.</li>
    <li>Understanding how IRBs can promote integrity-driven study designs by incorporating people with personal expertise throughout the research process.</li>
  </ul>
  <h3>SKILLS</h3>
  <p><strong>Foundational. </strong>At a foundational level, MCH leaders will be able to:</p>
  <ul>
    <li>Work to understand individual and community perspectives to support the development of programs, practices, and approaches that are respectful and aligned with community needs.</li>
    <li>Recognize and address key considerations within specific practice settings, including patient care, public health initiatives, and research.</li>
    <li>Examine the factors that shape experiences and outcomes for MCH populations and apply strategies to promote balanced decision-making and broader access to services.</li>
  </ul>
<p><strong>Advanced</strong>. Building on the foundational skills, MCH leaders will be able to:    </p>
<ul>
  <li>Act as a catalyst to discuss dilemmas that impact MCH populations, fostering an environment where complex issues can be addressed thoughtfully and effectively.</li>
</ul>
