<h3>Implementation Model</h3>
	  <p>
      <em>Ethical behavior </em> in professional roles includes conduct congruent with generally accepted principles and values. This could include general leadership ethics, such as honesty, integrity, understanding, and responsibility, as well as ethics specific to the Maternal and Child Health (MCH) population.<sup>1</sup></p>
    <p>Interest in ethical leadership from academics and practitioners has grown enormously in recent years.<sup>2</sup> After a thorough review of the literature Lawton and Paez (2015) suggest a framework to draw together the different dimensions of ethical leadership and aide in the implementation of executing skills associated with ethics. Competency 3: Ethics  is comprised of three dimensions: <strong>(1) Practices</strong>, <strong>(2) Purposes</strong>, and <strong>(3) Virtues</strong>.</p>
<p><img src="../../images/smart-implementation-framework-3.png"  alt="Implementation Framework"/></p>
