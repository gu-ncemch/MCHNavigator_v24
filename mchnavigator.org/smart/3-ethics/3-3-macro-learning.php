<h2><i class="mch-light-bulb"></i> 3.3.2 PRACTICE</h2>
	  <div class="evidenceBorder">
	  <h3><i class="mch-bookmarks"></i> <img src="../../images/smart-1-1-reflect.jpg" alt="Image of Thought Bubble" class="right ppic" />REFLECT. Staying Motivated</h3>
	  <p>As you work through your growth in this competency, it's important to stay motivated. Here are some tips to help you maintain focus and productivity along the way:</p>
      <ul>
        <li><strong>Create summary notes</strong>. 
          Create summary notes of each chapter or topic you&rsquo;re learning.          Condensing each chapter or section of new information into a handful of key points will make it easier for you to consolidate your learning.</li>
        <li><strong>Use the Pomodoro Technique</strong>. 
          If you can&rsquo;t focus for 30 to 45 minutes straight, try the Pomodoro Technique instead.            (&ldquo;Pomodoro&rdquo; is the Italian word for &ldquo;tomato.&rdquo;) The <a href="https://en.wikipedia.org/wiki/Pomodoro_Technique" target="_blank">Pomodoro&nbsp;Technique</a> is a way of studying in short bursts. Study in blocks of 25 minutes, with a 5-minute break in between blocks.          Once you&rsquo;ve completed four blocks of 25 minutes, take a break of 15 to 30 minutes.</li>
        <li><strong>Test yourself often</strong>. 
          Another useful study tip is to test yourself at regular intervals on what you&rsquo;ve been learning.          This not only enables you to consolidate the new information, but it also ensures that you&rsquo;re mastering the material.</li>
      </ul>
