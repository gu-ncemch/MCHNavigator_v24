<div class="panel">
  <p><strong>How to Advance Yourself as an MCH Leader</strong>. Identify your own personal strengths and areas of growth related to Competency 3: Ethics and Professionalism, by taking 5 minutes to register/log-in and answer questions related to this competency in the MCH Navigator's online <a href="http://mchnavigator.org/assessment/index.php">Self-Assessment</a>.</p>
</div>

<button class="accordion"><i class="mch-book"></i> <strong>Information Strategy. </strong>How to find and use tools to help you.</button>
