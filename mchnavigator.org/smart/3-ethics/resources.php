  <h3><span class="accordion"><strong><a name="k1" id="k1"></a>3K1.</strong> Professional ethical/legal principles, values, behaviors</span></h3>
   <p><strong>Applying What We Know</strong></p>
   <p>Nearly every decision an MCH professional makes has an ethical component. Consequently, a basic understanding of ethics within the professional, research, and practice domains is essential. For further exploration of the basic topics of ethics, visit <a href="https://libguides.regis.edu/ld.php?content_id=52453940">Ethics at a Glance</a>. For guidance on making ethical decisions, try using the ethical decision-making app at <a href="https://www.scu.edu/ethics-app/">Making an Ethical Decision</a>. The video presentation <a href="https://vimeo.com/69107396">Ethics in Public Health: A Closer Look at Current Issues</a> uses case studies to explore public health ethics in greater detail.   </p>
   <p><strong>Resources from the Field</strong></p>
   <ul>
     <li><a href="https://www.cdc.gov/od/science/integrity/phethics/index.htm">Public Health Ethics at CDC</a> - Public health ethics initiatives and trainings at the CDC</li>
    
   </ul>
   <h3><span class="accordion"><strong><a name="k2" id="k2"></a>3K2.</strong> Professional associations' code of ethics</span></h3>
   <p><strong>Applying What We Know</strong></p>
   <p>Become familiar with the code of ethics of the professional association for your MCH discipline, or with the <a href="https://www.apha.org/-/media/files/pdf/membergroups/ethics/ethics_brochure.ashx">Principles of the Ethical Practice of Public Health</a>. Seek out any resources that professional associations provide to assist or improve your ethical decision-making skills. </p>
<p><strong>Resources from the Field</strong></p>
<p><a href="https://link.springer.com/book/10.1007/978-3-319-23847-0">Public Health Ethics: Cases Spanning the Globe</a> - Provides an ethical decision-making framework for public health professionals, as well as case studies illustrating the decision-making process.</p>
