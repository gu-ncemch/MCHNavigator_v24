<div class="panel">
  <p>The MCH Leadership Competencies describe the necessary knowledge and skills (foundational and advanced) designed to support and promote MCH leadership. These areas will be further explored in the next section of the module.</p>
  <h3>KNOWLEDGE AREAS</h3>
  <p>MCH leaders will demonstrate a working knowledge of:</p>
  <ul>
    <li>Principles of communication using different modalities, including verbal, written, and nonverbal, in various practice, policy, and research settings.</li>
    <li>Approaches to address differences in communication needs, such as literacy levels, functional abilities, technical jargon, and acronyms.</li>
    <li>Responsiveness in communicating with individuals and communities.</li>
    <li>The role of health literacy in ensuring fair access to information and services..</li>
  </ul>
  <h3>SKILLS</h3>
  <p><strong>Foundational. </strong>At a foundational level, MCH leaders will be able to:</p>
  <ul>
    <li>Share thoughts, ideas, and feelings effectively with individuals and groups from varied backgrounds.</li>
    <li>Communicate clearly and effectively using plain language and other principles to express information about issues that affect MCH populations.</li>
    <li>Cultivate active listening skills and attentiveness to nonverbal communication cues.</li>
    <li>Tailor information for the intended audience(s), purpose, and context by using appropriate communication messaging, tools like interpretation services, and health literacy principles, and using different modalities for dissemination. Audiences can include consumers, policymakers, clinicians, and the public.</li>
  </ul>
<p><strong>Advanced</strong>. Building on the foundational skills, MCH leaders will be able to:    </p>
<ul><li>Employ foundational communication skills in challenging situations, such as receiving or presenting information during an emergency, relaying difficult news, or explaining opportunities and risks for health promotion and disease prevention.</li>
  <li>Summarize complex information appropriately for a variety of audiences and contexts.</li>
</ul>
