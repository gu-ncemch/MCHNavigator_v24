<h2><i class="mch-book-open"></i> 5.1.1 PROFICIENCY</h2>
<div class="evidenceBorder"><h3><i class="mch-search"></i> <img src="../../images/smart-1-1-magnifying-glass.jpg" alt="Image of Magnifying Glass" class="right ppic" />REVIEW. Summary of the Competency</h3>
<p><em>Communication</em> is the verbal, nonverbal, and written sharing of information. The communication process consists of a sender who develops and presents the message and the receiver who works to understand the message. Communication involves both the message and how the message is presented. Health communication is an essential part of influencing behavior that can lead to improved health.</p>
<p><em>Skillful communication</em> is the ability to convey information to and receive information from others effectively. It includes components of attentive listening and clarity in writing for or presenting to a variety of audiences. An understanding of effective communication strategies ensure clear and accessible information exchange between MCH professionals and the individuals and families they serve.</p>
