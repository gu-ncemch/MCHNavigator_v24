<div class="evidenceBorder"><h3><i class="mch-compass-2"></i> <img src="../../images/smart-1-1-compass-3.jpg" alt="Image of Compass" class="right ppic" />RELATE. Linking to Related Concepts and Competencies</h3>
<p>This competency relates to  <strong>Competency 5. Communication Skills</strong> of the <a href="http://www.phf.org/programs/corecompetencies/Pages/Core_Competencies_Domains.aspx">Core Competencies for Public Health Professionals</a>.</p>
<p>Communication Skills focus on assessing and addressing population literacy; soliciting and using community input; communicating data and information; facilitating communications; and communicating the roles of government, health care, and others.</p>
<p>(Note: The Core Competencies are organized into eight domains, reflecting skill areas within public health, and three tiers, representing career stages for public health professionals: tier 1, front line/entry-level; tier 2, program management/supervisory; and tier 3, senior management/executive-level.)</p>
<button class="accordion"><i class="mch-graduation-cap"></i> <strong>Learn More.</strong> Expanding on the related concept/competency.</button>
<div class="panel">
  <p>Effective communication is just as essential to public health as sound science. You can be doing good research on a public health issue, but if you can&rsquo;t get the message across about what you are doing or why it&rsquo;s important, much of that work will be wasted.</p>
  <p>All of the staffing-level tiers (front line/entry-level; program management/supervisory; and senior management/executive-level) of this domain need to be able to communicate effectively with individuals, groups, and organizations using a variety of approaches -- from written reports, letters and emails to public presentations. </p>
  <p>Key to this process is having a grasp of the literacy levels and health needs of the community or population you serve. The three tiers should work together to choose appropriate ways of getting their data and information to their intended audience. Staffers from Tiers 1 &amp; 2 <em>communicate</em> that information in ways that influence behavior and improve health, and Tier 3 staffers <em>evaluate</em> those communication strategies. </p>
  <p>Whenever possible, communication messages should include information about the roles of governmental public health, health care, and other partners in improving the health of a community.</p>
