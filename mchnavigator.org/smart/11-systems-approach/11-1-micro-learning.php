<h2><i class="mch-book-open"></i> 11.1.1 PROFICIENCY</h2>
<div class="evidenceBorder"><h3><i class="mch-search"></i> <img src="../../images/smart-1-1-magnifying-glass.jpg" alt="Image of Magnifying Glass" class="right ppic" />REVIEW. Summary of the Competency</h3>
<p> Improving the health and well-being of mothers, children, and families is a complex process influenced by multiple interconnected factors. Systems thinking recognizes this complexity and examines the connections between components—including norms, laws, resources, infrastructure, and individual behaviors—that affect outcomes. Systems thinking explores how these components interact at multiple levels and the leadership needed to drive meaningful improvements across those levels.</p>
    
