<div class="panel">
  <p>The MCH Leadership Competencies describe the necessary knowledge and skills (foundational and advanced) designed to support and promote MCH leadership. These areas will be further explored in the next section of the module.</p>
  <h3>KNOWLEDGE AREAS</h3>
  <p>MCH leaders will demonstrate a working knowledge of:</p>
  <ul>
    <li>How organizations or practice settings function as systems, including business and administrative principles related to planning, funding, budgeting, staffing, and evaluation.</li>
    <li>How organizations or practice settings function in relation to broader systems, including principles of systems thinking; features and issues of systems (including but not limited to health economics and health policy); principles of building constituencies and engaging in collaborative endeavors; and concepts of implementation science and factors that influence use of research findings in practice.</li>
  </ul>
  <h3>SKILLS</h3>
  <p><strong>Foundational. </strong>At a foundational level, MCH leaders will be able to:</p>
  <ul>
    <li>Ensure the mission, vision, and goals of an organization align with the broader system in which it operates, fostering shared understanding, responsibility, and action.</li>
    <li>Practice budgeting, effective resource use, continuous quality improvement, coordination of tasks, and problem solving.</li>
    <li>Develop projects that reflect a broader systems approach and lead meetings/teams effectively.</li>
    <li>Identify external partners and the extent of their engagement in the collaborative process.</li>
    <li>Interpret situations using a systems perspective (i.e., identify both the whole system and the dynamic interplay among its parts).</li>
    <li>Assess the environment, with community, family, and individual input, to determine goals and objectives for a new or continuing program, list factors that facilitate or impede implementation of evidence-informed/informed strategies, develop priorities, and establish a timeline for implementation.</li>
  </ul>
<p><strong>Advanced</strong>. Building on the foundational skills, MCH leaders will be able to:    </p>
<ul>
  <li>Manage a project effectively and efficiently, including planning, implementing, delegating, sharing responsibility, staffing, and evaluating.</li>
  <li>Promote the use of evidence-informed practices to improve outcomes, drawing on research and data to guide decision-making and strengthen health initiatives.</li>
  <li>Develop proficiency in program administration, policy development, and health care financing.</li>
  <li>Recognize the impact of past factors that have shaped health challenges for maternal and child health populations, and maintain strong external partnerships.</li>
  <li>Build effective and sustainable coalitions that drive positive, population-centered outcomes through collaborative action and resource-sharing.</li>
  <li>Use community collaboration models (e.g., collective impact) and leverage existing community improvement efforts to define a meaningful role for MCH.</li>
</ul>
