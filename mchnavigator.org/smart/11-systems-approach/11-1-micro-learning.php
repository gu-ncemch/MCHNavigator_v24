<div class="evidenceBorder"><h3><i class="mch-compass-2"></i> <img src="../../images/smart-1-1-compass-3.jpg" alt="Image of Compass" class="right ppic" />RELATE. Linking to Related Concepts and Competencies</h3>
<p>This competency relates to  <strong>Competency 5. Community Dimensions of Practice Skills</strong> of the <a href="http://www.phf.org/programs/corecompetencies/Pages/Core_Competencies_Domains.aspx">Core Competencies for Public Health Professionals</a>.</p>
<p>(Note: The Core Competencies are organized into eight domains, reflecting skill areas within public health, and three tiers, representing career stages for public health professionals: tier 1, front line/entry-level; tier 2, program management/supervisory; and tier 3, senior management/executive-level.)</p>

<p>Community Dimensions of Practice Skills focus on evaluating and developing linkages and relationships within the community; maintaining and advancing partnerships and community involvement; negotiating for use of community assets; defending public health policies and programs; and evaluating effectiveness and improving community engagement.</p>
<button class="accordion"><i class="mch-graduation-cap"></i> <strong>Learn More.</strong> Expanding on the related concept/competency.</button>
<div class="panel">
  <p>This domain recognizes that, to be successful, public health professionals need to keep the community they serve at the heart of their work. They should have a thorough understanding of the programs and services that already exist in the community and the relationships that are affecting the health of the families that live there. Public health professionals should seek out, establish, and nurture relationships that can improve health within a community. Collaboration with community partners is essential, as is tapping into all of the assets and resources that are available.</p>
  <p>Just as critical is giving community members ways to provide input on developing, implementing, evaluating, and improving policies, programs, and services. Community-based participatory research, which centers on community collaboration, is an effective approach for accomplishing this.</p>
  <p>The competencies in the three tiers (front line/entry-level; program management/supervisory; and senior management/executive-level) knit together to support the work we&rsquo;ve just outlined. For example, Tier 1 staffers collaborate with community partners to improve health in a community. So they would be the ones participating in committees, sharing data and information with partner organizations, and connecting people to resources. Meanwhile, Tier 2 staffers would be facilitating these types of collaborations among partner organizations, through activities like coalition building. And Tier 3 staffers would be creating the formal, written agreements — the MOUs, contracts and letters of endorsement -- that define the purpose and scope of each partnership.</p>
