  <h3>Negotiation &amp; Conflict Resolution</h3>
  <p><strong>Knowledge Areas. </strong>MCH leaders will demonstrate a working knowledge of:</p>
  <button class="accordion"><i class="mch-compress"></i> <strong>6K1.</strong> Conflict in organizations.</button>
  <div class="panel">
    <p>The word <strong>conflict </strong>is derived from the Latin <em>conflictus</em>, which means &ldquo;to strike together.&rdquo; Conflict has several characteristics:<sup>1</sup> </p>
    <ul>
      <li>It involves at least two interdependent parties</li>
      <li>One party interferes with the objectives of another</li>
      <li>There is disagreement between the parties and a discrepancy between the parties&rsquo; interests, needs, or objectives</li>
      <li>Both parties experience negative emotions</li>
    </ul>
    <p>One survey of workers in nine countries indicated that 85 percent of employees experienced workplace conflict to some degree.<sup>2</sup></p>
    <p> In organizational contexts, conflict can emerge from several sources:<sup>3</sup></p>
    <ul>
      <li>Lack of understanding/misunderstanding</li>
      <li>Personality clashes</li>
      <li>Competition for resources</li>
      <li>Authority issues</li>
      <li>Lack of cooperation</li>
      <li>Differences over methods or style</li>
      <li>Low performance</li>
      <li>Value or goal differences</li>
    </ul>
  </div>
  <button class="accordion"><i class="mch-bullhorn"></i> <strong>6K2.</strong> Sources of interdisciplinary conflict.</button>
  <div class="panel">
    <p>In settings that bring together people from different disciplines, several potential sources of conflict exist:<sup>4,5</sup></p>
    <ul>
      <li>  Lack of communication</li>
      <li>  Misunderstandings</li>
      <li> Differing values</li>
      <li> Task and workflow interdependencies </li>
      <li> Competition for resources that are limited, or perceived that way</li>
      <li>  Unresolved prior conflicts</li>
      <li> Power or value asymmetries </li>
		<li>Differences in terminology</li>
    </ul>
	  <p>In community and family settings, conflict can arise from the dynamics between communities and institutions, and the relationships between individuals in families.</p>
  </div>
    <button class="accordion"><i class="mch-book"></i> <strong>6K3.</strong>  Conflict management theories.</button>
  <div class="panel">
    <p>There are two <strong>theories of conflict management</strong>:.<sup>6</sup></p>
    <p><strong>Traditional theory</strong></p>
    <ul>
      <li>Conflicts are caused by trouble-makers</li>
      <li>Conflicts are bad</li>
      <li>Conflicts should be avoided or suppressed</li>
    </ul>
    <p><strong>Contemporary theory</strong></p>
    <ul>
      <li>Conflicts are inevitable between human beings</li>
      <li>Conflicts are often beneficial</li>
      <li>Conflicts are the natural result of change</li>
      <li>Conflicts can and should be managed</li>
    </ul>
    <p><strong>Theories of negotiation</strong> abound in the literature, but tend to fall into two categories:<sup>7</sup></p>
    <ol>
      <li>Competitive</li>
      <li>Problem-solving</li>
    </ol>
    <p>Negotiators exhibit behaviors that fall into these two categories, depending on training or previous experience. </p>
    <p>These behavioral patterns are associated with each category:</p>
    <p><strong>Competitive theory</strong></p>
    <ul>
      <li>Tries to maximize tangible resource gains for own client within limIts of current dispute problem</li>
      <li>Makes high opening demands and is slow to concede</li>
      <li>Uses threats, confrontation, argumentation</li>
      <li>Manipulates people and the process</li>
      <li>Is not open to persuasion or substance</li>
      <li>Is oriented to quantitative and competitive goals</li>
    </ul>
    <p><strong>Problem-solving theory</strong></p>
    <ul>
      <li>Tries to maximize returns for own client, including any joint gains available</li>
      <li>Focuses on common interests of parties</li>
      <li>Tries to understand the merits as objectively as possible</li>
      <li>Uses nonconfrontational debating techniques</li>
      <li>Is open to persuasion on substance</li>
      <li>Is oriented to qualitative goals; a fair/wise/durable agreement, efficiently negotiated</li>
    </ul>
    <p>Competitive theorists say that this form of conflict resolution best approximates the actual experience of negotiators and generates better outcomes. Problem-solving theorists hold that, even if actual negotiation is more competitive than the theory might prescribe, their approach generates better outcomes.</p>
  </div>
    <button class="accordion"><i class="mch-traffic-cone"></i> <strong>6K4.</strong> Group negotiation strategies.</button>
  <div class="panel">
    <p>Negotiation is &ldquo;the process of making decisions and reaching agreement in situations where participants have different preferences.&rdquo;<sup>5</sup></p>
    <p>There are two general strategies for negotiation:</p>
    <p><strong>Positional (or distributive) negotiation:</strong> Both sides have a fixed position and desire for their portion of the resource in question, and will argue for it. Haggling on a purchase price is an example.<sup>5,8</sup></p>
    <p>Pro: This type of negotiation can be useful when two parties disagree on a number of fronts, and it is easier to focus on a single issue to negotiate and set the rest aside.</p>
    <p>Con: As negotiations proceed, each side holds to its position more tightly (&ldquo;my way or the highway&rdquo;). </p>
    <p> Process:<sup>9</sup></p>
    <ul>
      <li>Negotiate tough, but don&rsquo;t be rude or arrogant. Hold to your position and let the other side know that you mean business. Don&rsquo;t be afraid to ask for extra concessions.</li>
      <li>Scrutinize the details point by point. Wherever an item is not settled, try to gain an advantage. Negotiate hard on every specific item.</li>
      <li>Focus on the rewards and stay calm. Use any emotional outbursts from the other party to your advantage by not responding in kind.</li>
      <li>Give the other party choices rather than ultimatums. If the other party gives you an ultimatum, walk away from the table. </li>
      <li>Find the other party&rsquo;s vulnerabilities and use them to your advantage. </li>
      <li>Stay in control of the negotiation. Set the game plan. Decide whether picking a location could work to your advantage.</li>
    </ul>
    <p>Results: Competition, compromise, accommodation</p>
    <p><strong>Interest-based (or &ldquo;consensual,&rdquo; &ldquo;integrative&rdquo; or &ldquo;win-win&rdquo;) negotiation: </strong>Both sides work to develop a mutually beneficial agreement based on their interests, which may include their needs, desires, concerns and fears.<sup>5,8</sup></p>
    <p>Pro: Understanding each party&rsquo;s interests fully can reveal solutions that allow their true needs and concerns to be met to some degree.</p>
    <p>Con: Pressures to compromise and accommodate; vulnerable to dishonesty of the other party in negotiations</p>
    <p>Process:</p>
    <ul>
      <li>Separate the people from the problem. This allows parties to address the issues without damaging their relationship and gives them a clearer view of the substantive problem.</li>
      <li>Focus on interests, rather than positions. Interests explain the underlying motivations behind a position, or what is driving a person to take a certain position (needs, desires, concerns, fears, etc.). When a problem is defined in terms of the parties&rsquo; underlying interests it is often possible to find a solution that satisfies all parties.</li>
      <li>Generate a variety of possible ways forward. However, separate the generation of options from the evaluation of options. Try to avoid a win-lose mentality.</li>
      <li>Decisions should be made using reasonable standards. Make sure that the result and the process are based on objective criteria.</li>
    </ul>
    <p>Results: Avoidance, compromise, collaboration</p>
  </div>
    <button class="accordion"><i class="mch-thumbs-up"></i> <strong>6K5.</strong> Positive role of conflict.</button>
  <div class="panel">
    <p>Conflict within an organization or between individuals can be costly and destructive. However, &ldquo;conflict has a bounty of positive potential, which if harnessed correctly, can stimulate progress in ways harmony often cannot.&rdquo; <sup>2</sup> </p>
    <p>When handled appropriately, conflict can lead to positive outcomes, including:<sup>2</sup></p>
    <ul>
      <li>A better understanding of others</li>
      <li>Improved solutions to problems or challenges</li>
      <li>Major innovation</li>
    </ul>
    <p>With training, MCH professionals can learn how to take in a range of  information, involving various opinions from different perspectives, and make appropriate decisions. Conflict-resolution training is important for all staffers, but especially client-facing personnel, who also need the support of their managers and team members to deal with the impact of disagreements.<sup>2</sup></p>
  </div>
 
  <p><strong>Skills</strong></p>
  <p><em><strong>Foundational</strong>. At a foundational level, MCH leaders will be able to:</em></p>
  <button class="accordion"><i class="mch-target"></i> <strong>6S1.</strong> Maintain self-awareness of viewpoint, negotiation style.</button>
  <div class="panel">
  <p>Everyone has perspectives. To understand their own viewpoints, MCH professionals should engage in regular self-reflection and seek out objective feedback.</p> 
<p>Start by taking implicit association tests, which can reveal your perspectives or viewpoints regarding different groups. Keep a journal to track decision-making patterns and emotional responses when working with different populations.</p> 
<p>Actively seek out various perspectives and experiences that challenge preconceived notions. Engage in  responsivness training and stay updated on the latest research regarding health variations and community circumstances affecting health.</p> <p>Collaborate with colleagues from different backgrounds and be open to their observations about your interactions and decisions.</p> 
<p>Remember that recognizing your own perspective is an ongoing process.</p> 
<p>In situations involving conflict, a person&rsquo;s behavior falls along two dimensions:<sup>10</sup> </p>
    <ol>
      <li>Assertiveness: the extent to which they attempt to satisfy their own concerns</li>
      <li>Cooperativeness: the extent to which they attempt to satisfy the other person&rsquo;s concerns.</li>
    </ol>
    <p>It&rsquo;s been suggested that there are five major styles of negotiation/conflict management that represent different degrees of these two dimensions. Everyone is capable of using all five styles. Knowing when and how to use each style can help control the course of a conflict.</p>
    <p>Five Major Negotiation/Conflict Management Styles<sup>10</sup></p>
    <ol>
      <li> <strong>Collaborating</strong>: A combination of being assertive and cooperative. People using this style attempt to work with others to identify a solution that satisfies everyone&rsquo;s concerns. Both sides can get what they want and negative feelings are minimized. This can be time-consuming, but valuable in the long-term. The opposite of avoiding.</li>
      <li><strong>Competing</strong>: People with this style are assertive and uncooperative and willing to pursue their own concerns at others&rsquo; expense. This can work when the outcome is important but the relationship is not. </li>
      <li><strong>Avoiding</strong>: Generally unassertive and uncooperative. People sidestep an issue, postpone dealing with it, or withdraw from a situation. This can be used for situations that aren&rsquo;t of great concern. </li>
      <li><strong>Accommodating</strong>: This style involves being unassertive and cooperative, neglecting one&rsquo;s own concerns to satisfy the other person. While it may seem generous, it could take advantage of the weak and cause resentment. This can be used when the outcome of a situation isn&rsquo;t of great concern. The opposite of competing. </li>
      <li><strong>Compromising</strong>: This style aims to find an expedient, mutually acceptable solution to a situation that will partially satisfy both sides. Assertiveness and cooperativeness are moderate. This style may provide the best path when a decision needs to be made quickly, and both sides are willing to relent a bit on their expectations. It gives up more than competing but less than accommodating. It is more direct than avoiding, but does not satisfy both parties like collaborating does.</li>
    </ol>
    <p><a href="https://www.waldenu.edu/news-and-events/walden-news/2017/0530-whats-your-conflict-management-style">Read more about conflict management styles</a>.</p>
  </div>
  <button class="accordion"><i class="mch-comments-1"></i> <strong>6S2.</strong> Understand others' viewpoints/negotiation styles.</button>
  <div class="panel">
    <p><strong>How to Understand Others&rsquo; Points of View</strong>. 
One important step in resolving conflict is to understand the other party&rsquo;s point of view. Here are suggestions on how to achieve that insight:<sup>11</sup> </p>
    <ul>
      <li>Show that you have situational awareness: Work to understand the opportunities and challenges the other party is facing. Tailor your ideas to fit this knowledge. Show that you &ldquo;get it.&rdquo;</li>
      <li>Show that you have personal awareness: Work to understand the other party&rsquo;s strengths, weaknesses, goals, hopes, priorities, needs, limitations, fears and concerns. Connect with them as people. Show that you &ldquo;get them.&rdquo; </li>
      <li>Show that you have solution awareness: Help the other party envision a path that offers empowering options and alternatives that will make things better for them. Show that you &ldquo;get their path.&rdquo; </li>
    </ul>
    <p><strong>How Various Styles Can Influence Negotiation and Conflict Resolution</strong>. Potential impact of negotiation styles:<sup>7</sup></p>
    <ul>
      <li>The competitive negotiator views the negotiating world as one controlled by an egocentric self-interest.</li>
      <li>To the problem-solver, the negotiating world is controlled by an enlightened self-interest.    </li>
    </ul>
    <p><strong>How To Adapt To Others&rsquo; Conflict Resolution Styles To Resolve Differences</strong>. First, assess the style that the other party has toward conflict. Look for patterns in how they react to conflict, ask others for their input, or ask the person directly.<sup>12</sup></p>
    <p>&nbsp;– <em>If both parties avoid conflict</em></p>
    <p>Potential results:</p>
    <ul>
      <li>You both tend to do nothing to resolve conflict.</li>
      <li>You repress feelings that could explode later on.</li>
    </ul>
    <p>Action to take:</p>
    <ul>
      <li>One party takes the lead and speaks up about the conflict and the need to address it, inviting the other party to work toward a resolution.</li>
      <li>Use a sensitive, thoughtful approach to draw out the other person.</li>
      <li>Fight the instinct to back down and let the conflict go unresolved.</li>
    </ul>
    <p> &nbsp;– <em>If both parties rise to conflict</em></p>
    <p>Potential results:</p>
    <ul>
      <li>You share your thoughts openly with others, which could cause a discussion to take a contentious turn. </li>
      <li>You could end up saying things you don&rsquo;t mean. </li>
      <li>Both parties can leave the table feeling disrespected. </li>
    </ul>
    <p>Action to take:</p>
    <ul>
      <li>Carefully prepare for the conversation.</li>
      <li>If you tend to be impatient, see if you can schedule breaks or a change of scenery into your discussion.</li>
      <li>Be ready for heated discussion. </li>
    </ul>
    <p>&nbsp;– <em>If you rise to conflict, but the other party avoids it</em></p>
    <p>Potential results:</p>
    <ul>
      <li>If you bulldoze the other party into agreeing with you, they might resort to passive-aggressive ways to get their point across.</li>
    </ul>
    <p>Action to take:</p>
    <ul>
      <li>Invite the other party to be an active participant in the conversation and speak their mind.</li>
      <li>Restrain any bullying tendencies.</li>
      <li>Have patience if the conversation&rsquo;s pace doesn&rsquo;t match your preferences.</li>
    </ul>
    <p>&nbsp;– <em>If you avoid conflict, and the other party rises to it</em></p>
    <p>Potential results:</p>
    <ul>
      <li>Going along with what the other party wants may be a tempting path of least resistance, but what you want may be ignored if you do. </li>
    </ul>
    <p>Action to take:</p>
    <ul>
      <li>Voice what you need from the other party to have a productive conversation. Lay out expectations for tone of voice and pace.</li>
      <li>Be direct and to the point, but respectful.</li>
    </ul>
  </div>
  <button class="accordion"><i class="mch-checklist"></i> <strong>6S3.</strong> Apply/evaluate negotiation strategies.</button>
  <div class="panel">
    <p>As previously stated, there are <strong>two primary negotiation strategies</strong>.</p>
    <ul>
      <li><strong>Positional (or distributive) negotiation:</strong> Both sides have a fixed position and desire for their portion of the resource in question, and will argue for it.</li>
      <li> <strong>Interest-based (or &ldquo;consensual,&rdquo; &ldquo;integrative&rdquo; or &ldquo;win-win&rdquo;) negotiation: </strong>Both sides work to develop a mutually beneficial agreement based on their interests, which may include their needs, desires, concerns and fears. </li>
    </ul>
    <p>There are numerous effective negotiation techniques that support the above strategies. However, if one party subscribes wholly to one camp and the other does not, differences can be introduced into negotiations that could derail them. </p>
    <p>The following techniques occupy the &ldquo;middle ground&rdquo; between the two sides:<sup>9</sup></p>
    <ul>
      <li>Do your homework. Understand the significance of the items up for discussion and the results of various concessions. Figure out the &quot;bottom line&quot; of the negotiations.</li>
      <li>Deal with those who have the authority to implement what is negotiated. </li>
      <li>Build positive relationships whenever possible. It is difficult to negotiate with those who don&rsquo;t know you or consider you an adversary. </li>
      <li>Do not make hasty concessions. Take time to consider all the implications to ensure you don&rsquo;t give away more than you want. </li>
      <li>Reframe negatives as positives whenever possible. Start with positive points and then move to more controversial issues. </li>
      <li>Stay calm. Maintain composure and use the moment to decode the other party's needs and wants.</li>
      <li>Don't give up. Do not accept an agreement you don&rsquo;t like simply because there seem to be no alternatives. Look at the issues again for a way to get around the impasse. </li>
    </ul>
    <p>There are <strong>five negotiation styles</strong>. Everyone has a primary style, but skilled negotiators will be able to deploy the other styles as appropriate to the situation.</p>
    <p>The Five Negotiation Styles</p>
    <ol>
      <li>Competing</li>
      <li>Collaborating</li>
      <li>Compromising</li>
      <li>Accommodating</li>
      <li>Avoiding</li></ol></div>
    
 
    

  
 
 
  <p><em><strong>Advanced.</strong> Building on the foundational skills, MCH leaders will be able to:</em></p>
  <button class="accordion"><i class="mch-cogs"></i> <strong>6S4.</strong> Demonstrate conflict management.</button>
  <div class="panel">
    <p>To truly resolve dysfunctional conflict the underlying reasons for it must be resolved. This can be difficult to achieve, but unresolved conflict frequently generates more conflict, so it is worth the time and effort to address it head-on. </p>
    <p>This requires a good understanding of the stages of conflict, the potential causes of conflict, and approaches to conflict management.</p>
    <p>To successfully resolve conflict you should:<sup>13</sup></p>
    <ul>
      <li>Remain alert and calm by managing stress. This will allow you to accurately read and interpret verbal and nonverbal communication.</li>
      <li>Control emotions and behavior. This will let you communicate what you need without threatening, intimidating, or punishing others.</li>
      <li>Pay attention to the emotions the other party is expressing as well as the words they are speaking.</li>
      <li>Be aware of and respect differences. Avoiding disrespectful words and actions generally allows you to resolve a problem faster.</li>
      <li>Know when to let the argument go. If an agreement eludes you, agree to disagree. Disengage and move on.</li>
    </ul>
  </div>
  <button class="accordion"><i class="mch-compass3"></i> <strong>6S5.</strong> Navigate the ways background shapes conflict.</button>
  <div class="panel">
    <p>Background differences:<sup>5</sup></p>
    <ul>
      <li>The concept of time past is significant and has the potential to cause conflict when misunderstandings occur between individuals who value punctuality vs. those that value events and relationships. </li>
      <li>Conflict can emerge when people from short-term values, which value results in the moment, try to work with people from long-term values, which focus on future results. </li>
      <li>Similarly people from high-power-distance values, which accept that power is unbalanced  and de-emphasize individualism, work with those from low-power-distance values, in which the opposite is true.</li>
    </ul>
    <p>Personal characteristics:<sup>14</sup></p>
    <ul>
      <li>Personal background, including gender and community circumstances affecting health, can influence how negotiators behave, the way they are treated by the other party, the way the other party feels about them, and whether one side or the other even negotiates at all.</li>
    </ul>
    <p>Power or value asymmetries:<sup>5</sup></p>
    <ul>
      <li>Conflict can emerge when one person must depend on another of higher status, or vice versa. </li>
      <li>When two groups have different values yet are interdependent, conflict can result.</li>
    </ul>
  </div>
    
    <button class="accordion"><i class="mch-icon"></i> <strong>6S6.</strong> Build consensus.</button>
  <div class="panel">
    <p>Consensus involves reaching a solution to a conflict or issue that the overwhelming majority of those involved can accept. While unanimous agreement on the solution is something to strive for, it is rarely achieved. </p>
    <p>There are many approaches to consensus-building. One such approach envisions the process in five steps:<sup>15</sup></p>
    <p> Convening</p>
    <ul>
      <li>Identify the people who should be at the table</li>
      <li>Research to understand the substance and context of issue or problem</li>
    </ul>
    <p>Clarifying responsibilities</p>
    <ul>
      <li>Agree upon ground rules</li>
      <li>Define the responsibilities of members</li>
    </ul>
    <p>Deliberating</p>
    <ul>
      <li>Generate proposals and ideas</li>
      <li>Identify and collect additional information</li>
      <li>Share the information collected</li>
      <li>Share perspectives and perceived pros and cons of various solutions and parts of solutions</li>
      <li>Group members take what they&rsquo;ve heard and try to create a solution that meets everyone&rsquo;s needs</li>
    </ul>
    <p>Deciding</p>
    <ul>
      <li>Agree to agree</li>
      <li>One of the &ldquo;invented&rdquo; solutions is put forth – &ldquo;Can everyone live with this?&rdquo;</li>
      <li>Group members indicate yes or no and explain their position</li>
      <li>Tweak solution to get everyone to yes</li>
      <li>The group needs to listen to identify the right modifications</li>
      <li>Those with concerns need to be clear and honest about concerns and possible revisions</li>
    </ul>
    <p>Implementing agreements</p>
    <ul>
      <li>Include agreement on who will do what in any plan</li>
      <li>Implementation starts
        <ul>
          <li>Uninvolved parties question the solution – they may be good questions or may indicate lack of understanding</li>
          <li>Things change from status at the beginning of project</li>
          <li>Plan for anticipated changes</li>
        </ul>
      </li>
      <li>May need to reconvene to make the agreement better with new or changed information</li>
    </ul>
    <p>For greater detail on this process, see <a href="http://web.mit.edu/publicdisputes/practice/cbh_ch1.html">A Short Guide to Consensus Building</a>. </p>
  </div>
    
  
