  <button class="accordion"><i class="mch-list-ordered"></i> <strong>10S1.</strong> Describe roles, responsibilities, and scope of practice of ID/IP team.</button>
  <div class="panel">
    <p>Describing a role, responsibility or scope of practice starts with understanding each capacity:</p>
    <ul>
      <li><strong>Roles</strong> are the positions that people undertake or are assigned &mdash; the part that each person plays in an organization.<sup>4</sup></li>
      <li><strong>Responsibilities</strong> are the specific tasks or duties that people are expected to complete according to their roles.<sup>4</sup> They are the specific activities or obligations for which they are held accountable when they undertake or are assigned a role. </li>
      <li><strong>Scope of practice</strong> describes the activities that a qualified professional is deemed competent to perform and permitted to undertake, in keeping with the terms of their professional license. This is usually defined by state law.<sup>5</sup></li>
    </ul>
    <p>Each profession&rsquo;s roles and responsibilities vary within legal boundaries; actual roles 
      and responsibilities change depending on the specific care situation. Professionals 
      may find it challenging to communicate their own role and responsibilities to 
      Others. However, being able to do so is essential to interdisciplinary teamwork.<sup>6</sup></p>
  </div>
  <button class="accordion"><i class="mch-puzzle"></i> <strong>10S2.</strong> Seek varied perspectives in decision-making.</button>
  <div class="panel">
    <p>Gathering input from various perspectives can help MCH professionals make better decisions. </p>
    <p>Here are tips on how to do this:<sup>7</sup></p>
    <ul>
      <li>Solicit team members' views in appropriate ways that respect different backgrounds. Not every team member will be comfortable sharing their views in a group discussion, so provide other options for input.</li>
      <li>Ensure that there is adequate time to gather viewpoints. It may be necessary to make this task a discrete step in the decision-making process.</li>
      <li>Come to consensus on the shared goals and criteria for decision-making</li>
      <li>As the decision becomes clearer, check-in with those who have alternative viewpoints to see how their needs could be either better integrated or otherwise satisfied.</li>
    </ul>
  </div>
  <button class="accordion"><i class="mch-icon"></i> <strong>10S3.</strong> Assemble teams appropriate to the task.</button>
  <div class="panel">
    <p>Potential team members may come from a variety of disciplines and backgrounds. </p>
    <p>Members of teams need three kinds of skills:<sup>8</sup></p>
    <ul>
      <li>Technical or functional (expertise in the field(s) that they and the team are working in)</li>
      <li>Problem-solving</li>
      <li>Interpersonal</li>
    </ul>
    <p>While some people will be strong in all three areas, others will not. Be aware of the ways in which people will affect other team members and the community when choosing them for your team.</p>
