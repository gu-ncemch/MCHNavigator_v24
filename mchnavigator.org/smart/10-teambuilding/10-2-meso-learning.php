      <button class="accordion"><i class="mch-cogs"></i> <strong>10S4.</strong> Facilitate group processes for team-based decisions.</button>
  <div class="panel">
    <p>Research indicates that when leaders facilitate group decision-making, the outcomes are better than when they make decisions on behalf of the group. The balanced participation of group members is essential to effective decision-making.</p>
    <p><strong>Group Decision-Making Techniques</strong></p>
    <ul>
      <li><strong>Brainstorming:</strong> Used when creative ideas are needed. Ideas are written down as they are generated, and are not critiqued. Members meet for a separate session in which they evaluate the ideas and select what to act upon.<sup>11</sup><u> </u></li>
      <li><strong>Consensus:</strong> Used when it&rsquo;s important that every team member is heard. Consensus is reached through a process that involves clarifying the issue, discussing the issue thoroughly, reaching a group agreement in principle and resolving concerns until team members can comfortably commit to supporting the proposed course of action. <a href="https://youtu.be/NTjEqek1D5E">Learn more about the consensus process in this video</a>.<sup>9</sup></li>
      <li><strong>Multivoting:</strong> Used when there are a large number of possible ideas. Ideas are presented and numbered. Each group member numbers their top five (or up to 10) selected ideas; they start by assigning 5 to their top choice and work down to 1 for their fifth choice. Results are tallied for each idea by adding the numbers each group member has given it. The idea with the highest tally is the result that is favored by all, but not the top choice of any. <a href="https://asq.org/quality-resources/multivoting">Learn more about multivoting</a>.<sup>9</sup></li>
      <li><strong>Nominal Group Technique (NGT):</strong> A structured variation of a small-group discussion to reach consensus. Individuals respond to questions posed by the group leader by writing ideas on cards. These are collected, read and prioritized by the group. Multivoting may be used to determine the top idea/suggestion. This process prevents the domination of the discussion<br>
        by a single person and encourages all group members to participate regardless of status.<sup>10</sup> <a href="https://asq.org/quality-resources/nominal-group-technique?srsltid=AfmBOoqJowV2E6m3TFoHY2H6Hh7D1ixR1RVolzkVeaRzIVi0QY7zlRYV">Read more about NGT</a>.</li>
      <li><strong>Stepladder technique: </strong>A decision-making technique that prevents team members from dominating others while also eliciting equal amounts of participation from all team members, thus preventing social loafing. Two team members share ideas with each other, then are joined by a third, who must present their own ideas before hearing the initial pair&rsquo;s ideas. One by one, the remaining team members join, each repeating the process just described. After all ideas have been presented, the team makes a decision. Research indicates that groups using the stepladder technique produced higher-quality decisions.<sup>11</sup> <a href="http://www.restore.ac.uk/mrp/services/ldc/mrp/resources/peopleskills/stepdecmodel/">See more about the stepladder technique</a>.</li>
    </ul>
    <p>The above techniques can be useful when crafting and articulating a team&rsquo;s shared vision. </p>
    <p>The following steps can be useful for defining a shared vision:<sup>12</sup> </p>
    <ol>
      <li>Define the team&rsquo;s core values: These are its deeply held beliefs</li>
      <li>Establish the team purpose: Define why the team exists, and its impact</li>
      <li>Determine the team&rsquo;s mission: Define what the team is trying to achieve.</li>
      <li>Explain the team&rsquo;s strategy: How will the team put its mission into action? </li>
    <li>Set the team&rsquo;s goals: Team members should separate the strategy into smaller, attainable objectives to focus the team&rsquo;s work.</li></ol>
	<p>Trust and respect are the cornerstones of effective teamwork. Here are some key strategies to foster them:</p>
	 <p>Building Trust</p>
	  <ul><li>Open communication: Encourage honest and transparent dialogue.</li>
	  <li>Vulnerability: Create a safe space for team members to share their thoughts and feelings.</li>
      <li>Reliability: Consistently deliver on commitments and follow through on promises.</li>
      <li>Empathy: Show understanding and compassion for team members' perspectives.</li>
      <li>Consistency: Maintain fair and equitable treatment of all team members.</li>
      <li>Support: Provide assistance and encouragement to colleagues.</li></ul>
	<p>Building Respect</p>
	  <ul>
	  <li>Active listening: Pay full attention to others' viewpoints.</li>
      <li>Value Backgrounds: Appreciate different perspectives and backgrounds.</li>
      <li>Recognition: Acknowledge and celebrate individual contributions.</li>
      <li>Conflict resolution: Handle disagreements constructively and respectfully.</li>
      <li>Personal boundaries: Respect team members' personal space and time.</li></ul>
	<p>Collaboration and cooperation are also essential for team success. Here are some key strategies to promote these behaviors:</p>
	<p>Fostering Collaboration</p>
	  <ul>
	<li>Shared Goals: Ensure everyone understands and is aligned with the team's objectives.</li>
    <li>Open communication: Create channels for open and honest dialogue.</li>
    <li>Shared decision-making: Empower team members to contribute to decisions.</li>
    <li>Cross-functional teams: Bring together individuals with various skills and perspectives.</li>
    <li>Technology: Utilize collaboration tools to enhance communication and information sharing.</li></ul>
	<p>Fostering Cooperation</p>
	<ul>
	<li>Clear roles and responsibilities: Define expectations and boundaries.</li>
    <li>Mutual respect: Encourage appreciation for each team member's contributions.</li>
    <li>Conflict resolution: Establish effective strategies for managing disagreements.</li>
    <li>Team-building activities: Foster camaraderie and trust through shared experiences.</li>
    <li>Recognition: Celebrate team successes and individual contributions.</li></ul>
	</div>
    
    
 
