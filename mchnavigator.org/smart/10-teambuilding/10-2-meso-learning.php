 <button class="accordion"><i class="mch-paper-plane"></i> <strong>10S5.</strong>  Identify and redirect negative influences on team.</button>
  <div class="panel">
    <p>There are a number of forces that can negatively influence team dynamics. It is important that team leaders be able to identify and redirect these forces.<sup>13</sup> </p>
    <ul>
      <li><strong>Nonparticipating leadership</strong>: Team members do not use a leadership style that involves and engages team members.</li>
      <li><strong>Poor decision-making</strong>: Team members make decisions too quickly and do not use proven decision-making methods.</li>
      <li><strong>Infrequent communication</strong>: Communication is difficult and infrequent.</li>
      <li><strong>Varied Backgrounds not valued</strong>:The team fails to tap into the various  experiences and backgrounds of their fellow team members, which makes decision-making and solutions less successful.</li>
      <li><strong>Lack of mutual trust</strong>: Team members do not fully trust each other or the team as a whole.</li>
      <li>Inability to manage conflict: Not dealing with conflict openly and transparently and allowing grudges to build up can destroy team morale.</li>
      <li><strong>Lack of goal clarity</strong>: Team members are unsure of the team&rsquo;s ultimate goals, resulting in a lack of commitment and engagement.</li>
      <li><strong>Poorly defined roles and responsibilities</strong>: Team members are unclear about what they should do (and not do) to show that they are committed to the team and its success.</li>
      <li><strong>Relationship issues</strong>: Weak bonds between team members affect their efficiency and effectiveness.</li>
      <li><strong>Negative atmosphere</strong>: An overall team environment that is not open, transparent, positive and future-focused results in a failure to perform at high levels.</li>
    </ul>
    <p>Positively influence team dynamics by doing the following:<sup>14</sup> </p>
    <ul>
      <li><strong>Build a healthy workplace</strong>: Value an environment where people feel free to share their ideas.</li>
      <li><strong>Value various perspectives</strong>: Help people to understand that having and leveraging a  team with many perspectives will better allow the organization to achieve its objectives. </li>
      <li><strong>Value relationships</strong>: Trust, respect and camaraderie are essential to effective teams. Take steps to ensure your team builds strong relationships and deals with conflicts that arise. </li>
      <li><strong>Hire well</strong>: Prioritize that new hires&rsquo; values align with those of your organization. Only promote people to leadership roles when they consistently demonstrate the values and behaviors you want to encourage.  </li>
      <li><strong>Set clear behavioral expectations</strong>: Clearly articulate your core values and the behaviors you expect from everyone in your team.  </li>
      <li><strong>Hold people accountable</strong>: Set expectations and then follow through and hold people accountable to them.  </li>
      <li><strong>Resolve conflicts</strong>: Recognize when members of your team are struggling to relate to one another or move past disagreements. Encourage open, honest, respectful discussion about conflicts.</li>
    </ul>
  </div>
    
    <button class="accordion"><i class="mch-share"></i> <strong>10S6.</strong> Use shared outcomes to promote team synergy.</button>
  <div class="panel">
    <p>A shared outcome defines why a team comes together and is what they work toward each day. The outcome will help support their organization&rsquo;s mission; it is their contribution to the cause.<sup>15</sup>  <u> </u></p>
    <p>When a team works together toward a shared outcome that is significant and meaningful, it can inspire action, boost collaboration, and spur team members to leverage contacts and resources outside the team. Committing to a shared outcome can move team members to set aside their personal desires and agendas to focus instead on the team&rsquo;s agenda and goals. Similarly, team members will tolerate moving out of their comfort zones as they work toward a shared outcome.<sup>15</sup></p>
    <p>Characteristics of a strong shared outcome:<sup>15</sup><u> </u></p>
    <ul>
      <li><strong>Clear and credible</strong>: Team members can see the value of the work</li>
      <li><strong>Significant</strong>: It is something so big that it can only be achieved with team synergy, which allows a team to produce something beyond the sum of individual member contributions</li>
      <li><strong>Broad and open-ended</strong>: Outside resources and input will be required</li>
      <li><strong>Narrow and tangible</strong>: The outcome is defined enough that the team can find focus and make decisions </li>
      <li><strong>Meaningful</strong>: It connects to members&rsquo; values and identity</li>
    </ul>
    
  </div>
    
     <button class="accordion"><i class="mch-users"></i> <strong>10S7.</strong> Share leadership appropriately.</button>
  <div class="panel">
    <p>Shared leadership has been described as &ldquo;an emergent team property that results from the distribution of leadership influence across multiple team members.&rdquo;<sup>16</sup> It is a form of collectivist leadership, where leadership roles and responsibilities are shared, distributed or rotated amongst team members. Other forms of collectivist leadership include distributed, team, co-leadership, rotated and collective leadership.</p>
    <p>Shared leadership flattens an organization&rsquo;s hierarchy and disperses power, authority and decision-making ability. Each designated leader has authority in their area of expertise, and similarly defers that authority to others in areas in which they are not as expert.</p>
    <p>Suggestions for implementing shared leadership include:<sup>17</sup></p>
    <ul>
      <li>Select the people who are most qualified to make decisions on given topics</li>
      <li>Define the limits of each person&rsquo;s power to make decisions</li>
      <li>Create a work environment that encourages people to take the lead on projects</li>
      <li>Ensure that the people selected to make decisions have the autonomy and resources to do so, and feel empowered to use these freely</li>
      <li>Refrain from second-guessing decisions that people make</li>
      <li>Think of leaders as resources rather than managers</li>
      <li>Ensure that procedures are in place to track and review progress and amend the course of action if needed</li>
    </ul>
  
  </div>
    
     <button class="accordion"><i class="mch-wrench"></i> <strong>10S9.</strong> Adopt interdisciplinary tools/techniques/methods.</button>
  <div class="panel">
    <p>The ability to solve problems within one discipline by borrowing the tools, techniques and methods of others has long been recognized. &ldquo;There is scarcely an individual phenomenon or event in society with which we can deal adequately without knowing a great deal of several disciplines,&rdquo; the Nobel laureate economist and philosopher F.A. Hayek noted in the 1950s.<sup>18</sup></p>
    <p>One approach for working across disciplines suggests that professionals:<sup>19</sup></p>
    <ul>
      <li>Ensure a shared language exists among disciplines: Understand the terminology of the various disciplines involved at a functional, fluent level </li>
      <li>Ensure conceptual clarity: Be sure meanings of terms and understanding of concepts are consistent when moving between disciplines</li>
      <li>Understand research methodologies and how knowledge is created in different disciplines</li>
    </ul>
    <p>For interdisciplinary work the following abilities have been suggested for those wishing to address a real-world problem:</p>
    <ul>
      <li>Be able to occupy and understand different disciplinary perspectives</li>
      <li>Be able to critically evaluate knowledge from a broad range of disciplines</li>
      <li>Be able to engage in interdisciplinary inquiry and problem-solving, employing multiple ways of knowing</li>
      <li>Have a meta-disciplinary understanding of the nature of knowledge and the disciplines</li>
      <li>Be able to integrate, synthesize, balance and accommodate knowledge from multiple disciplines in order to produce something greater than would be possible from any one disciplinary perspective</li>
    </ul>
    <p>As one set of public health researchers noted in an editorial, working with and between disciplines requires constant explanation, adaptation and scientific readjustment from all of the individuals involved.<sup>20</sup> </p>

  </div>
    
     <button class="accordion"><i class="mch-compass3"></i> <strong>10S9.</strong> Use competencies and interdisciplinary knowledge.</button>
  <div class="panel">
    <p>As we&rsquo;ve discussed elsewhere in this curriculum, most of the disciplines that fall under the umbrella of MCH have their own sets of competencies that define the expectations of the field for practitioners. Similarly, each discipline has a role, formally or informally, within the MCH sphere, though roles may change depending on the situation. </p>
    <p>Because no single discipline can solve the complex issues that involve women, children and families, being able to work across disciplines is a necessary skill.<sup>21</sup></p>
