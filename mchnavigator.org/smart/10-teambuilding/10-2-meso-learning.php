      <p><strong>Citation.</strong> Buelow JR, Rathsack C, Downs D, Jorgensen K, Karges JR, Nelson D. Building interdisciplinary teamwork among allied health students through live clinical case simulations. J Allied Health. 2008 Summer; 37(2):e109-23. PMID: 19753390.</p>
      <p><strong>Abstract.</strong> A limited, yet growing, body of research suggests that health care students educated in interdisciplinary teamwork may become more collaborative professionals in the workplace, which, in turn, may foster more productive and satisfied health care professionals. Researchers also have identified lower mortality and morbidity rates, fewer hospitalizations, decreased costs, and improved function by patients among significant health benefits of interdisciplinary teamwork, especially when it is applied to underserved and geriatric populations. Such positive outcomes have prompted medical schools and accreditation boards of many allied health professions to add interdisciplinary education into their training requirements. Meeting these requirements has challenged universities, where there are multiple allied health programs and limited time, faculty, and financial resources to coordinate interdisciplinary education. The challenges have been magnified by insufficient research on the most effective methods to educate university students about interdisciplinary teamwork. This article presents the background, evolution, and key building blocks of one such method: a simulation-based workshop designed at our university over 7 years to educate its allied health students about various health professions through shared learning, interaction, and collaboration.</p>
