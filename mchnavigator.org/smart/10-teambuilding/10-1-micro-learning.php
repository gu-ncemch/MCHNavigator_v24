<div class="panel">
  <p>The MCH Leadership Competencies describe the necessary knowledge and skills (foundational and advanced) designed to support and promote MCH leadership. These areas will be further explored in the next section of the module.</p>
  <h3>KNOWLEDGE AREAS</h3>
  <p>MCH leaders will demonstrate a working knowledge of:</p>
  <ul>
    <li>The role of ID/IP teams in building stronger outcomes.</li>
    <li>Team building concepts, including stages of team development; practices that enhance teamwork; and management of team dynamics.</li>
  </ul>
  <h3>SKILLS</h3>
  <p><strong>Foundational. </strong>At a foundational level, MCH leaders will be able to:</p>
  <ul>
    <li>Accurately describe roles, responsibilities, and scope of practice of all members of the ID/IP team.</li>
    <li>Actively seek out and use input from people with varied experiences and perspectives in decision making processes.</li>
    <li>Identify and assemble team members with knowledge and skills appropriate to a given task.</li>
    <li>Facilitate group processes for team-based decisions, including articulating a shared vision, building trust and respect, and fostering collaboration and cooperation.</li>
  </ul>
<p><strong>Advanced</strong>. Building on the foundational skills, MCH leaders will be able to:    </p>
<ul>
  <li>Identify and redirect forces that negatively influence team dynamics.</li>
  <li>Use a shared vision of mutually beneficial outcomes to promote team synergy.</li>
  <li>Share leadership based on appropriate use of team member strengths in carrying out activities and managing challenges.</li>
  <li>Adopt tools, techniques, and methods from a range of disciplinary knowledge and practice bases to address challenges and meet needs.</li>
  <li>Use knowledge of competencies and roles for disciplines other than one's own to improve teaching, research, advocacy, and systems of care.</li>
</ul>
