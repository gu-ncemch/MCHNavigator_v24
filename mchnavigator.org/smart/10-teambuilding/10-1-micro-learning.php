<div class="evidenceBorder"><h3><i class="mch-compass-2"></i> <img src="../../images/smart-1-1-compass-3.jpg" alt="Image of Compass" class="right ppic" />RELATE. Linking to Related Concepts and Competencies</h3>
<p>This  competency relates to <strong>Evidence-Based Decision Making</strong>.</p>
<p>Evidence  provides the foundation for effective health transformation. Title V leaders  and staff should be able to design, deliver and improve selected evidence-based  interventions.</p>
<button class="accordion"><i class="mch-graduation-cap"></i> <strong>Learn More.</strong> Expanding on the related concept/competency.</button>
<div class="panel">
  <p>Generally speaking, the term &ldquo;evidence&rdquo; refers to information that has been systematically obtained in a manner that is replicable, observable, and verifiable. This type of evidence is often published in the scientific literature. However, there are other important domains of evidence to consider within the broad environmental and organizational context. To achieve the best possible health outcomes, the MCH workforce will need to examine experiential and contextual forms of evidence, drawing on the collective experiences and expertise of multiple partners while taking into consideration the unique characteristics of their own communities.</p>
  <p>In response to ever-tightening budgets, public health staffers need to ensure that the decisions they make about their programs, practices, and policies are based on sound, thoroughly evaluated evidence. Data-driven decision-making skills are essential as national and state health reforms continue to be implemented and Title V programs are held accountable for national performance measures.  </p>
  <p>Evidence Based Decision Making is a process that synthesizes the best available <em>research evidence</em> with <em>experiential evidence</em> from the field and relevant <em>evidence from the context</em>in which the program, policy or practice will take place. </p>
  <p>The decision-making process involves three steps:</p>
  <ul>
    <li>First, staffers must gather all of the forms of evidence I just named.</li>
    <li>Then, they need to interpret that evidence, which includes evaluating its strength and how well it fits the needs of the community. This step definitely should involve the input of local partners.</li>
    <li>Finally, your group will need to consider and prioritize all the evidence it has gathered and then come to a decision on the final content of the new program, practice or policy. This step should include deciding how you&rsquo;ll evaluate its effectiveness.</li>
  </ul>
  <p>As you might expect, strong leadership and critical-thinking skills are required throughout this process.</p>
  <p>The primary competencies needed for Evidence-Based Decision Making include:</p>
  <ul>
    <li>Action planning,</li>
    <li>Adapting interventions,</li>
    <li>Communicating research to policy makers, </li>
    <li>Economic evaluation, </li>
    <li>Designing evaluations (including qualitative and quantitative approaches), </li>
    <li>And prioritizing program and policy options.</li>
  </ul>
</div>
