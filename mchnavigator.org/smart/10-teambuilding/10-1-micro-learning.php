<h2><i class="mch-book-open"></i> 10.1.1 PROFICIENCY</h2>
<div class="evidenceBorder"><h3><i class="mch-search"></i> <img src="../../images/smart-1-1-magnifying-glass.jpg" alt="Image of Magnifying Glass" class="right ppic" />REVIEW. Summary of the Competency</h3>
<p>MCH systems are interdisciplinary/interprofessional (ID/IP) in nature. <em>ID/IP</em> practice provides a supportive environment in which the skills and expertise of team members from different disciplines, including a variety of professionals, MCH populations, and community partners, are acknowledged and seen as essential and synergistic. Input from each team member is elicited and valued in making collaborative, outcome-driven decisions to address individual, community-level, or systems-level problems.</p>
<p>Members of an ID/IP team may include a variety of professionals, MCH populations, family and self-advocate leaders, and community partners. The "team", which is the core of ID/IP practice, is characterized by mutual respect among partners, shared leadership, equal or complementary investment in the process, and acceptance of responsibility for outcomes.</p>
    
