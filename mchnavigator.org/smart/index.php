				<h2><i class="icon mch-telescope"></i> Conceptual Model</h2>
				<p>The MCH Leadership Competencies are organized within a conceptual framework based on the developmental progression of leadership.</p>

				<p>This framework can be visualized as a series of concentric circles that represent broadening spheres of influence. At the center is the “self,” where leadership is directed at one’s own learning. Moving outward, leadership in the next level extends to “others:” coworkers, colleagues, trainees, fellow students, and patients. The final, outermost circle represents the “wider community,” which can be entire organizations, systems, or general modes of practice.</p>

				<p>The MCH Leadership Competencies apply across these spheres of influence. Consequently, they are relevant to MCH professionals at every career stage, from entry level to executive.
					Because leadership is viewed as occurring at all levels in MCH, these competencies further form a road map to assist students and current/future faculty in gaining the knowledge and skills needed to make a difference to MCH populations.</p>
