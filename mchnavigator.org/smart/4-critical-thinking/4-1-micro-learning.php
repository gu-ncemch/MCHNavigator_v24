<div class="evidenceBorder"><h3><i class="mch-compass-2"></i> <img src="../../images/smart-1-1-compass-3.jpg" alt="Image of Compass" class="right ppic" />RELATE. Linking to Related Concepts and Competencies</h3>
<p>This competency relates to  <strong>1. Analytical/Assessment Skills </strong>and<strong> 7. Financial Planning and Management Skills </strong> of the <a href="http://www.phf.org/programs/corecompetencies/Pages/Core_Competencies_Domains.aspx">Core Competencies for Public Health Professionals</a>.</p>
<p>Core Competency 1: Analytical/Assessment Skills focus on understanding the foundation and prominent events of public health; applying public health sciences to practice; critiquing and developing research; using evidence when developing policies and programs; and establishing academic partnerships.</p>
<p>The public health sciences, which include epidemiology, biostatistics, and the environmental health sciences, create the evidence base that supports the programs and policies that public health professionals create, initiate, and administer.</p>
<p>Core Competency 7: Financial Planning and Management Skills focus on  factors that affect the health of an organization (e.g.,  fair treatment of employees, support from the governing body and community, sustainability of funding, training of managers); human resources (e.g., staff, interns, consultants, volunteers); professional development (e.g., training, mentoring, peer advising, coaching, drills, exercises); financial resources; organizational policies, programs, and services to achieve advanced health outcomes for all; managing programs and services; contingency planning (e.g., for emergencies, succession, cross-training staff, continuity of operations, economic downturns); critical thinking in decision making; engaging  individuals and teams to achieve program and organizational goals;  collaboration among individuals, groups, and organizations;  and performance management.</p>
<p>(Note: The Core Competencies are organized into eight domains, reflecting skill areas within public health, and three tiers, representing career stages for public health professionals: tier 1, front line/entry-level; tier 2, program management/supervisory; and tier 3, senior management/executive-level.)</p>
<button class="accordion"><i class="mch-graduation-cap"></i> <strong>Learn More.</strong> Expanding on the related concept/competency.</button>
<div class="panel">
  <p>It&rsquo;s important for personnel from all three tiers to understand how the public health sciences form this foundation for public health, and also to be familiar with the historical events that helped shape the field.</p>
  <p>The same is true for making sure that the public health sciences are used to deliver the 10 Essential Public Health Services, which describe the public health activities that all communities should undertake. Tier Two staffers are asked to apply public health sciences to the 10 Essentials, with support from those in the other tiers.</p>
  <p>For Core Competency 1: Analytical/Assessment Skills: </p>
  <p>Personnel in Tiers Two and Three  (front line/entry-level; program management/supervisory; and senior management/executive-level) are responsible for using public health sciences to administer and manage programs and organizations.</p>
  <p>All three staffing tiers are expected to contribute to the evidence base of public health, and most of the competencies in this domain address what is expected of each tier regarding research and evidence. For example, Tier 1 staffers <em>describe</em> the use of evidence in developing, implementing, evaluating, and improving policies, programs, and services; Tier 2 staffers <em>use</em> this evidence; and Tier 3 staffers <em>ensure</em> that evidence is used in these capacities. All three tiers play a role in conducting research ethically &mdash; for example, protecting patient confidentiality &mdash; however it falls to Tier 3 personnel to ensure that this actually happens.</p>
  <p>For Core Competency 7: Financial Planning and Management Skills:</p>
  <p>Personnel in Tier One contribute to implement financial planning an management skills needed for organizational infrastructure, programs, and services.</p>
  <p>Personnel in Tier Two help to determine and operationalize financial planning an management skills needed for organizational infrastructure, programs, and services.</p>
  <p>Personnel in Tier Three develop and oversee financial planning an management skills needed for organizational infrastructure, programs, and services.</p>
