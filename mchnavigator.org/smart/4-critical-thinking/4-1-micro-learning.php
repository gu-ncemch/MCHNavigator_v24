<h2><i class="mch-book-open"></i> 4.1.1 PROFICIENCY</h2>
<div class="evidenceBorder"><h3><i class="mch-search"></i> <img src="../../images/smart-1-1-magnifying-glass.jpg" alt="Image of Magnifying Glass" class="right ppic" />REVIEW. Summary of the Competency</h3>
<p>Complex challenges faced by MCH systems require critical thinking. <em>Critical thinking</em> is the ability to identify an issue or problem, frame it as a specific question, consider it from multiple perspectives, evaluate relevant information, and develop a resolution.</p>
<p><em>Evidence-informed decision-making</em> is the conscientious, explicit, and judicious use of current best evidence to guide practice, policy, and research. It is an advanced manifestation of critical thinking skills and develops a reasoned resolution.</p>
<p><em>Implementation science </em>is a vital component of critical thinking to promote the timely adoption and integration of evidence-informed practices, interventions, and policies.<sup>1</sup></p>
    <p><sup>1</sup> Adapted from NIH Fogarty International Center Implementation Science Information and Resources. Available at <a href="https://www.fic.nih.gov/researchtopics/pages/implementationscience.aspx" target="_blank">www.fic.nih.gov/researchtopics/pages/implementationscience.aspx</a></p>
