<div class="panel">
  <p>The MCH Leadership Competencies describe the necessary knowledge and skills (foundational and advanced) designed to support and promote MCH leadership. These areas will be further explored in the next section of the module.</p>
  <h3>KNOWLEDGE AREAS</h3>
  <p>MCH leaders will demonstrate a working knowledge of:</p>
  <ul>
    <li>The components of critical thinking: knowledge, comprehension, application, analysis, synthesis, and evaluation.</li>
    <li>Basic statistics, epidemiology, qualitative and quantitative research, systematic review, and meta-analysis.</li>
    <li>The types of evidence used in guidelines and recommendations for public health and clinical practice, including lived experience and community-generated knowledge.</li>
  </ul>
  <h3>SKILLS</h3>
  <p><strong>Foundational. </strong>At a foundational level, MCH leaders will:</p>
  <ul>
    <li>Evaluate various perspectives, sources of information, strengths and limitations of various approaches, and possible unintended consequences of addressing a clinical, organizational, community-based, or research challenge.</li>
    <li>Use population data, community input, and lived experience to determine the needs of a population for the purposes of designing programs, formulating policy, and conducting research or training.</li>
    <li>Demonstrate the ability to use a balanced perspective to critically analyze research, programs, and policies.</li>
  </ul>
<p><strong>Advanced</strong>. Building on the foundational skills, MCH leaders will be able to:    </p>
<ul>
  <li>Identify and propose promising practices and policies that can be used in situations where action is needed, but where the evidence base is not yet established.</li>
  <li>Present and discuss a rationale for policies and programs that is grounded in evidence and addresses the information needs of varied audiences.</li>
  <li>Use implementation science to analyze and translate evidence into policies and programs.</li>
  <li>Develop and apply evidence-informed practice guidelines and policies in their field.</li>
</ul>
