   <h3><span class="accordion"><strong><a name="k2" id="k2"></a>4K2.</strong> Statistics, epidemiology, research</span></h3>
   <p><strong>Applying What We Know</strong></p>
   <p>Health statistics are used to understand risk factors for communities, track and monitor diseases, see the impact of policy changes, and assess the quality and safety of health care. It is essential for MCH professionals to understanding how to find and interpret existing research as well as to conduct research of their own.</p>
   <ul>
     <li><a href="https://onlinecourses.science.psu.edu/statprogram/reviews/statistical-concepts">Basic Statistical Concepts</a> - A review from Penn State Eberly College of Science</li>
     <li><a href="https://hsl.lib.umn.edu/biomed/help/systematic-review#1">Conducting a Systematic Review</a> - The basic steps comprising a systematic review of literature that exists on a research question</li>
     <li>The <a href="https://www.cochranelibrary.com/">Cochrane Database of Systematic Reviews</a> is the leading journal and database for systematic reviews in health care. </li>
     <li><a href="https://ph.cochrane.org/">Cochrane Public Health</a> compiles systematic reviews of population level interventions that address the structural and social determinants of health, qualitative questions relevant to public health and more.</li>
     <li><a href="https://www.ncbi.nlm.nih.gov/pubmed/">PubMed</a> compiles citations for biomedical literature from Medline, life science journals, and online books. Links to full-text options are provided but may have a paywall. <a href="http://www.ovid.com/product-details.901.html">OVID Medline</a>, a subscription search service, allows users to perform focused searches of just Medline content (watch this video about <a href="https://himmelfarb.gwu.edu/tutorials/studydesign101/howtofind.cfm">how to find specific study designs and publication types in OVID Medline</a>).</li>
   </ul>
   <p><strong>Resources from the Field</strong></p>
   <ul>
     <li><a href="https://himmelfarb.gwu.edu/tutorials/studydesign101/">Study Design 101</a> - Learn the defining characteristics of the levels of evidence-based research.</li>
     <li><a href="https://www.mchlibrary.org/professionals/datadbs.php">Maternal and Child Health Data and Statistics Professional Resource Brief</a> compiles data sets, data tools, and statistics about infants, children, adolescents, pregnant women, and their families.</li>
     <li><a href="https://www.nlm.nih.gov/nichsr/stats_tutorial/cover.html">Finding and Using Health Statistics</a> - This online course describes different types of health statistics, how they are collected, and where they can be found.</li>
   </ul>
