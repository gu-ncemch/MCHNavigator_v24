<div class="panel">
  <p>The MCH Leadership Competencies describe the necessary knowledge and skills (foundational and advanced) designed to support and promote MCH leadership. These areas will be further explored in the next section of the module.</p>
  <h3>KNOWLEDGE AREAS</h3>
  <p>MCH leaders will demonstrate a working knowledge of:</p>
  <ul>
    <li>Government policy-making processes at the local, state/jurisdiction, and national levels. This involves understanding the intricate mechanisms through which policies are developed, debated, and implemented across different governmental tiers.</li>
    <li>Current public policies and private-sector initiatives that are especially relevant to MCH populations. This requires staying informed about existing regulations and programs that directly impact maternal and child health.</li>
    <li>Appropriate methods for informing and educating policymakers about the various needs of MCH populations and the impact of current policies on those populations. This includes developing effective communication strategies to advocate for meaningful policy changes.</li>
    <li>Strategies for organizational and public-facing communications regarding key MCH priorities. This involves creating clear, compelling narratives that highlight the critical importance of maternal and child health initiatives.</li>
  </ul>
  <h3>SKILLS</h3>
  <p><strong>Foundational. </strong>At a foundational level, MCH leaders will be able to:</p>
  <ul>
    <li>Frame problems based on key data that affect MCH populations, including epidemiological, economic, and other community and state/jurisdictional trends.</li>
    <li>Use available sources of evidence when assessing the effectiveness of existing policies or proposing policy change.</li>
    <li>Distinguish the roles and relationships of groups (executive, legislative, and judicial branches, as well as interest groups and community coalitions) involved in the public policy development and implementation process.</li>
  </ul>
  <p><strong>Advanced</strong>. Building on the foundational skills, MCH leaders will be able to:    </p>
<ul>
  <li>Apply appropriate evaluation standards and criteria to the analysis of alternative policies. </li>
  <li>Analyze the potential impact of policies on different MCH populations.</li>
  <li>Formulate strategies to balance the interests of various partners in ways that are consistent with MCH priorities.</li>
  <li>Effectively present evidence and information as a cohesively crafted MCH story to a legislative body, key decision makers, foundations, or the general public to inspire action.</li>
</ul>
