<h2><i class="mch-book-open"></i> 12.1.1 PROFICIENCY</h2>
<div class="evidenceBorder"><h3><i class="mch-search"></i> <img src="../../images/smart-1-1-magnifying-glass.jpg" alt="Image of Magnifying Glass" class="right ppic" />REVIEW. Summary of the Competency</h3>
<p>It is important for MCH leaders to possess policy skills, particularly in dynamic state and community environments. MCH leaders understand the resources necessary to improve health and well-being of mothers, children, and families, and the need to articulate those needs in the context of policy development and implementation at all levels.</p>
<p>A <em>public policy</em> is a law, regulation, procedure, administrative action, or voluntary practice of government that affects groups or populations and influences resource allocation.</p>
<p>Organizations also create policies to provide guidelines for decision-making processes. Clear policies contribute to increased transparency, accountability, and stability. They have both a direct and indirect impact on the MCH workforce and populations.</p>
    
