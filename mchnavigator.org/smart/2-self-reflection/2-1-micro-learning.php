<div class="evidenceBorder"><h3><i class="mch-compass-2"></i> <img src="../../images/smart-1-1-compass-3.jpg" alt="Image of Compass" class="right ppic" />RELATE. Linking to Related Concepts and Competencies</h3>
<p>This competency relates to <strong>Competency 8: Leadership and Systems Thinking Skills</strong> of the <a href="http://www.phf.org/programs/corecompetencies/Pages/Core_Competencies_Domains.aspx">Core Competencies for Public Health Professionals</a>.</p>
<p>Leadership and Systems Thinking Skills focus on incorporating ethical standards into the organization; creating opportunities for collaboration among public health, health care, and other organizations; mentoring personnel; adjusting practice to address changing needs and environment; ensuring continuous quality improvement; managing organizational change; and advocating for the role of governmental public health.</p>
<p>(Note: The Core Competencies are organized into eight domains, reflecting skill areas within public health, and three tiers, representing career stages for public health professionals: tier 1, front line/entry-level; tier 2, program management/supervisory; and tier 3, senior management/executive-level.)</p>
<button class="accordion"><i class="mch-graduation-cap"></i> <strong>Learn More.</strong> Expanding on the related concept/competency.</button>
<div class="panel">
  <p>This domain has to do with taking the long view on public health and your involvement in the field — from your career, to your organization and community, and even nationally and globally. </p>
  <p>The first competency in this domain encourages all three staffing tiers to have ethical standards of practice in place for all interactions with individuals, organizations, and communities. </p>
  <p>The next several competencies emphasize systems thinking. For example, Tier 1 and 2 staffers (front line/entry-level; program management/supervisory) should be able to describe public health as part of a larger, interrelated system of organizations that influence the health of populations at local, national, and global levels, and Tier 3 staffers (senior management/executive-level) should be able to interact with that system. </p>
  <p>Professional development is another topic this domain addresses. Tier 1 staffers should participate in professional development opportunities, and Tier 2 and 3 staffers should make sure that individuals and teams from across the organization are taking advantage of these opportunities.</p>
  <p>Change is an important factor to plan for, and the last few competencies in this domain address this. Tier 1 staffers should be able to describe the impact of social, political, and other changes on organizational practices, Tier 2 staffers should be ready to modify organizational practices in consideration of changes, and Tier 3 staffers should ensure that they can effectively manage organizational change.</p>
