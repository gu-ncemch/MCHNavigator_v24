    </ul>
  <p><strong>Advanced</strong>. Building on the foundational skills, MCH leaders will be able to:    </p>
  <ul>
    <li>Use self-reflection techniques to strengthen communication across program development and implementation, service delivery, clinical care, community collaboration, teaching, research, and scholarship.</li>
    <li>Seek and use feedback from peers and mentors to improve leadership practice.</li>
    <li>Apply understanding of one's own leadership style and sources of personal resilience to assemble and promote cohesive, well-functioning teams with perspectives and complementary styles.</li>
  </ul>
