	  <div class="evidenceBorder">
	  <h3><i class="mch-bookmarks"></i> <img src="../../images/smart-1-1-reflect.jpg" alt="Image of Thought Bubble" class="right ppic" />REFLECT. Staying Motivated</h3>
	  <p>As you work through your growth in this competency, it's important to stay motivated. Here are some tips to help you maintain focus and productivity along the way:</p>
      <ul>
        <li>        <strong>Take full responsibility for your own learning</strong>. When you take responsibility for your learning, you&rsquo;ll become more proactive. One way to bring about a change in this area is to ask yourself: &ldquo;If I&rsquo;m not responsible for my learning, then who is?&rdquo; Asking yourself this question will help you to realize that no one else is responsible for your learning.        </li>
        <li><strong>Use systems and routines</strong>. Focus on developing systems and routines, instead of continually thinking about what you want to accomplish.
        The problem with focusing too much on goals is that this doesn&rsquo;t enable you to connect your goals with the effort required.
        Success involves committing to a discipline and a routine, even when the going gets tough.</li>
        <li><strong>Be clear about your purpose</strong>. If you lack motivation, think about the purposes of education that are bigger than yourself. Remember that the purpose of education is less about you and more about the contribution you can make to society.          With a strong sense of purpose, you&rsquo;ll be more committed.</li>
      </ul>
