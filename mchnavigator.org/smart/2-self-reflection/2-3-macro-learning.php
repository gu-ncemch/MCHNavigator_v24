<h3>Implementation Model</h3>
	  <p>Self-reflection is the process of assessing the impact of personal values, beliefs, communication styles, background influences, and experiences on one's personal and professional leadership style.<sup>1</sup></p>
	  <p>The <strong>Five Practices of Exemplary Leaders</strong> leadership model developed by Kouzes and Posner<sup>2</sup> has been established and utilized in the literature over other leadership models because of its clarity, and the presumed adaptability of the practices to professionals early in their careers. The model posits that when leaders are at their personal best there are five core practices common to all: (1) Model the way, (2) Inspire a shared vision, (3) Challenge the process, (4) Enable others to act, and (5) Encourage the heart.</p>
    <p>This leadership model has proven itself in the literature as a clear, evidence-based path to achieving the extraordinary—for individuals, teams, organizations, and communities. It turns the abstract concept of leadership into easy-to-grasp practices and behaviors that can be taught and learned by anyone willing to accept the challenge to lead.  As measured and validated by the Leadership Practices Inventory (LPI)—one of the most widely used leadership assessment instruments in the world—ongoing studies consistently confirm that The Five Practices model is positively related to both the effectiveness of leaders and the level of commitment, engagement, and satisfaction of those that follow. </p>
