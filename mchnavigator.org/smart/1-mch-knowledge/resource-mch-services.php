   <h2><img src="../../images/smart-books.jpg" alt="Module icon" class="right border" width="600"/><i class="mch-compass3"></i> MCH Services Beyond DHHS</h2>
   <p>Both federal and state funds contribute to MCH-related programs. States also play an important role in administering these programs. The following is a summary of several of these key programs.   </p>
   <h3>Services through the Department of Agriculture</h3>
   <p><strong>National School Lunch Program/National School Breakfast Program</strong></p>
   <ul>
     <li>Meal program operating in public and nonprofit private schools and residential child care institutions. It provides nutritionally balanced, low-cost or free breakfasts and lunches to qualifying children each school day. The Food and Nutrition Service (FNS) of the United States Department of Agriculture (USDA) administers the program at the federal level. State agencies operate the program through agreements with school food authorities and ensure the programs are managed according to federal requirements.</li>
   </ul>
<p><strong>Special Supplemental Nutrition Program for Women, Infants, and Children (WIC)</strong></p>
<ul>
  <li>Provides federal grants to states for supplemental foods, health care referrals, and nutrition education for low-income pregnant, breastfeeding, and non-breastfeeding postpartum women, and to infants and children up to age five who are found to be at nutritional risk.</li>
</ul>
<h3>Services through the Department of Education</h3>
   <p><strong>IDEA</strong></p>
   <ul>
     <li>A law that makes available a free appropriate public education to eligible children with disabilities throughout the nation and ensures special education and related services to those children. The IDEA authorizes grants to states to support special education and related services and early intervention services and discretionary funding to state educational agencies, institutions of higher education, and other nonprofit organizations to support research, demonstrations, technical assistance and dissemination, technology development, personnel preparation and development, and parent-training and information centers.</li>
   </ul>
   <h3>Services through the Department of Justice</h3>
   <p>Programs cover a range of areas, from domestic violence prevention and response to children exposed to violence and trauma to youth intervention and mentoring, including:</p>
   <p><strong>Children's Advocacy Centers </strong></p>
   <ul>
     <li>Coordinate the investigation, treatment, and prosecution of child abuse cases by utilizing multidisciplinary teams of professionals involved in child protective and victim advocacy services, law enforcement and prosecution, and physical and mental health. </li>
   </ul>
<p><strong>Missing and Exploited Children Program</strong></p>
<ul>
  <li>Collects and distributes data regarding missing and exploited children and operates a national toll-free hotline.</li>
</ul>
<p><strong>Youth Mentoring Program</strong></p>
<ul>
  <li>Aims to increase opportunities for youth to have mentors and improve the quality and impact of the mentoring they receive.</li>
</ul>
<h3>Services through the Department of Labor</h3>
   <p>Programs and services govern employment and workplace protections for women, particularly pregnant women, children and youth, including youth with disabilities. These include:</p>
   <p><strong>Occupational Safety and Health Administration (OSHA) </strong></p>
   <ul>
     <li>Administers the Occupational Safety and Health (OSH) Act, which sets regulations for safety and health conditions in most private industries. OSHA-approved state programs cover public sector employers.</li>
   </ul>
<p><strong>Fair Labor Standards Act (FLSA) </strong></p>
<ul>
  <li>Prescribes standards for wages and overtime pay. Requires employers to provide reasonable break time for nursing mothers. Enforces child labor provisions designed to protect the educational opportunities of minors and prohibit their employment in jobs and under conditions detrimental to their health and well-being. </li>
</ul>
<p><strong>Family and Medical Leave Act (FMLA) </strong></p>
<ul>
  <li>Requires employers of 50 or more employees to give up to 12 weeks of unpaid, job-protected leave to eligible employees for the birth or adoption of a child or for the serious illness of the employee or a spouse, child or parent.</li>
</ul>
<p><strong>Office of Disability Employment Policy (ODEP) </strong></p>
<ul>
  <li>Non-regulatory federal agency that promotes policies and coordinates with employers and all levels of government to increase workplace success for people with disabilities. </li>
</ul>
<h3>Gaps in Services</h3>
   <p>Health care access and affordability are barriers for U.S. women. As a result, more than one-third of women in the U.S. report skipping needed medical care because of costs. This rate is higher than in other affluent nations.<sup>10</sup></p>
   <p>The maternal mortality rate in the U.S. is among the highest in the developed world, at 14 maternal deaths per 100,000 live births among women ages 15-49. Lack of prenatal care is a possible contributor to this high rate.<sup>10</sup> </p>
   <p>Maternal mortality rates also exhibit considerable racial, rural-urban, and other socioeconomic disparities.<sup>11</sup> (<a href="https://www.cdc.gov/mmwr/volumes/68/wr/mm6835a3.htm?s_cid=mm6835a3_w">See a recent report about racial/ethnic disparities in pregnancy-related death</a>.)</p>
   <p>Poor access to primary care in the United States had led to inadequate management and prevention of diagnoses and diseases, including hypertension, diabetes and heart disease, in women.<sup>10</sup></p>
   <p>Child welfare agencies report gaps in services to children and their caregivers in the areas of:<sup>12</sup></p>
   <ul>
     <li>Caregiver substance abuse services</li>
     <li>Caregiver domestic violence services</li>
     <li>Individualized Family Service Plan or Individualized Education Program for developmental problems in children ages 5 and under</li>
     <li>Caregiver mental health services</li>
     <li>Child and youth behavioral health services</li>
   </ul>
