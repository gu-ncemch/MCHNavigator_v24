<h3>Implementation Model</h3>
	  <p>The <strong>MCH Functions Framework</strong>, developed by the Johns Hopkins Child and Adolescent Health Policy Center and other collaborators, is comprised of three main components: </p>
	  <ol>
	    <li>A list of the ten public health services essential to promote maternal and child health.</li>
	    <li>An outline detailing program functions specific to MCH that apply to all levels of government and population.</li>
	    <li>Examples of local, state and federal activities for implementing MCH program functions.<sup>1</sup></li>
      </ol>
<p>The MCH Functions Framework can be used in many ways, but because it describes the substance of MCH work it is the perfect conceptual tool to use for classifying learning opportunities focused on implementing the MCH Leadership Competency #1: MCH Knowledge Base/Context.</p>
