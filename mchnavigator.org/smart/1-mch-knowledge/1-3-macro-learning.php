  <h3><i class="mch-compass-2"></i> <img src="../../images/smart-1-1-compass-3.jpg" alt="Image of Compass" class="right ppic" />RELATE. Strategies to Enable Effective Implementation</h3>

<p>In this section, reflect on how you can implement this competency in your daily work. To help with this process, review these <strong>five implementation strategies</strong>.</p>
<h3>Tiered Approach</h3>
<p>These implementation strategies follow a <strong>tiered approach of widening circles of influence</strong>. Through this approach, MCH leaders utilize resources and tools to activate change within their organization, which in turn incorporates partners through its systems of influence. Together, changes may be implemented to affect specific target populations and the MCH community in general. </p>
<p><img src="../../images/spheres-of-influence.png" alt="Implementation Model" width="1956" height="700"/></p>
<h3>Implementation Strategies</h3>

<button class="accordion"><i class="mch-light-bulb"></i> <strong>Self-Reflection Strategy. </strong>How to advance yourself as an MCH leader.</button>
<div class="panel">
  <p><strong>How to Advance Yourself as an MCH Leader</strong>. Identify your own personal strengths and areas of growth related to Competency 1, MCH Knowledge Base in a Public Health Context, by taking 5 minutes to register/log-in and answer questions related to this competency in the MCH Navigator's online <a href="http://mchnavigator.org/assessment/index.php">Self-Assessment</a>.</p>
</div>

<button class="accordion"><i class="mch-book"></i> <strong>Information Strategy. </strong>How to find and use tools.</button>
<div class="panel">
  <p><strong>How to Find and Use Tools</strong>. You can  utilize <a href="https://www.mchlibrary.org/toolkits/">Title V Toolkits</a>, <a href="https://www.mchlibrary.org/professionals/">Professional Resource Guides</a>, <a href="https://www.mchlibrary.org/schools/">School Resource Briefs</a>, and <a href="https://www.mchlibrary.org/families/">Family Materials</a> through the <a href="https://www.mchlibrary.org/">MCH Digital Library</a> at the National Center for Education in Maternal and Child Health (NCEMCH) &mdash; of special note is the <a href="https://www.mchevidence.org/">MCH Evidence Center</a> where you can find <a href="https://www.mchevidence.org/tools/">resources and tools</a> related to the National Performance Measures and evidence-based/informed strategies through the <a href="https://www.mchevidence.org/tools/strategies/">MCHbest</a> database. </p>
</div>

<button class="accordion"><i class="mch-university"></i> <strong>Organizational Strategy. </strong>How to activate your organization.</button>
<div class="panel">
  <p><strong>How to Activate Your Organization</strong>. Conduct a &quot;Lunch and Learn&quot; event in your department where staff who have been in MCH for a long time share their perspectives on where the field is going. Consider inviting colleagues who have retired to speak to the next generation of MCH professionals on how the field has changed.</p>
</div>

<button class="accordion"><i class="mch-cogs"></i> <strong>Systems Strategy. </strong>How to incorporate partners.</button>
<div class="panel">
  <p><strong>How to Incorporate Partners</strong>. Use this <a href="../../documents/Implementation-Strategies-Partners.pdf">Partners Wheel</a> to help identify natural partners for your organization/department to collaborate with in addressing the focus areas addressed by the NPMs. Use the graphic on page 1 of the document to help frame the 6 domains and prioritize the 15 NPM topics; then on pages 2-4, brainstorm and record partners (individuals, community groups, governmental agencies, MCHB-funded projects) that you currently work with or those who you would like to reach out to.</p>
</div>

<button class="accordion"><i class="mch-globe"></i> <strong>Community Strategy. </strong>Population- and individual-focused program synergy.</button>
<div class="panel">
  <p><strong>How  to  Engage Your Communities</strong>. Use the <a href="https://www.mchlibrary.org/professionals/community-services-locator.php">Community Services Locator</a> to develop a deep, working understanding of available services in your state or community. Use the links to national searches to identify education, health, mental health, family support, parenting, child care, and other services that can help children and families right in your  area. Use <a href="http://brightfutures.org/tools/BFtoolsWhere.pdf">Where to Seek Help: A Bright Futures Referral Tool for Providers</a> in conjunction with the Locator to help build a customized directory of community-based resources. Encourage families in your community to use these tools to learn about services that they can use.</p>
</div>
