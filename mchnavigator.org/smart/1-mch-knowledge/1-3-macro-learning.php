	  <div class="evidenceBorder">
	  <h3><i class="mch-bookmarks"></i> <img src="../../images/smart-1-1-reflect.jpg" alt="Image of Thought Bubble" class="right ppic" />REFLECT. Staying Motivated</h3>
	  <p>As you work through your growth in this competency, it's important to stay motivated. Here are some tips to help you maintain focus and productivity along the way:</p>
      <ul>
        <li><strong>Make a pact with yourself.</strong> It is a good technique for overcoming procrastination and getting things done on time. You can make deal with yourself, deal small or large. You simply say to yourself something like that when I’m done with this book’s chapter, I can take a walk in the playground and enjoy a chocolate. This kind of activity will certainly cheer you up and provide a needed motivation.</li>
        <li><strong>Shift the goals and objectives.</strong> Try to set a huge and detailed goal because this will motivate you much more than your smaller goals. A bigger goal has a big result and can produce a lot of motivation.</li>
        <li><strong>Do something small and generate a flow.</strong> Sometimes you simply need to get started by doing smaller things such as answering your emails or reviewing meeting minutes. Doing smaller tasks helps build momentum and gives you energy to tackle the big jobs ahead.</li>
      </ul>
