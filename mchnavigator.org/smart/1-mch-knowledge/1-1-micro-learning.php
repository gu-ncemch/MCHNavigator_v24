<h2><i class="mch-book-open"></i> 1.1.1 PROFICIENCY</h2>
<div class="evidenceBorder"><h3><i class="mch-search"></i> <img src="../../images/smart-1-1-magnifying-glass.jpg" alt="Image of Magnifying Glass" class="right ppic" />REVIEW. Summary of the Competency</h3>
<p>Maternal and child health (MCH) is a specialty area within the larger field of public health, distinguished by:</p>
<ul>
  <li>Promotion of the health and well-being of mothers, children, and families. Particular attention is directed to the MCH population domains: women and people who are pregnant or have given birth, infants, children, adolescents, young adults, fathers or other caregivers, and children and youth with special health care needs (CYSHCN).</li>
  <li>A focus on individuals as well as the families, communities, populations, and systems of care in communities that support and are accountable to these individuals.</li>
  <li>A life course perspective as an organizing framework that acknowledges distinct periods in human development and presents both risks and opportunities for interventions to make lasting improvements.</li>
</ul>
    
