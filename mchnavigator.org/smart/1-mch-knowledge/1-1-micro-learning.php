<div class="panel">
  <p>The MCH Leadership Competencies describe the necessary knowledge and skills (foundational and advanced) designed to support and promote MCH leadership. These areas will be further explored in the next section of the module.</p>
  <h3>KNOWLEDGE AREAS</h3>
  <p>MCH leaders will demonstrate a working knowledge of:</p>
  <ul>
    <li>MCH populations and the history and current structure of the key MCH programs serving these populations, including state Title V programs.</li>
    <li>The core values and strategic objectives with a special focus on:
      <ul>
        <li index="0">Prevention</li>
        </ul>
      <ul>
        <li index="1">Individuals and populations</li>
        </ul>
      <ul>
        <li index="2">Life course, including key transitions and intergenerational influences on health</li>
        </ul>
      <ul>
        <li index="3">Responsiveness to various backgrounds and perspectives</li>
        </ul>
      <ul>
        <li index="4">Partnerships with individuals with community expertise</li>
        </ul>
      <ul>
        <li index="5">Organizational/interagency partnerships</li>
        </ul>
      <ul>
        <li index="6">Community-based systems of services</li>
        </ul>
      <ul>
        <li index="7">Improving health outcomes for all groups</li>
        </ul>
      <ul>
        <li index="8">Evidence-informed practice (inclusive of research, contextual, and experiential evidence that can drive decision-making at all levels)</li>
        </ul>
      <ul>
        <li index="9">Community circumstances affecting health</li>
      </ul>
      </li>
    <li>The services available through major MCH programs and their limitations and gaps.</li>
    <li>The implications of policies, laws, and regulations that may affect MCH populations.</li>
    <li>The underlying principles of public health, population data collection, and analysis as well as the strengths, limitations, and utility of such data.</li>
    <li>The role of federal, state, and local government in ensuring healthcare for women, children, youth, families, and CYSHCN.</li>
    <li>The relationships between public health strategies and clinical strategies to promote health and well-being of mothers, children, and families.</li>
    <li>Understand the use of community generated evidence and how it can complement other forms of evidence.</li>
  </ul>
  <h3>SKILLS</h3>
  <p><strong>Foundational. </strong>At a foundational level, MCH leaders will:</p>
  <ul>
    <li>Describe MCH populations and provide examples of MCH programs, including Title V programs.</li>
    <li>Describe the utility of a systems approach in understanding how interactions between individuals, groups, organizations, and communities determine health outcomes.</li>
    <li>Use data to identify issues related to the health status of a particular MCH population group and use these to develop or evaluate policy.</li>
    <li>Describe community circumstances affecting health, understand approaches to improve health for all groups, and offer strategies to address health needs within MCH populations.</li>
    <li>Critically evaluate programs and policies for translation of evidence to practice.</li>
    <li>Understand the value of partnering with individuals with community expertise and family and community-led organizations to improve programs, policies, and practices.</li>
  </ul>
  <p><strong>Advanced</strong>. Building on the foundational skills, MCH leaders will:    </p>
  <ul>
    <li>Demonstrate the use of a systems approach to examine the interactions among individuals, groups, organizations, and communities.</li>
    <li>Assess the effectiveness of an existing program for specific MCH population groups.</li>
    <li>Ensure that responsiveness to unique needs is at the forefront of program planning and service delivery.</li>
  </ul>
