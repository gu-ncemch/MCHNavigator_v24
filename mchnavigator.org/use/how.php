<h2>How It Works</h2>
<p><strong>Are you looking for a structured learning experience? Do you need a refresher on standard MCH content and topics?</strong></p>
<p>No problem, check out the MCH Navigator’s Semi-Structured Learning resources. </p>
<p>We have developed: </p>
<ul>
  <li>Core MCH Training Bundles--trainings on key MCH and leadership topics such as MCH 101, Epidemiology and the MCH Planning Cycle.  </li>
  <li>Training Spotlights--in-depth portals to trainings on specific issues of importance to public health professionals such as Access to Care, and Children and Youth with Special Health Care Needs. </li>
  <li>Training Briefs--short lists of learning opportunities on MCH topics including Family Centered Care, and, Health and Lifecourse Theory. </li>
</ul>
<p>Learn about all of these topics and more!</p>
<p><strong>Do you know what you want to learn? Are you interested in a specific MCH or more general Public Health topic?</strong></p>
<p>The MCH Navigator’s Self-Directed Learning tools are just what you need. </p>
<p>We have created various mechanisms to help you find competency-based trainings that address the needs of the MCH workforce, students and community.  Use our:</p>
<ul>
  <li> Quick Finds option and learn based on your job function or need; explore on your own; or learn while on the go. </li>
  <li>Search Page with 3 easy ways to find information including searching the MCH Competencies or Public Health Competencies. </li>
  <li>A-Z Trainings List and more!</li>
  </ul>
<p><strong>Need ongoing, fast learning? Are you a busy public health professional?</strong></p>
<p>You need Fast-Interactive Learning.  </p>
<p>Try the MCH Navigator’s Microlearning Programs. Start with our 5-Minute MCH which explores the MCH Leadership Competencies through short, ongoing bursts of learning that encourage learners to proceed at their own pace, all while being engaged in a collaborative environment. For each competency, the program presents a 5-minute introduction, 5 learning opportunities, 5 implementation strategies, and a 15-minute summary discussion with an expert from the field.  Sign up today!</p>
<p><strong>Not sure of your training needs? Would you like to test your knowledge of the MCH Leadership Competencies?</strong></p>
<p>Sounds like you are looking for Self-Reflective Learning.  Take the MCH Navigator’s Online Self-Assessment. </p>
<p>Map your learning pathway using our 3-step process that can be used individually or part of a group: </p>
<ul>
  <li>Identify your strengths and learning needs,  </li>
  <li>Match your learning needs to appropriate trainings, and </li>
  <li>Receive a personalized learning plan. </li>
</ul>
<p>Register today!</p>
