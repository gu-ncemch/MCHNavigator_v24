<p>The National MCH Workforce Development Center hosted a webinar to introduce a comprehensive self-assessment tool, State Assessment​ Tool (SAT), that will help your Title V agency assess health reform-related activities in your state. The tool can help you clearly define your state's capacity and opportunities to leverage existing MCH skills to drive health transformation - in all its forms - for maternal and child health populations.</p>
<p>Iowa recently completed the tool and had this to say: &quot;This tool was helpful for us to think about how Iowa's Title V program interfaces with the health care reform activities going on in our state. It helped us identify areas of strength and provided direction on important next steps as we move MCH forward post ACA implementation. Hopefully, other states will use the tool to ignite conversations in their states.&quot; Center staff are available for consultation to assist you in completing the tool and examining results. </p>

