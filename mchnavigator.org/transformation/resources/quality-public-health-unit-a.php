<h3>​​​​​​​Unit A: Defining Quality and the Quality Continuum</h3>
<p>This module provides a description of key characteristics of quality, quality assurance, and quality improvement.  Explore different approaches (Lean, Six Sigma, etc.) that can be used in public health to improve quality and walk through examples that apply the concepts and tools.​</p>

  <p><strong>Learning Objectives</strong></p>
  <ul>
    <li>Describe characteristics of quality, including consistency, timeliness, stakeholder expectations, and technical specifications</li>
    <li>Compare Quality Assurance (QA)/Quality Control (QC) and Quality Improvement</li>
    <li>Explore methods and approaches to improve quality, including the PDSA Cycle, Lean Thinking, Six Sigma, Total Quality Management, and theories of Organizational Effectiveness</li>
    <li>Consider how quality methods may be applied in public health</li>
    <li>Describe the quality continuum, the performance management cycle, and open feedback systems​</li>
  </ul>
