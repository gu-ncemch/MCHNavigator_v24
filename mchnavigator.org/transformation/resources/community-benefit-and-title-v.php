<p> This fact sheet highlights the CHNA needs assessment process, potential opportunities for collaboration with the Title V, and a few state examples.</p>
<p>​The Patient Protection and Affordable Care Act (ACA) adds new provisions for 501(c)(3) nonprofit hospital organizations. Under this new provision, each hospital facility that plans to maintain its 501(c)(3) nonprofit status must fulfill the requirement of a community health needs assessment (CHNA). The CHNA is to be completed every three years beginning after Mar. 23, 2012. In essence, the CHNA is required to include an assessment of community health needs and implementation strategies on how the needs will be met. As a result of the ACA standardization of the CHNA process, there is potential for state Title V MCH programs to combine forces and amplify these efforts made by hospitals in their state.</p>
