<p>This video provides an overview of Health Impact Assessment (HIA). HIA is a valuable approach to build working relationships with partners outside of public health to improve health outcomes.</p>
<p>Building effective partnerships often requires public health practitioners to collaborate beyond their traditional networks. Health Impact Assessment (HIA) provides a structured method to develop productive working relationships with potential partners outside the public health sector to achieve better health outcomes. A newly released resource from the National MCH Workforce Development Center provides an overview of how the HIA process can benefit MCH professionals as they address key factors affecting maternal and child health.</p>
<p>The HIA framework helps teams identify opportunities for cross-sector collaboration, assess potential health impacts of programs and policies, develop evidence-based recommendations, create actionable implementation plans, and measure program effectiveness.</p>
