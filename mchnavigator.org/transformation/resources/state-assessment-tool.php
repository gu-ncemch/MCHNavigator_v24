<p> The State Assessment Tool provides a snapshot of your MCH program’s capacity to address and influence key areas of health reform affecting MCH and CYSHCN populations in your state.</p>
<p>Through the National MCH Workforce Development Center’s Access to Care Core, a State Assessment Tool (SAT or the Tool) was designed to help Title V programs conduct an environmental scan to better understand the landscape of health reform and health transformation at the state and local levels. The Tool includes key questions in five areas affected by health reform: </p>
<ul>
  <li>1) Eligibility and Enrollment</li>
  <li> 2) Coverage and Insurance Benefits </li>
  <li>3) Provider Networks and Network Adequacy </li>
  <li>4) Continuity of Care, and </li>
  <li>5) Partnerships between Title V, Medicaid, CHIP, Marketplaces and other key stakeholders. </li>
</ul>

<p>By having users assess the current state of health reform at the state level, this Tool can help Title V agencies identify gaps and potential action steps to influence movement in National Performance Measures (NPMs), including adolescent well-visit, well-woman visit, and adequacy of insurance, among others.​​</p>
<p>The Tool can accessed at <a href="http://syssci.renci.org/SAT" target="_blank">syssci.renci.org/SAT</a>. ​Once users set up an account, the instructions for completing and downloading the completed Tool are available on the website. Account information can be shared among multiple users in order to make completing the Tool a more collaborative process. </p>
