  <p>Children and youth with autism spectrum disorder and other developmental disabilities (ASD/DD) are a subpopulation of children and youth with special health care needs (CYSHCN). As with all CYSHCN, they require a comprehensive array of medical, behavioral and developmental health services that are often inadequately covered under the current system of health coverage. This leaves children at risk for missed or delayed care and their families with significant financial burdens. The Patient Protection and Affordable Care Act (ACA) is designed to increase health coverage, improve benefits, and provide important new insurance protections for all Americans. Many of the law’s provisions will impact children, including those with ASD/DD, and will be implemented over time. This report, developed by The Catalyst Center at the Boston University School of Public Health, offers a brief description of selected provisions in the ACA that have implications for CYSHCN with ASD/DD. It also describes how state Title V maternal and child health (MCH) programs can maximize opportunities under the ACA to develop and strengthen systems of care for CYSHCN with ASD/DD</p>
