<h3>​​Unit B: ​Quality Measurement</h3>
<p>This module provides key definitions and concepts for performance measurement.  Learn practical tips for selecting and using quality and performance measurement to effectively monitor system performance.  A step-by-step example illustrates the process and provides a reference for implementation.</p>

  <p><strong>Learning Objectives</strong></p>
  <ul>
    <li> Understand important quality measurement terms and concepts</li>
    <li>Apply the following practical measurement strategies:
      <ul>
        <li>Preserving the context</li>
        <li>Listening to the Voice of the Process</li>
        <li>Knowing when to bundle and unbundle data</li>
        <li>Using a balanced set of measures</li>
        <li>Differentiating types of measures and their uses</li>
        <li>Implementing a measurement system, not just measures</li>
      </ul>
    </li>
    </ul>
