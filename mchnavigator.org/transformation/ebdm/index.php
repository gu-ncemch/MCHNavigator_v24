			<p><strong>About the Action Framework</strong></p>
			<p>The rings on the periphery of the EBDM cycle represent the external system. The work of each element happens within and is affected by a fluid, complex environment. Therefore systems science and change management skills are central to the cycle described here. </p>
			<p>Innovations encompass any way of work (e.g. program, services, approach, etc.) that is new to the people who are involved with them. These innovations may be:</p>
			<ul>
				<li>Labelled as a program, a policy, a practice, an intervention, a strategy, or many others.</li>
				<li>Evidence-based or evidence-informed or newly developed and untested.</li>
			</ul>
			<p>The framework is dynamic in that there are many ways to use it. Some key considerations: </p>
			<ul>
				<li>We may ‘jump into the cycle’ at any point based on what our center clients bring to the table.</li>
				<li> Clients have varying levels of capacity and experience relevant to the cycle.</li>
				<li>Clients may not recognize their work as presented by the framework.</li>
				<li>Key partners (e.g. The Evidence Center) can be identified along the cycle.</li>
				<li>The EBDM framework should be understood as bridging multiple systems and sectors.</li>
				<li>Other frameworks overlap with and/or for within this cycle.</li>
			</ul>
