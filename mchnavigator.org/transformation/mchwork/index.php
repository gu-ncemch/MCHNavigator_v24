<p>​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​​<strong><img src="../../images/mchwork-2.png" alt="MCHwork" width="585" height="586" class="right border" /></strong>The National MCH Workforce Development Center (WDC) and the MCH Navigator have teamed up to share an innovative microlearning program, <strong>MCHwork</strong>, to actively engage busy professionals in the field on thematic topics over the course of five years. This ongoing  program draws upon the evidence-based theory of spaced microlearning to ensure that learning happens in small bursts at regular intervals to maximize the learning process. MCHwork addresses knowledge and skills related to some of the most pressing issues of our time, starting with improving health outcomes in year one and moving onto community partnerships, evaluation and assessment, aligning plans and strategy building, and communication in subsequent years.</p>
<p>Each quarter, a new skill related to the focus area will be shared via the MCH Navigator learning list, the <a href="../../connect/alert.php">MCHalert</a> newsletter, and personally through WDC contacts. These skills will be presented in a Ready-Set-Go approach to kickstart learning, dig deeper into related competencies and skill sets, and move to implementation through resources and tools collected by the <a href="https://www.mchlibrary.org">MCH Digital Library</a>.</p>
<p>We invite you to join us on this journey &#8212; you can register below to receive  quarterly messages or access archives through this page. We are excited to share new tools and approaches to help you in your daily &quot;MCH work.&quot; We look forward to a long, bright learning journey ahead!</p>
