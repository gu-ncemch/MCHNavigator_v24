    <p>Project faculty and  <a href="team.php#partners">advisory group</a> members utilize the  de Beaumont Foundation's <em><a href="../documents/DRAFT-PHWF-EL-Quality-Stand-2-23-16.pdf">Pubic Health Workforce Continuing and Professional Education (CPE) Quality E-Learning Standards</a> (1/20/16 draft)</em>, a set of 8 general standards adapted with permission from the <em>Quality Matters CPE Rubric</em>, and <a href="../documents/Quality Standards v1.0 FINAL PDF.pdf"><em>Quality Standards for Training Design and Delivery v. 1.0: Online Learning Edition</em></a>, developed in collaboration with the Public Health Learning Network, as a basis to vet learning opportunities. They then employ a <strong>three-pronged approach</strong> to determine whether  materials are accurate and applicable to MCH professionals and students in a variety of settings. </p>
