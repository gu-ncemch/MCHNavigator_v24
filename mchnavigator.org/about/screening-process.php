    <h2><i class="icon mch-thumbs-up" aria-hidden="true"></i> <i class="icon mch-thumbs-down" aria-hidden="true"></i> Three-Pronged Approach to Vetting</h2>
    <h3>1. Utilize Quality Protocols</h3>
    <article>
      <p>In addition to the de Beaumont Foundation's quality standards, use these protocol questions when vetting or creating learning opportunities:</p>
      <p><strong>Protocol questions to ask when linking to existing high-quality content: </strong></p>
    <ul class="list-arrow">
      <li>How does the training fit into MCH conceptual models, MCH topic areas, MCH Training Goals, and life course/social determinants theory? </li>
      <li>Who was the author; when was it produced (1–2 years old; 3–5 years old); what was the funding source; are there commercial partners? </li>
      <li>Is the training engaging for the learner? </li>
      <li>Does the training deal with data, epidemiology? </li>
      <li>Are leadership/management/communication topics addressed?</li>
      <li>What are the learning modalities of the training (mixed methodologies, synchronous or asynchronous, instructor or student feedback)? </li>
      <li>Are there existing reviews (professional or student) on the training? </li>
      <li>What is the level of training (introductory, intermediate, advanced), time required, and format of training?</li>
      <li>Is there credit offered? </li>
      <li>What is the reading level of the training? Is it Section 508 compliant? Is there a lengthy registration required?</li>
      <li>Are there related materials that would be useful for learners?</li>
    </ul>
    <p><strong>Protocol questions to ask when developing content where gaps exist: </strong></p>
    <ul class="list-arrow">
      <li>Is there a gap related to one or more MCH and/or Public Health competences? </li>
      <li>Does this gap appear in the current MCH literature or by identification of primary audiences? </li>
      <li>Is there commercially available content available through professional membership organizations (American Academy of Pediatrics or American Academy of Family Physicians) to adapt? Similar content material to adapt? </li>
      <li>What informational/non-training resources exist on the topic? </li>
      <li>Would an information brief on the topic fulfill need, just-in-time training, mini module? </li>
      <li>Could collaborative social media address the need, podcast, blog entry, MCH Wiki? </li>
      <li>Is there content expertise on staff for this training, among the MCH Faculty Learning Collaborative? Are other MCH Training grantees currently working on this topic? </li>
    </ul></article>
    <h3>2. Consider Special Criteria for Inclusion</h3>
    <article><p>Specific criteria used to identify learning opportunities for posting are as follows:</p>
    <p><strong>Content</strong></p>
    <ul class="list-arrow">
      <li>Are the presentations/learning objectives clearly articulated?</li>
      <li>Does course content support the presentation/learning objectives?</li>
      <li>Is the material presented current, accurate and relevant?</li>
      <li>Is the content applicable to a range of MCH professionals?&nbsp; If discussing particular examples of programs, does it inform the audience of its application in other settings?</li>
    </ul>
    <p><strong>Pedagogy</strong></p>
    <ul class="list-arrow">
      <li>Is the content (presentations, ideas, materials) well-organized and reasonably easy to follow (in an intuitive manner)?</li>
      <li>If there are resources to complement the materials presented, are they logically related, clear?</li>
    </ul>
    <p><strong>Presentation</strong></p>
    <ul class="list-arrow">
      <li>Is the video/audio of high quality?</li>
      <li>Does the presenter speak clearly and at a reasonable pace, are they understandable?</li>
      <li>Absence of technical problems, including those related to any registration process?</li>
    </ul></article>
    <h3>3. Include Preferred and Prioritized Learning Opportunities</h3>
    <article>
      <p>Pay particular attention to learning opportunities that highlight or include:</p>
      <ul class="list-arrow">
        <li>Engaging speaker</li>
        <li>Interactive components</li>
        <li>Additional resources, workbooks, exercises are included</li>
        <li>CE credits </li>
        <li>Availability in more than one language</li>
      </ul>
    </article>
