<h2><a name="ncemch" id="MCH-GU"></a><img src="../images/NCEMCH-projects.gif" alt="Georgetown University" width="355" height="385" class="right border" />NCEMCH at Georgetown University</h2>
    <p>Faculty and staff of this project are members of the <a href="http://www.ncemch.org">National Center for Education in Maternal and Child Health (NCEMCH)</a>. For over 40 years NCEMCH has provided leadership to the MCH community in 3 key areas: (1) systems-wide program development; (2) culturally competent, family-centered resources and research; and (3) evidence-based training and resource development to advance the knowledge and skills of the MCH workforce. NCEMCH puts knowledge about MCH issues to practical use for federal, state, and local public health professionals, policymakers, and the public. As such, NCEMCH&rsquo;s mission is perfectly aligned with the goals of this project. </p>
    <p>NCEMCH has maintained a productive relationship with MCHB and other partners and has founded and operated numerous national centers and data projects, including:</p>
