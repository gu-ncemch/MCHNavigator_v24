<h2 align="left"><img src="../images/work-blocks.jpg" alt="Work" width="300" height="200" class="right border" />Advisory Group, Funding, and Partners<a name="group"></a></h2>
    <p align="left">The MCH Navigator  benefits from a well-established network of MCH organizations that have served as long-time collaborators with NCEMCH. Project staff  tap these partners to engage a 24-plus-member Advisory Group  composed of experts from the field and academia. In addition, staff  continue to consult the NCEMCH Expert Panel, organizational partners, and experts in the field.</p>
    <h3 align="left">MCH Navigator Advisory Group</h3>
    <p>This Advisory Group consists of two levels: an outer ring of members from key MCH organizations who  provide strategic advice and an inner ring of 24 professionals who  serve as content reviewers. Both rings  include representation from the professional field and academic settings. </p>
    <p align="left"><strong><em>Outer Ring.</em></strong> This group includes MCH organizations representing the workforce (AMCHP, NMCHWDC, CityMatCH, and NCCPHT) and academics (ATMCH and the Centers of Excellence in MCH Education). Representatives from these six groups  assist in strategic planning across MCH Navigator objectives.</p>
    <p align="left"><strong><em>Inner Ring.</em></strong> This group consists of 24 members: one professional from the field and one professional in an academic setting for each of the 12 MCH Leadership Competencies. These professionals have been chosen based on their self-identified area of competency-based expertise and  serve as the prime vetting mechanism for  new trainings identified by the project. Project staff  send bundled groups of trainings to members of this group based on topic area. Group members are asked to review only materials that are aligned with their area of expertise. </p>
