  <li>Meeting with UAlbany MCH Catalyst, BU COE, and Berkeley COEs on community-partnered pedagogy (CPP).  The working definition we are using for CPP is: &quot;teaching and learning that engages faculty, students, and community (organizations or agencies) in mutually beneficial and respectful collaboration to address real world challenges in real time, and results in deliverables that can be used to address these challenges.&quot; CPP can be considered as a continuum of increasingly deeper engagement with the community partner in the teaching/learning endeavor. CPP is often also referred to as &ldquo;Practice-based Teaching&rdquo; (PBT). Over the next 12-18 months, we intend on developing 4 virtual workshops focused on CPP on different topics: Community (or needs) assessment, Program Development/Planning, Program Evaluation, and Leadership.  Our intention is to produce 4 virtual two hour workshops where faculty and hopefully community partners will present on how they have partnered on their courses focused on these topics, lessons learned (including mistakes made), and recommendations for others wanting to do the same. This would be the first hour.  The second hour would focus on discussion with attendees and really helping people to think about the &quot;nuts and bolts&quot; of this type of pedagogy and its implementation. 10/21/22.</li>
