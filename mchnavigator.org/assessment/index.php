        <div class="panel">
          <p>Self-assessment is considered to be a major component of learning in public health.<sup>1</sup> It provides an opportunity for health professionals to reflect on competency-based strengths and weaknesses in order to identify learning needs and reinforce new skills or behaviors in order to improve performance.&nbsp;</p>
          <p>The literature suggests that self-assessment is a prerequisite for maintaining professional competence,<sup>2</sup> and cites an increasing need for self-assessment in healthcare settings as health systems change.<sup>3</sup> Based on interviews and focus groups with persons who engaged in self-assessment, four types of benefits associated with self-assessment have also been identified in the literature:<sup>4</sup> </p>
          <ul>
            <li>Learning from experience</li>
            <li>Functioning more effectively</li>
            <li>Strengthening commitment to competent performance</li>
            <li>Fostering self-agency and authority</li>
          </ul>
          <p>With all of the important and critical benefits associated with self-assessment, <strong>it is not designed to be a short process</strong>. An extensive review of learner strengths and areas of improvement require multiple levels of validation and questions to ascertain.  Online self-assessment approaches offer the most efficient methods of completion and many other advantages including providing learners with a user-friendly and quick way to answer questions, streamlining the management of the assessment process (i.e., registration/log-in and tool layout pages etc.), and allowing data to be saved, kept confidential, and used for longitudinal data analysis. </p>
