<h2><i class="icon mch-map"></i> MCH Navigator&rsquo;s Self-Assessment Group Reports</h2>
<strong> We are happy to generate a customized report for your GROUP!</strong>
<p><strong><a href="reports-intro.php"><img src="../images/self-assessment-report.jpg" width="299" height="200" alt="This image contains a link to watch an introductory video about the reports" class="right border" /></a></strong>Self-assessment is considered to be a major component of learning in public health<sup>1</sup>. It provides an opportunity for health professionals to reflect on competency-based strengths and weaknesses in order to identify learning needs and reinforce new skills or behaviors in order to improve performance. </p>
<p>The MCH Navigator has been collecting data from our online Self-Assessment for close to ten years and generated a database with thousands of completed assessments.  During this time, we have collaborated with several universities  to provide customized self-assessment data reports for their students.  We would like to explain the process, show you examples, and offer this service to anyone nationwide who would like to benefit from this specialized reporting.</p>
<h2>How it Works</h2>
<p>We pull data from our database based on the client request.  If learners have taken the assessment more than once, we provide a pre/post analysis of mean changes in their knowledge and skills across the 12 MCH Leadership Competencies. If learners have only taken the assessment once, we provide a baseline report of their knowledge and skills across the 12 competencies.  There are two ways to request analysis. You can request:</p>
<ol>
  <li><strong>Analysis for a group</strong>—more than one learner email address (e.g. a groups of public health students, or colleagues in the field).</li>
  <li><strong>Individual analysis</strong>—You can access your personalized learning plan directly from the <strong>&quot;Personalized Learning Plan&quot; </strong>link at the left of this page. This report provides a short summary of overall understanding across competencies with customized learning opportunities for each sub-competency based on how the assessment questions were answered. You need to be logged in and have taken the self-assessment to receive your plan. You can either access electronically, print it out, or print it as a PDF.</li>
  </ol>
